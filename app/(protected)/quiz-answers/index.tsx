import React, { useMemo, useState } from 'react';

import { View, FlatList, ActivityIndicator, TextInput, StyleSheet } from 'react-native';

import { useSelector } from 'react-redux';

import PageHeaderWithActions from '../../../src/components/Shared/PageHeaderWithActions';
import TenantListItem from '../../../src/components/Tenants/TenantListItem';
import ExportButtons from '../../../src/features/questioner/components/ExportButtons';
import TemplateEditorModal from '../../../src/features/questioner/components/TemplateEditorModal';
import { notifySuccess } from '../../../src/lib/notifications';
import { FM } from '../../../src/localization/helpers';
import { useQuestionerWebCompletedQuestionersUpdate, useQuestionerWebCompletedQuestionersDelete } from '../../../src/server/autoGeneratedHooks/questioner/completedquestioners/completedquestioners';
import useCompletedQuestionersWithUsers, { type CompletedQuestionerWithUser } from '../../../src/server/customHooks/useCompletedQuestionersWithUsers';
import ThemeMode from '../../../src/shared/enums/ThemeMode';
import { layoutStyles, themePalette } from '../../../src/theme/utils/styles';
import { isValueDefined } from '../../../src/utils/is';
import { logger } from '../../../src/utils/logger';

import type { CompletedQuestionerDto } from '../../../src/server/autoGeneratedHooks/questioner/models/completedQuestionerDto';
import type { UpdateCompletedQuestionerRequest } from '../../../src/server/autoGeneratedHooks/questioner/models/updateCompletedQuestionerRequest';
import type { RootState } from '../../../src/store/reduxStore';

const screenStyles = StyleSheet.create({
  searchInput: { borderWidth: 1, padding: 8, borderRadius: 6, flex: 1 },
});

const QuizAnswersPage = (): React.ReactElement => {
  const theme = useSelector((s: RootState) => s.ui.theme);
  const colors = theme === ThemeMode.Dark ? themePalette.dark : themePalette.light;

  const updateMutation = useQuestionerWebCompletedQuestionersUpdate();
  const deleteMutation = useQuestionerWebCompletedQuestionersDelete();

  const [editingItem, setEditingItem] = useState<CompletedQuestionerDto | null>(null);
  const [isModalVisible, setIsModalVisible] = useState(false);
  const [isViewMode, setIsViewMode] = useState(false);

  // Use composed hook that returns completed questioners with optional user details
  const composed = useCompletedQuestionersWithUsers();
  const items: CompletedQuestionerWithUser[] = composed.data;

  // Simple client-side search state (searches name, username and email)
  const [search, setSearch] = useState('');
  const filteredItems = useMemo((): CompletedQuestionerWithUser[] => {
    const s = search.trim().toLowerCase();
    if (s.length === 0) return items;
    return items.filter((it) => {
      const name = (it.name ?? '').toLowerCase();
      const username = (it.user?.username ?? '').toLowerCase();
      const email = (it.user?.email ?? '').toLowerCase();
      return name.includes(s) || username.includes(s) || email.includes(s);
    });
  }, [items, search]);

  const colorStyles = useMemo(
    () => ({
      container: { backgroundColor: colors.background },
      searchInputBorder: { borderColor: colors.border, backgroundColor: colors.background },
      listItemBorder: { borderBottomColor: colors.border },
    }),
    [colors.background, colors.border],
  );

  // Handlers moved out of JSX
  function handleEdit(item: CompletedQuestionerDto): void {
    setEditingItem(item);
    setIsViewMode(false);
    setIsModalVisible(true);
  }

  function handleCancelEdit(): void {
    setIsModalVisible(false);
    setEditingItem(null);
    setIsViewMode(false);
  }

  function handleSaveEdit(payload: UpdateCompletedQuestionerRequest): void {
    const externalId = editingItem?.externalId;
    if (!isValueDefined(externalId) || externalId.length === 0) return;
    updateMutation.mutate(
      { externalId, data: payload },
      {
        onSuccess: () => {
          composed.refetch();
          setIsModalVisible(false);
          setEditingItem(null);
          try {
            notifySuccess(FM('quizAnswers.messages.updateSuccess'));
          } catch (_err) {
            logger.warn('QuizAnswersPage', 'notifySuccess failed');
          }
        },
      },
    );
  }

  function handleView(id: string): void {
    if (id.length === 0) return;
    const found = items.find((it) => it.externalId === id);
    if (!isValueDefined(found)) return;
    setEditingItem(found);
    setIsViewMode(true);
    setIsModalVisible(true);
  }

  function handleDelete(id: string): void {
    if (id.length === 0) return;
    deleteMutation.mutate(
      { externalId: id },
      {
        onSuccess: () => {
          composed.refetch();
          try {
            notifySuccess(FM('quizAnswers.messages.deleteSuccess'));
          } catch (_err) {
            logger.warn('QuizAnswersPage', 'notifySuccess failed');
          }
        },
      },
    );
  }

  return (
    <View style={[layoutStyles.container, colorStyles.container]}>
      <PageHeaderWithActions
        refreshing={composed.isLoading}
        refreshLabel={FM('common.refresh')}
        title={FM('quizAnswers.title')}
        onRefresh={() => {
          composed.refetch();
        }}
      />

      <View style={layoutStyles.formRow}>
        <TextInput
          accessibilityHint="Search quiz answers by name, username, or email"
          accessibilityLabel="Text input field"
          placeholder={FM('quizAnswers.searchPlaceholder')}
          style={[screenStyles.searchInput, colorStyles.searchInputBorder]}
          value={search}
          onChangeText={setSearch}
        />
      </View>

      <View style={layoutStyles.formRow}>
        <ExportButtons items={filteredItems} />
      </View>

      {composed.isLoading ? (
        <ActivityIndicator />
      ) : (
        <FlatList
          data={filteredItems}
          keyExtractor={(it, idx) => it.externalId ?? String(idx)}
          renderItem={({ item }) => (
            <View style={[layoutStyles.listItem, colorStyles.listItemBorder]}>
              <TenantListItem
                item={item}
                titleKey="name"
                translationNs="quizAnswers"
                onDelete={(id) => handleDelete(id)}
                onEdit={() => handleEdit(item)}
                onView={(id) => handleView(id)}
              />
            </View>
          )}
        />
      )}

      <TemplateEditorModal
        enableAnswerSection
        item={editingItem}
        readOnly={isViewMode}
        visible={isModalVisible}
        onCancel={handleCancelEdit}
        onSave={handleSaveEdit}
      />
    </View>
  );
};

export default QuizAnswersPage;
