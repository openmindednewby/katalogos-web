const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..', 'src', 'server', 'autoGeneratedHooks');
const SWAGGER = path.resolve(__dirname, '..', 'src', 'server', 'swagger.json');

function ensureDir(p) {
  if (!fs.existsSync(p)) fs.mkdirSync(p, { recursive: true });
}

function toPascalCase(input) {
  if (!input || typeof input !== 'string') return '';
  // split on non-alphanumeric and also try to split camel/lower boundaries
  const spaced = input
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2') // split camelCase
    .replace(/[^a-zA-Z0-9]+/g, ' ')
    .trim();
  return spaced
    .split(/\s+/)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join('')
    .replace(/[^a-zA-Z0-9]/g, '');
}

function singularize(name) {
  if (!name) return name;
  if (name.endsWith('ies')) return name.slice(0, -3) + 'y';
  if (name.endsWith('ses')) return name.slice(0, -2); // addresses 'menus' incorrectly sometimes
  if (name.endsWith('s') && name.length > 1) return name.slice(0, -1);
  return name;
}

// common verbs - longest first to avoid partial matches
const VERBS = ['GetById', 'ListAll', 'ActivateTemplate', 'Create', 'Delete', 'Update', 'List', 'Get'];

function detectVerb(operationId) {
  if (!operationId) return null;
  for (const v of VERBS) {
    if (operationId.endsWith(v)) return v;
    // also try case-insensitive match
    if (operationId.toLowerCase().endsWith(v.toLowerCase())) return v;
  }
  // fallback: try to find last capitalized word
  const m = operationId.match(/([A-Z][a-z0-9]+)$/);
  return m ? m[1] : null;
}

function normalizeTagName(tag) {
  // Known overrides for tags that don't split well automatically.
  const overrides = {
    tenants: 'Tenants',
    tenantmenus: 'TenantMenus',
    questionertemplates: 'QuestionerTemplates',
    completedquestioners: 'CompletedQuestioners',
  };
  if (!tag) return '';
  const key = tag.toLowerCase().replace(/[^a-z0-9]/g, '');
  if (overrides[key]) return overrides[key];
  return toPascalCase(tag);
}

function findGeneratedBundleForTag(tag) {
  // try to find the directory in ROOT that best matches tag (case-insensitive, strip non-alnum)
  const norm = (s) => (s || '').toLowerCase().replace(/[^a-z0-9]/g, '');
  const target = norm(tag);
  const entries = fs.readdirSync(ROOT, { withFileTypes: true }).filter((d) => d.isDirectory());
  for (const e of entries) {
    const candidate = norm(e.name);
    if (candidate === target) return path.join(ROOT, e.name);
  }
  // fallback: try includes
  for (const e of entries) {
    const candidate = norm(e.name);
    if (candidate.includes(target) || target.includes(candidate)) return path.join(ROOT, e.name);
  }
  // last fallback: return first directory that's not models
  for (const e of entries) {
    if (e.name.toLowerCase() === 'models') continue;
    return path.join(ROOT, e.name);
  }
  return null;
}

(function main() {
  console.log('Splitting Orval output (improved) in', ROOT);
  if (!fs.existsSync(SWAGGER)) {
    console.error('swagger.json not found at', SWAGGER);
    process.exit(1);
  }

  const swagger = JSON.parse(fs.readFileSync(SWAGGER, 'utf8'));
  const indexExports = new Set();

  // gather operations from swagger paths
  const ops = [];
  for (const [p, methods] of Object.entries(swagger.paths || {})) {
    for (const [method, op] of Object.entries(methods || {})) {
      if (!op || !op.operationId) continue;
      const tag = (op.tags && op.tags[0]) || 'Api';
      ops.push({ path: p, method, operationId: op.operationId, tag });
    }
  }

  // group by tag
  const byTag = {};
  for (const o of ops) {
    byTag[o.tag] = byTag[o.tag] || [];
    byTag[o.tag].push(o);
  }

  for (const [tag, operations] of Object.entries(byTag)) {
    const generatedDir = findGeneratedBundleForTag(tag);
    if (!generatedDir) {
      console.warn('No generated bundle dir found for tag', tag, ' — skipping');
      continue;
    }

    // pick a source file inside the generated dir to import from
    const tsFiles = fs.readdirSync(generatedDir).filter((f) => f.endsWith('.ts'));
    if (tsFiles.length === 0) {
      console.warn('No .ts files found in', generatedDir);
      continue;
    }
    // prefer index.ts or <dir>.ts
    let sourceFile = null;
    const idx = tsFiles.find((f) => f === 'index.ts');
    if (idx) sourceFile = path.join(generatedDir, idx);
    else {
      // try file with same base name as folder
      const baseCandidate = path.join(generatedDir, `${path.basename(generatedDir)}.ts`);
      if (fs.existsSync(baseCandidate)) sourceFile = baseCandidate;
      else sourceFile = path.join(generatedDir, tsFiles[0]);
    }

    const folderName = normalizeTagName(tag);
    const newTagDir = path.join(ROOT, folderName);
    ensureDir(newTagDir);

    for (const op of operations) {
      const operationId = op.operationId;
      const generatedHook = `use${operationId}`; // Orval typically uses 'use' + operationId
      const verb = detectVerb(operationId) || 'Call';
      const pascalTagSingular = singularize(folderName);
      const niceName = `use${verb}${pascalTagSingular}`;

      const relImport = path.relative(newTagDir, sourceFile).replace(/\\/g, '/').replace(/\.ts$/, '');
      let importPath = relImport;
      if (!importPath.startsWith('.')) importPath = `./${importPath}`;

      const filePath = path.join(newTagDir, `${niceName}.ts`);
      const fileContent = `// Auto-wrapper file: re-export from generated Orval bundle\nexport { ${generatedHook} as ${niceName} } from '${importPath}';\n`;
      fs.writeFileSync(filePath, fileContent, 'utf8');
      console.log('Wrote', filePath);
    }

    indexExports.add(`export * as ${folderName} from './${folderName}';`);
  }

  // write root index.ts
  const idxPath = path.join(ROOT, 'index.ts');
  const idxContent = Array.from(indexExports).join('\n') + '\n';
  fs.writeFileSync(idxPath, idxContent, 'utf8');
  console.log('Wrote', idxPath);
})();
