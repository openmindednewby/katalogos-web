/*
 Simple post-processing script for Orval-generated hooks.
 Heuristic: when a generated function calls `customInstance({...})` without an explicit generic,
 try to infer a response type from the `.././models` import list (prefer *Response or *Dto types)
 and insert the generic into the `customInstance<...>` call.

 This script is intentionally conservative: it only patches when a strong candidate is found.
*/

const fs = require('fs');
const path = require('path');

const hooksDir = path.resolve(__dirname, '..', 'src', 'server', 'autoGeneratedHooks');

function findModelImports(content) {
  const modelsImport = /import type \{([\s\S]*?)\}\s*from\s*"\.\/.\/models"/m.exec(content);
  if (!modelsImport) return [];
  const inner = modelsImport[1];
  return inner
    .split(',')
    .map((s) => s.trim())
    .filter(Boolean);
}

function wordsFromFnName(fnName) {
  // Split camelCase / PascalCase / snake/words
  return fnName
    .replace(/([a-z0-9])([A-Z])/g, '$1 $2')
    .replace(/[_\-/]/g, ' ')
    .split(/\s+/)
    .map((w) => w.toLowerCase())
    .filter(Boolean);
}

function pickCandidateModel(models, fnName) {
  if (!models || models.length === 0) return null;
  const candidates = models.filter((m) => /Response$|Dto$|Result$|Model$/i.test(m));
  const words = wordsFromFnName(fnName);

  // Prefer names that contain multiple tokens from fnName
  let best = null;
  let bestScore = 0;
  const consider = candidates.length ? candidates : models;

  for (const m of consider) {
    const low = m.toLowerCase();
    let score = 0;
    for (const w of words) {
      if (w.length < 3) continue;
      if (low.includes(w)) score += 1;
    }
    // small boost if it ends with Response/Dto
    if (/Response$|Dto$/i.test(m)) score += 1;
    if (score > bestScore) {
      best = m;
      bestScore = score;
    }
  }

  // require at least one matched token or a Response/Dto fallback
  if (bestScore > 0) return best;
  return null;
}

function processFile(filePath) {
  let content = fs.readFileSync(filePath, 'utf8');
  const models = findModelImports(content);
  if (!models || models.length === 0) return false;

  // Find function definitions that return customInstance
  // Pattern: export const <fnName> = ( paramList ) => { ... return customInstance(
  const funcRegex = /export const\s+([A-Za-z0-9_]+)\s*=\s*\(([^)]*)\)\s*=>\s*\{[\s\S]*?return\s+customInstance\s*\(/g;
  let match;
  let changed = false;
  const toReplace = [];
  while ((match = funcRegex.exec(content))) {
    const fnName = match[1];
    const paramList = match[2] || '';
    const startIndex = match.index + match[0].lastIndexOf('return');
    // find the exact customInstance call starting position from startIndex
    const callPos = content.indexOf('customInstance', startIndex);
    if (callPos === -1) continue;
    // Check if customInstance already has a generic: customInstance<
    const afterName = content.slice(callPos + 'customInstance'.length, callPos + 'customInstance'.length + 2);
    if (afterName.startsWith('<')) continue; // already has generic

    // Infer response candidate from imported models (prefer Response/Dto)
    const respCandidate = pickCandidateModel(models, fnName);

    // Infer request/query types from the function parameter list
    const paramTypeRegex = /:\s*([A-Za-z0-9_<>\[\]]+)/g;
    const paramTypes = [];
    let pMatch;
    while ((pMatch = paramTypeRegex.exec(paramList))) {
      const t = pMatch[1];
      if (t && !['AbortSignal', 'signal'].includes(t)) paramTypes.push(t);
    }

    // Choose request type (prefer names ending with Request/Dto or containing Create/Update)
    let reqCandidate = null;
    for (const t of paramTypes) {
      if (/Request$|Dto$|Create|Update|Body/i.test(t)) {
        reqCandidate = t;
        break;
      }
    }
    if (!reqCandidate && paramTypes.length) {
      // fallback to first param type that's not AbortSignal
      reqCandidate = paramTypes[0];
    }

    // Choose query type (params/query keywords)
    let qryCandidate = null;
    for (const t of paramTypes) {
      if (/Query$|Params$|Filter$/i.test(t) || /params?/i.test(t)) {
        qryCandidate = t;
        break;
      }
    }

    // If no resp candidate found, try a name-based heuristic
    const resp = respCandidate || pickCandidateModel(models, fnName) || null;

    if (!resp && !reqCandidate && !qryCandidate) continue; // nothing to infer

    // Build generic insertion string depending on available types
    const generics = [];
    if (resp) generics.push(resp);
    if (reqCandidate) generics.push(reqCandidate);
    if (qryCandidate) generics.push(qryCandidate);

    const insertion = `<${generics.join(', ')}>`;
    const insertionPoint = callPos + 'customInstance'.length;
    toReplace.push({ pos: insertionPoint, insert: insertion });
  }

  // Apply replacements backwards
  if (toReplace.length) {
    toReplace.sort((a, b) => b.pos - a.pos);
    for (const r of toReplace) {
      content = content.slice(0, r.pos) + r.insert + content.slice(r.pos);
      changed = true;
    }
  }

  if (changed) {
    fs.writeFileSync(filePath, content, 'utf8');
    console.log('Patched', filePath);
  }
  return changed;
}

function walk(dir) {
  const items = fs.readdirSync(dir);
  for (const item of items) {
    const p = path.join(dir, item);
    const st = fs.statSync(p);
    if (st.isDirectory()) walk(p);
    else if (st.isFile() && p.endsWith('.ts')) {
      try {
        processFile(p);
      } catch (err) {
        console.error('Error processing', p, err);
      }
    }
  }
}

if (!fs.existsSync(hooksDir)) {
  console.warn('Orval hooks directory not found:', hooksDir);
  process.exit(0);
}

walk(hooksDir);
// Ensure each tag folder has an index.ts barrel so top-level imports like `export * as Tenants from './Tenants'` resolve.
const entries = fs.readdirSync(hooksDir, { withFileTypes: true });
for (const e of entries) {
  if (!e.isDirectory()) continue;
  if (e.name === 'models') continue;
  const folder = path.join(hooksDir, e.name);
  const tsFiles = fs.readdirSync(folder).filter((f) => f.endsWith('.ts'));
  if (tsFiles.length === 0) continue;
  // create index.ts that re-exports all files
  const idxPath = path.join(folder, 'index.ts');
  const exports = tsFiles
    .filter((f) => f !== 'index.ts')
    .map((f) => `export * from './${f.replace(/\.ts$/, '')}';`)
    .join('\n') + '\n';
  try {
    fs.writeFileSync(idxPath, exports, 'utf8');
    console.log('Wrote folder barrel', idxPath);
  } catch (err) {
    console.error('Failed writing barrel for', folder, err);
  }
}
console.log('Orval patch script finished.');
