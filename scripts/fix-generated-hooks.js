/**
 * Post-processing script for Orval-generated hooks.
 *
 * Fixes React Query v5 compatibility issues where Orval generates code
 * that passes queryClient as a second argument to useQuery/useMutation,
 * which is deprecated in React Query v5.
 *
 * This script is run automatically after hooks generation.
 */
const fs = require('fs');
const path = require('path');

const HOOKS_DIR = path.join(__dirname, '..', 'src', 'server', 'autoGeneratedHooks');

function fixFile(filePath) {
  let content = fs.readFileSync(filePath, 'utf8');
  const originalContent = content;

  // Fix useQuery calls - remove queryClient second parameter
  content = content.replace(/useQuery\(queryOptions, queryClient\)/g, 'useQuery(queryOptions)');

  // Fix useMutation calls - remove queryClient second parameter
  content = content.replace(/useMutation\(mutationOptions, queryClient\)/g, 'useMutation(mutationOptions)');

  if (content !== originalContent) {
    fs.writeFileSync(filePath, content);
    console.log(`  Fixed: ${path.relative(HOOKS_DIR, filePath)}`);
    return true;
  }
  return false;
}

function walkDir(dir) {
  let fixedCount = 0;
  const files = fs.readdirSync(dir);

  for (const file of files) {
    const filePath = path.join(dir, file);
    const stat = fs.statSync(filePath);

    if (stat.isDirectory()) {
      fixedCount += walkDir(filePath);
    } else if (file.endsWith('.ts')) {
      if (fixFile(filePath)) {
        fixedCount++;
      }
    }
  }

  return fixedCount;
}

console.log('Fixing React Query v5 compatibility in generated hooks...');

if (!fs.existsSync(HOOKS_DIR)) {
  console.log('  No hooks directory found, skipping.');
  process.exit(0);
}

const fixedCount = walkDir(HOOKS_DIR);

if (fixedCount > 0) {
  console.log(`  ✅ Fixed ${fixedCount} file(s)`);
} else {
  console.log('  ✅ No fixes needed');
}
