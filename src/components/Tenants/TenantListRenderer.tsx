import React from 'react';

import { ActivityIndicator, FlatList, View } from 'react-native';

import TenantForm from './TenantForm';
import TenantListItem from './TenantListItem';
import { FM } from '../../localization/helpers';
import { TestIds } from '../../shared/testIds';
import { layoutStyles } from '../../theme/utils/styles';
import { isValueDefined } from '../../utils/is';
import EmptyListState from '../Shared/EmptyListState';


import type { TenantFormPayload } from '../../hooks/useTenantActions';
import type { IdentityServiceAPITenantsTenantDto } from '../../server/autoGeneratedHooks/identity/models/identityServiceAPITenantsTenantDto';

/** Default OTP code length (number of digits) */
const DEFAULT_OTP_CODE_LENGTH = 6;
/** Default OTP expiry time in minutes */
const DEFAULT_OTP_EXPIRY_MINUTES = 5;

interface TenantListRendererProps {
  tenants: IdentityServiceAPITenantsTenantDto[];
  isLoading: boolean;
  editingId: string | null;
  isUpdating: boolean;
  listItemBorderColor: string;
  onDelete: (id: string) => void;
  onStartEdit: (id: string) => void;
  onCancelEdit: () => void;
  onUpdate: (tenantId: string, payload: TenantFormPayload) => void;
}

/**
 * Renders the tenant list with inline editing support.
 */
export const TenantListRenderer: React.FC<TenantListRendererProps> = ({
  tenants,
  isLoading,
  editingId,
  isUpdating,
  listItemBorderColor,
  onDelete,
  onStartEdit,
  onCancelEdit,
  onUpdate,
}) => {
  if (isLoading)
    return (
      <ActivityIndicator
        accessibilityHint={FM('loading')}
        accessibilityLabel={FM('loading')}
        accessibilityRole="progressbar"
        testID={TestIds.LOADING_INDICATOR}
      />
    );

  return (
    <FlatList
      data={tenants}
      keyExtractor={(it, idx) => it.tenantId ?? String(idx)}
      ListEmptyComponent={<EmptyListState messageKey="tenants.emptyList" testID={TestIds.EMPTY_LIST_STATE} />}
      renderItem={({ item }) => (
        <View style={[layoutStyles.listItem, { borderBottomColor: listItemBorderColor }]}>
          {isValueDefined(editingId) && editingId === item.tenantId ? (
            <TenantForm
              initialAllowEmailAuth={item.allowEmailAuth ?? false}
              initialAllowPhoneAuth={item.allowPhoneAuth ?? false}
              initialName={item.name ?? ''}
              initialOtpCodeLength={item.otpCodeLength ?? DEFAULT_OTP_CODE_LENGTH}
              initialOtpExpiryMinutes={item.otpExpiryMinutes ?? DEFAULT_OTP_EXPIRY_MINUTES}
              initialPrimaryAuthMethod={item.primaryAuthMethod ?? 0}
              initialRequireSmsVerification={item.requireSmsVerification ?? true}
              initialSmsProvider={item.smsProvider ?? null}
              initialStatus={typeof item.tenantStatus === 'number' ? Number(item.tenantStatus) : 1}
              saving={isUpdating}
              onCancel={onCancelEdit}
              onSave={(payload) => {
                if (isValueDefined(editingId)) 
                  onUpdate(editingId, payload);
                
              }}
            />
          ) : (
            <TenantListItem
              idKey="tenantId"
              item={item}
              onDelete={onDelete}
              onEdit={onStartEdit}
            />
          )}
        </View>
      )}
    />
  );
};
