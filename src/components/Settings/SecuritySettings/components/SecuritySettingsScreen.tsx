/**
 * Security Settings Screen.
 * Combines change-password form and active-sessions list.
 * Wired to sessions API via Orval hooks.
 */
import React, { useCallback } from 'react';

import { ActivityIndicator, ScrollView, StyleSheet, Text, View } from 'react-native';

import { useQueryClient } from '@tanstack/react-query';

import ChangePasswordForm from './ChangePasswordForm';
import SessionItem from './SessionItem';
import { notifyError, notifySuccess } from '../../../../lib/notifications';
import { FM } from '../../../../localization/helpers';
import {
  getIdentityServiceAPIMeListSessionsQueryKey,
  useIdentityServiceAPIMeListSessions,
  useIdentityServiceAPIMeRevokeSession,
} from '../../../../server/autoGeneratedHooks/identity/me/me';
import { TestIds } from '../../../../shared/testIds';
import { useTheme } from '../../../../theme/hooks/useTheme';
import Breadcrumb from '../../../Shared/Breadcrumb';
import Heading from '../../../Shared/Heading';
import Section from '../../../Shared/Section';
import {
  SECTION_SPACING,
  TITLE_FONT_SIZE,
  DESCRIPTION_FONT_SIZE,
  MEDIUM_SPACING,
  BODY_FONT_SIZE,
  ERROR_TEXT_MARGIN_TOP,
} from '../constants';

const TITLE_FONT_WEIGHT = '600' as const;

const styles = StyleSheet.create({
  container: { flex: 1 },
  scrollContent: { padding: SECTION_SPACING },
  loadingContainer: { flex: 1, justifyContent: 'center', alignItems: 'center' },
  errorText: { fontSize: BODY_FONT_SIZE, textAlign: 'center', marginTop: ERROR_TEXT_MARGIN_TOP },
  sectionTitle: {
    fontSize: TITLE_FONT_SIZE,
    fontWeight: TITLE_FONT_WEIGHT,
  },
  emptyText: {
    fontSize: DESCRIPTION_FONT_SIZE,
    fontStyle: 'italic',
    marginTop: MEDIUM_SPACING,
  },
  sectionGap: { marginTop: SECTION_SPACING },
});

const SecuritySettingsScreen = (): React.ReactElement => {
  const { theme } = useTheme();
  const { colors } = theme;
  const primary = theme.palette.primary['500'];
  const errorColor = theme.semantic.error['500'];

  const {
    data: sessionsData,
    isLoading,
    isError,
  } = useIdentityServiceAPIMeListSessions();

  const revokeSession = useIdentityServiceAPIMeRevokeSession();
  const queryClient = useQueryClient();

  const handleRevoke = useCallback(
    (sessionId: string) => {
      revokeSession.mutate(
        { sessionId },
        {
          onSuccess: () => {
            queryClient.invalidateQueries({
              queryKey: getIdentityServiceAPIMeListSessionsQueryKey(),
            }).catch(() => {
              // Ignore invalidation errors
            });
            notifySuccess(FM('settings.security.messages.sessionRevoked'));
          },
          onError: () => {
            notifyError(FM('settings.security.messages.sessionRevokeError'));
          },
        },
      );
    },
    [revokeSession, queryClient],
  );

  const sessions = sessionsData?.sessions ?? [];
  const hasNoSessions = !isLoading && !isError && sessions.length === 0;

  return (
    <View
      style={[styles.container, { backgroundColor: colors.background }]}
      testID={TestIds.SECURITY_SETTINGS_SCREEN}
    >
      <ScrollView contentContainerStyle={styles.scrollContent}>
        <Breadcrumb />
        <Heading>{FM('settings.security.title')}</Heading>

        <Section>
          <ChangePasswordForm />
        </Section>

        <View style={styles.sectionGap}>
          <Section>
            <Text style={[styles.sectionTitle, { color: colors.text }]}>
              {FM('settings.security.activeSessions')}
            </Text>

            {isLoading ? (
              <View style={styles.loadingContainer} testID={TestIds.SECURITY_SETTINGS_LOADING}>
                <ActivityIndicator color={primary} size="small" />
              </View>
            ) : null}

            {isError ? (
              <View testID={TestIds.SECURITY_SETTINGS_ERROR}>
                <Text style={[styles.errorText, { color: errorColor }]}>
                  {FM('settings.security.messages.loadError')}
                </Text>
              </View>
            ) : null}

            {hasNoSessions ? (
              <View testID={TestIds.SECURITY_SESSIONS_LIST}>
                <Text style={[styles.emptyText, { color: colors.textSecondary }]}>
                  {FM('settings.security.noSessions')}
                </Text>
              </View>
            ) : null}

            {sessions.length > 0 ? (
              <View testID={TestIds.SECURITY_SESSIONS_LIST}>
                {sessions.map((session) => (
                  <SessionItem
                    key={session.id}
                    isPending={revokeSession.isPending}
                    session={session}
                    onRevoke={handleRevoke}
                  />
                ))}
              </View>
            ) : null}
          </Section>
        </View>
      </ScrollView>
    </View>
  );
};

export default SecuritySettingsScreen;
