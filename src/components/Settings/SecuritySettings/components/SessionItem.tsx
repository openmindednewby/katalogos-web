/**
 * Session Item.
 * Renders a single active session row with IP address, last access time,
 * client info, and a revoke button.
 */
import React, { useCallback } from 'react';

import { StyleSheet, Text, View } from 'react-native';

import { FM } from '../../../../localization/helpers';
import { TestIds } from '../../../../shared/testIds';
import { useTheme } from '../../../../theme/hooks/useTheme';
import { isValueDefined } from '../../../../utils/is';
import { Button } from '../../../core/Button';
import ButtonVariant from '../../../core/Button/utils/ButtonVariant';
import {
  DESCRIPTION_FONT_SIZE,
  SMALL_SPACING,
  TITLE_GAP,
} from '../constants';

import type { IdentityServiceAPIMeSessionItem } from '../../../../server/autoGeneratedHooks/identity/models';

const ITEM_PADDING = 12;
const ITEM_BORDER_RADIUS = 6;
const ITEM_BORDER_WIDTH = 1;

const styles = StyleSheet.create({
  container: {
    padding: ITEM_PADDING,
    borderRadius: ITEM_BORDER_RADIUS,
    borderWidth: ITEM_BORDER_WIDTH,
    marginTop: SMALL_SPACING,
  },
  detailText: { fontSize: DESCRIPTION_FONT_SIZE, marginBottom: TITLE_GAP },
  clientText: { fontSize: DESCRIPTION_FONT_SIZE },
  buttonRow: { marginTop: SMALL_SPACING, alignSelf: 'flex-start' },
});

export function formatTimestamp(epoch: number | undefined): string {
  if (!isValueDefined(epoch)) return '';
  return new Date(epoch).toLocaleString();
}

interface Props {
  readonly session: IdentityServiceAPIMeSessionItem;
  readonly isPending: boolean;
  readonly onRevoke: (sessionId: string) => void;
}

const SessionItem = ({ session, isPending, onRevoke }: Props): React.ReactElement => {
  const { theme } = useTheme();
  const { colors } = theme;

  const ipDisplay = session.ipAddress ?? '';
  const lastAccessDisplay = formatTimestamp(session.lastAccess);
  const clientDisplay = session.clients?.join(', ') ?? '';

  const handleRevoke = useCallback((): void => {
    if (isValueDefined(session.id)) onRevoke(session.id);
  }, [session.id, onRevoke]);

  return (
    <View
      style={[styles.container, { borderColor: colors.border, backgroundColor: colors.surface }]}
      testID={TestIds.SECURITY_SESSION_ITEM}
    >
      <Text style={[styles.detailText, { color: colors.text }]}>
        {FM('settings.security.sessionIp', ipDisplay)}
      </Text>
      <Text style={[styles.detailText, { color: colors.textSecondary }]}>
        {FM('settings.security.sessionLastAccess', lastAccessDisplay)}
      </Text>
      {clientDisplay !== '' ? (
        <Text style={[styles.clientText, { color: colors.textSecondary }]}>
          {FM('settings.security.sessionClient', clientDisplay)}
        </Text>
      ) : null}
      <View style={styles.buttonRow}>
        <Button
          accessibilityHint={FM('settings.security.revokeSessionHint')}
          accessibilityLabel={FM('settings.security.revokeSession')}
          disabled={isPending}
          label={FM('settings.security.revokeSession')}
          testID={TestIds.SECURITY_REVOKE_SESSION_BUTTON}
          variant={ButtonVariant.Danger}
          onPress={handleRevoke}
        />
      </View>
    </View>
  );
};

export default SessionItem;
