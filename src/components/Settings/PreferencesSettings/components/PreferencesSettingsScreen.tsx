/**
 * Preferences Settings Screen.
 * Allows user to configure language, timezone, and date format preferences.
 * Wired to preferences API via Orval hooks.
 */
import React, { useCallback, useEffect, useState } from 'react';

import { ActivityIndicator, ScrollView, StyleSheet, Text, View } from 'react-native';

import { useQueryClient } from '@tanstack/react-query';

import PreferencesFormFields from './PreferencesFormFields';
import { notifyError, notifySuccess } from '../../../../lib/notifications';
import { resetAllTourStorage } from '../../../../lib/tourStorage';
import { FM } from '../../../../localization/helpers';
import {
  getIdentityServiceAPIMeGetPreferencesQueryKey,
  useIdentityServiceAPIMeGetPreferences,
  useIdentityServiceAPIMeUpdatePreferences,
} from '../../../../server/autoGeneratedHooks/identity/me/me';
import { TestIds } from '../../../../shared/testIds';
import { useTheme } from '../../../../theme/hooks/useTheme';
import { isValueDefined } from '../../../../utils/is';
import { Button } from '../../../core/Button';
import ButtonVariant from '../../../core/Button/utils/ButtonVariant';
import Breadcrumb from '../../../Shared/Breadcrumb';
import Heading from '../../../Shared/Heading';
import {
  SECTION_SPACING,
  BODY_FONT_SIZE,
  ERROR_TEXT_MARGIN_TOP,
} from '../constants';

const styles = StyleSheet.create({
  container: { flex: 1 },
  scrollContent: { padding: SECTION_SPACING },
  loadingContainer: { flex: 1, justifyContent: 'center', alignItems: 'center' },
  errorText: { fontSize: BODY_FONT_SIZE, textAlign: 'center', marginTop: ERROR_TEXT_MARGIN_TOP },
  buttonSpacing: { marginTop: SECTION_SPACING },
});

const PreferencesSettingsScreen = (): React.ReactElement => {
  const { theme } = useTheme();
  const { colors } = theme;
  const primary = theme.palette.primary['500'];
  const errorColor = theme.semantic.error['500'];

  const {
    data: preferences,
    isLoading,
    isError,
  } = useIdentityServiceAPIMeGetPreferences();

  const updatePreferences = useIdentityServiceAPIMeUpdatePreferences();
  const queryClient = useQueryClient();

  const [language, setLanguage] = useState('en');
  const [timezone, setTimezone] = useState('UTC');
  const [dateFormat, setDateFormat] = useState('YYYY-MM-DD');

  useEffect(() => {
    if (isValueDefined(preferences)) {
      setLanguage(preferences.language ?? 'en');
      setTimezone(preferences.timezone ?? 'UTC');
      setDateFormat(preferences.dateFormat ?? 'YYYY-MM-DD');
    }
  }, [preferences]);

  const handleSave = useCallback(() => {
    updatePreferences.mutate(
      { data: { language, timezone, dateFormat } },
      {
        onSuccess: () => {
          queryClient.invalidateQueries({
            queryKey: getIdentityServiceAPIMeGetPreferencesQueryKey(),
          }).catch(() => {
            // Ignore invalidation errors
          });
          notifySuccess(FM('settings.preferences.messages.saveSuccess'));
        },
        onError: () => {
          notifyError(FM('settings.preferences.messages.saveError'));
        },
      },
    );
  }, [language, timezone, dateFormat, updatePreferences, queryClient]);

  const handleResetTooltips = useCallback(() => {
    resetAllTourStorage();
    notifySuccess(FM('settings.preferences.resetTooltipsSuccess'));
  }, []);

  if (isLoading)
    return (
      <View style={styles.loadingContainer} testID={TestIds.PREFERENCES_SETTINGS_LOADING}>
        <ActivityIndicator color={primary} size="large" />
        <Text style={[styles.errorText, { color: colors.textSecondary }]}>{FM('loading')}</Text>
      </View>
    );

  if (isError)
    return (
      <View style={styles.loadingContainer} testID={TestIds.PREFERENCES_SETTINGS_ERROR}>
        <Text style={[styles.errorText, { color: errorColor }]}>
          {FM('settings.preferences.messages.loadError')}
        </Text>
      </View>
    );

  const isSaving = updatePreferences.isPending;
  const saveLabel = isSaving ? FM('common.saving') : FM('settings.preferences.save');

  return (
    <View
      style={[styles.container, { backgroundColor: colors.background }]}
      testID={TestIds.PREFERENCES_SETTINGS_SCREEN}
    >
      <ScrollView contentContainerStyle={styles.scrollContent}>
        <Breadcrumb />
        <Heading>{FM('settings.preferences.title')}</Heading>

        <PreferencesFormFields
          dateFormat={dateFormat}
          language={language}
          textSecondaryStyle={{ color: colors.textSecondary }}
          timezone={timezone}
          onDateFormatChange={setDateFormat}
          onLanguageChange={setLanguage}
          onTimezoneChange={setTimezone}
        />

        <View style={styles.buttonSpacing}>
          <Button
            accessibilityHint={FM('settings.preferences.saveHint')}
            accessibilityLabel={FM('settings.preferences.save')}
            disabled={isSaving}
            label={saveLabel}
            testID={TestIds.PREFERENCES_SAVE_BUTTON}
            variant={ButtonVariant.Primary}
            onPress={handleSave}
          />
        </View>

        <View style={styles.buttonSpacing}>
          <Button
            accessibilityHint={FM('settings.preferences.resetTooltipsHint')}
            accessibilityLabel={FM('settings.preferences.resetTooltips')}
            label={FM('settings.preferences.resetTooltips')}
            testID={TestIds.PREFERENCES_RESET_TOOLTIPS_BUTTON}
            variant={ButtonVariant.Secondary}
            onPress={handleResetTooltips}
          />
        </View>
      </ScrollView>
    </View>
  );
};

export default PreferencesSettingsScreen;
