/**
 * Hook for location CRUD mutations.
 * Wraps Orval-generated location hooks with cache invalidation and toast notifications.
 */
import { useCallback, useMemo } from 'react';

import { useQueryClient } from '@tanstack/react-query';

import { FM } from '../../../../localization/helpers';
import {
  useOnlineMenuWebLocationsCreate,
  useOnlineMenuWebLocationsUpdate,
  useOnlineMenuWebLocationsDelete,
  getOnlineMenuWebLocationsListQueryKey,
} from '../../../../server/autoGeneratedHooks/onlinemenu/locations/locations';
import { notifyError, notifySuccess } from '../../../notifications';

import type { CreateLocationRequest, UpdateLocationRequest } from '../../../../server/autoGeneratedHooks/onlinemenu/models';

interface MutationCallbacks {
  onCreateSuccess?: () => void;
  onUpdateSuccess?: () => void;
  onDeleteSuccess?: () => void;
}

interface UseLocationMutationsResult {
  createLocation: (data: CreateLocationRequest) => void;
  updateLocation: (externalId: string, data: UpdateLocationRequest) => void;
  deleteLocation: (externalId: string) => void;
  isCreating: boolean;
  isUpdating: boolean;
  isDeleting: boolean;
  isMutating: boolean;
}

/** Builds mutation options with cache invalidation and notifications. */
function buildMutationOptions(
  invalidateList: () => void,
  successKey: string,
  errorKey: string,
  onSuccess?: () => void,
): { mutation: { onSuccess: () => void; onError: () => void } } {
  return {
    mutation: {
      onSuccess: () => { invalidateList(); notifySuccess(FM(successKey)); onSuccess?.(); },
      onError: () => notifyError(FM(errorKey)),
    },
  };
}

function useInvalidateLocationList(): () => void {
  const queryClient = useQueryClient();
  const listQueryKey = getOnlineMenuWebLocationsListQueryKey();
  return useCallback(() => {
    queryClient.invalidateQueries({ queryKey: listQueryKey }).catch(() => {});
  }, [queryClient, listQueryKey]);
}

export function useLocationMutations(callbacks?: MutationCallbacks): UseLocationMutationsResult {
  const invalidateList = useInvalidateLocationList();

  const createOpts = useMemo(
    () => buildMutationOptions(invalidateList, 'settings.locations.messages.createSuccess', 'settings.locations.messages.createError', callbacks?.onCreateSuccess),
    [invalidateList, callbacks],
  );
  const updateOpts = useMemo(
    () => buildMutationOptions(invalidateList, 'settings.locations.messages.updateSuccess', 'settings.locations.messages.updateError', callbacks?.onUpdateSuccess),
    [invalidateList, callbacks],
  );
  const deleteOpts = useMemo(
    () => buildMutationOptions(invalidateList, 'settings.locations.messages.deleteSuccess', 'settings.locations.messages.deleteError', callbacks?.onDeleteSuccess),
    [invalidateList, callbacks],
  );

  const create = useOnlineMenuWebLocationsCreate(createOpts);
  const update = useOnlineMenuWebLocationsUpdate(updateOpts);
  const del = useOnlineMenuWebLocationsDelete(deleteOpts);

  const createLocation = useCallback((data: CreateLocationRequest) => create.mutate({ data }), [create]);
  const updateLocation = useCallback((id: string, data: UpdateLocationRequest) => update.mutate({ externalId: id, data }), [update]);
  const deleteLocation = useCallback((id: string) => del.mutate({ externalId: id }), [del]);

  return {
    createLocation, updateLocation, deleteLocation,
    isCreating: create.isPending, isUpdating: update.isPending, isDeleting: del.isPending,
    isMutating: create.isPending || update.isPending || del.isPending,
  };
}
