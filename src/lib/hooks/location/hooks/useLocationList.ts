/**
 * Hook for fetching the list of tenant locations.
 * Wraps the Orval-generated query hook.
 */
import { useOnlineMenuWebLocationsList } from '../../../../server/autoGeneratedHooks/onlinemenu/locations/locations';

import type { LocationDto } from '../../../../server/autoGeneratedHooks/onlinemenu/models';

interface UseLocationListResult {
  locations: LocationDto[];
  isLoading: boolean;
  isError: boolean;
}

export function useLocationList(): UseLocationListResult {
  const { data, isLoading, isError } = useOnlineMenuWebLocationsList();

  return {
    locations: data ?? [],
    isLoading,
    isError,
  };
}
