/**
 * Unit tests for useLocationMutations hook.
 * Tests mutation callbacks, cache invalidation, and toast notifications.
 */
import { useQueryClient } from '@tanstack/react-query';
import { renderHook, waitFor } from '@testing-library/react-native';

import { useLocationMutations } from './useLocationMutations';

jest.mock('../../../../server/autoGeneratedHooks/onlinemenu/locations/locations', () => ({
  useOnlineMenuWebLocationsCreate: jest.fn(),
  useOnlineMenuWebLocationsUpdate: jest.fn(),
  useOnlineMenuWebLocationsDelete: jest.fn(),
  getOnlineMenuWebLocationsListQueryKey: jest.fn(() => ['/api/v1/api/v1/locations']),
}));

jest.mock('@tanstack/react-query', () => ({
  ...jest.requireActual('@tanstack/react-query'),
  useQueryClient: jest.fn(),
}));

jest.mock('../../../notifications', () => ({
  notifySuccess: jest.fn(),
  notifyError: jest.fn(),
}));

jest.mock('../../../../localization/helpers', () => ({
  FM: jest.fn((key: string) => key),
}));

const {
  useOnlineMenuWebLocationsCreate,
  useOnlineMenuWebLocationsUpdate,
  useOnlineMenuWebLocationsDelete,
} = jest.requireMock('../../../../server/autoGeneratedHooks/onlinemenu/locations/locations');
const { notifySuccess, notifyError } = jest.requireMock('../../../notifications');

const mockUseQueryClient = useQueryClient as jest.Mock;
const mockCreateHook = useOnlineMenuWebLocationsCreate as jest.Mock;
const mockUpdateHook = useOnlineMenuWebLocationsUpdate as jest.Mock;
const mockDeleteHook = useOnlineMenuWebLocationsDelete as jest.Mock;

interface HookOptions {
  mutation: {
    onSuccess?: () => void;
    onError?: () => void;
  };
}

describe('useLocationMutations', () => {
  const mockInvalidateQueries = jest.fn().mockResolvedValue(undefined);
  const mockMutate = jest.fn();
  const defaultMutation = { mutate: mockMutate, isPending: false, isError: false, error: null };

  beforeEach(() => {
    jest.clearAllMocks();
    mockUseQueryClient.mockReturnValue({ invalidateQueries: mockInvalidateQueries });
    mockCreateHook.mockReturnValue(defaultMutation);
    mockUpdateHook.mockReturnValue(defaultMutation);
    mockDeleteHook.mockReturnValue(defaultMutation);
  });

  it('calls create mutate with correct data shape', () => {
    const { result } = renderHook(() => useLocationMutations());
    const data = { name: 'Main Office', city: 'NYC' };
    result.current.createLocation(data);
    expect(mockMutate).toHaveBeenCalledWith({ data });
  });

  it('calls update mutate with externalId and data', () => {
    const updateMutate = jest.fn();
    mockUpdateHook.mockReturnValue({ ...defaultMutation, mutate: updateMutate });
    const { result } = renderHook(() => useLocationMutations());
    const data = { name: 'Updated Office' };
    result.current.updateLocation('ext-123', data);
    expect(updateMutate).toHaveBeenCalledWith({ externalId: 'ext-123', data });
  });

  it('calls delete mutate with externalId', () => {
    const deleteMutate = jest.fn();
    mockDeleteHook.mockReturnValue({ ...defaultMutation, mutate: deleteMutate });
    const { result } = renderHook(() => useLocationMutations());
    result.current.deleteLocation('ext-456');
    expect(deleteMutate).toHaveBeenCalledWith({ externalId: 'ext-456' });
  });

  it('returns combined isMutating when any mutation is pending', () => {
    mockCreateHook.mockReturnValue({ ...defaultMutation, isPending: true });
    const { result } = renderHook(() => useLocationMutations());
    expect(result.current.isMutating).toBe(true);
    expect(result.current.isCreating).toBe(true);
  });

  it('invalidates list query and shows success toast on create', async () => {
    let successCallback: (() => void) | undefined;
    mockCreateHook.mockImplementation((options: HookOptions) => {
      successCallback = options.mutation.onSuccess;
      return defaultMutation;
    });
    const onCreateSuccess = jest.fn();
    renderHook(() => useLocationMutations({ onCreateSuccess }));
    if (successCallback) successCallback();
    await waitFor(() => {
      expect(mockInvalidateQueries).toHaveBeenCalled();
      expect(notifySuccess).toHaveBeenCalledWith('settings.locations.messages.createSuccess');
      expect(onCreateSuccess).toHaveBeenCalled();
    });
  });

  it('shows error toast on create failure', async () => {
    let errorCallback: (() => void) | undefined;
    mockCreateHook.mockImplementation((options: HookOptions) => {
      errorCallback = options.mutation.onError;
      return defaultMutation;
    });
    renderHook(() => useLocationMutations());
    if (errorCallback) errorCallback();
    await waitFor(() => {
      expect(notifyError).toHaveBeenCalledWith('settings.locations.messages.createError');
    });
  });

  it('invalidates list query and shows success toast on delete', async () => {
    let successCallback: (() => void) | undefined;
    mockDeleteHook.mockImplementation((options: HookOptions) => {
      successCallback = options.mutation.onSuccess;
      return defaultMutation;
    });
    const onDeleteSuccess = jest.fn();
    renderHook(() => useLocationMutations({ onDeleteSuccess }));
    if (successCallback) successCallback();
    await waitFor(() => {
      expect(mockInvalidateQueries).toHaveBeenCalled();
      expect(notifySuccess).toHaveBeenCalledWith('settings.locations.messages.deleteSuccess');
      expect(onDeleteSuccess).toHaveBeenCalled();
    });
  });

  it('invalidates list query and shows success toast on update', async () => {
    let successCallback: (() => void) | undefined;
    mockUpdateHook.mockImplementation((options: HookOptions) => {
      successCallback = options.mutation.onSuccess;
      return defaultMutation;
    });
    const onUpdateSuccess = jest.fn();
    renderHook(() => useLocationMutations({ onUpdateSuccess }));
    if (successCallback) successCallback();
    await waitFor(() => {
      expect(mockInvalidateQueries).toHaveBeenCalled();
      expect(notifySuccess).toHaveBeenCalledWith('settings.locations.messages.updateSuccess');
      expect(onUpdateSuccess).toHaveBeenCalled();
    });
  });
});
