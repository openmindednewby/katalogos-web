/**
 * Unit tests for usePrivacyConsent hooks.
 * Tests logic and behavior, not rendering.
 */
import { useQueryClient } from '@tanstack/react-query';
import { renderHook, waitFor } from '@testing-library/react-native';

import { useGetConsent, useUpdateConsent, CONSENT_QUERY_KEY } from './usePrivacyConsent';

jest.mock('../../../../server/autoGeneratedHooks/identity/privacy/privacy', () => ({
  identityServiceAPIPrivacyGetConsent: jest.fn(),
  getIdentityServiceAPIPrivacyGetConsentQueryKey: jest.fn(() => ['/api/privacy/consent']),
  identityServiceAPIPrivacyUpdateConsent: jest.fn(),
}));

jest.mock('@tanstack/react-query', () => ({
  ...jest.requireActual('@tanstack/react-query'),
  useQueryClient: jest.fn(),
  useQuery: jest.fn(),
  useMutation: jest.fn(),
}));

const { useQuery, useMutation } = jest.requireMock('@tanstack/react-query');
const mockUseQueryClient = useQueryClient as jest.Mock;
const mockUseQuery = useQuery as jest.Mock;
const mockUseMutation = useMutation as jest.Mock;

describe('useGetConsent', () => {
  beforeEach(() => jest.clearAllMocks());

  it('returns empty array when data is loading', () => {
    mockUseQuery.mockReturnValue({
      data: undefined, isLoading: true, isError: false, error: null,
    });
    const { result } = renderHook(() => useGetConsent());
    expect(result.current.consents).toEqual([]);
    expect(result.current.isLoading).toBe(true);
  });

  it('returns consent records when loaded', () => {
    const mockConsents = [
      { consentType: 'essential', isGranted: true },
      { consentType: 'analytics', isGranted: false },
    ];
    mockUseQuery.mockReturnValue({
      data: mockConsents, isLoading: false, isError: false, error: null,
    });
    const { result } = renderHook(() => useGetConsent());
    expect(result.current.consents).toEqual(mockConsents);
    expect(result.current.isLoading).toBe(false);
  });

  it('returns error state when query fails', () => {
    const testError = new Error('Network error');
    mockUseQuery.mockReturnValue({
      data: undefined, isLoading: false, isError: true, error: testError,
    });
    const { result } = renderHook(() => useGetConsent());
    expect(result.current.isError).toBe(true);
    expect(result.current.error).toBe(testError);
  });

  it('uses correct query key', () => {
    mockUseQuery.mockReturnValue({
      data: [], isLoading: false, isError: false, error: null,
    });
    renderHook(() => useGetConsent());
    expect(mockUseQuery).toHaveBeenCalledWith(
      expect.objectContaining({ queryKey: CONSENT_QUERY_KEY }),
    );
  });
});

describe('useUpdateConsent', () => {
  const mockInvalidateQueries = jest.fn().mockResolvedValue(undefined);
  const mockMutate = jest.fn();

  beforeEach(() => {
    jest.clearAllMocks();
    mockUseQueryClient.mockReturnValue({ invalidateQueries: mockInvalidateQueries });
  });

  it('calls mutate with correct consent data', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: false });
    const { result } = renderHook(() => useUpdateConsent());
    result.current.updateConsentType('analytics', true);
    expect(mockMutate).toHaveBeenCalledWith({ consentType: 'analytics', isGranted: true });
  });

  it('returns isPending status from mutation', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: true });
    const { result } = renderHook(() => useUpdateConsent());
    expect(result.current.isPending).toBe(true);
  });

  it('calls onSuccess and invalidates queries on success', async () => {
    let successCallback: (() => void) | undefined;
    mockUseMutation.mockImplementation((options) => {
      successCallback = options?.onSuccess;
      return { mutate: mockMutate, isPending: false };
    });
    const onSuccess = jest.fn();
    renderHook(() => useUpdateConsent({ onSuccess }));
    if (successCallback) successCallback();
    await waitFor(() => {
      expect(mockInvalidateQueries).toHaveBeenCalledWith({ queryKey: CONSENT_QUERY_KEY });
      expect(onSuccess).toHaveBeenCalled();
    });
  });

  it('calls onError callback when mutation fails', async () => {
    const testError = new Error('Update failed');
    let errorCallback: ((error: unknown) => void) | undefined;
    mockUseMutation.mockImplementation((options) => {
      errorCallback = options?.onError;
      return { mutate: mockMutate, isPending: false };
    });
    const onError = jest.fn();
    renderHook(() => useUpdateConsent({ onError }));
    if (errorCallback) errorCallback(testError);
    await waitFor(() => expect(onError).toHaveBeenCalledWith(testError));
  });

  it('wraps non-Error objects in Error for onError callback', async () => {
    let errorCallback: ((error: unknown) => void) | undefined;
    mockUseMutation.mockImplementation((options) => {
      errorCallback = options?.onError;
      return { mutate: mockMutate, isPending: false };
    });
    const onError = jest.fn();
    renderHook(() => useUpdateConsent({ onError }));
    if (errorCallback) errorCallback('string error');
    await waitFor(() => expect(onError).toHaveBeenCalledWith(expect.any(Error)));
  });
});
