/**
 * Unit tests for useAccountDeletion hooks.
 * Tests logic and behavior, not rendering.
 */
import { useQueryClient } from '@tanstack/react-query';
import { renderHook, waitFor } from '@testing-library/react-native';

import {
  useRequestDeletion,
  useCancelDeletion,
  DELETION_QUERY_KEY,
} from './useAccountDeletion';

jest.mock('../../../../server/autoGeneratedHooks/identity/privacy/privacy', () => ({
  identityServiceAPIPrivacyRequestAccountDeletion: jest.fn(),
  identityServiceAPIPrivacyConfirmAccountDeletion: jest.fn(),
  identityServiceAPIPrivacyCancelAccountDeletion: jest.fn(),
}));

jest.mock('@tanstack/react-query', () => ({
  ...jest.requireActual('@tanstack/react-query'),
  useQueryClient: jest.fn(),
  useMutation: jest.fn(),
}));

const { useMutation } = jest.requireMock('@tanstack/react-query');
const mockUseQueryClient = useQueryClient as jest.Mock;
const mockUseMutation = useMutation as jest.Mock;

describe('useRequestDeletion', () => {
  const mockInvalidateQueries = jest.fn().mockResolvedValue(undefined);
  const mockMutate = jest.fn();

  beforeEach(() => {
    jest.clearAllMocks();
    mockUseQueryClient.mockReturnValue({ invalidateQueries: mockInvalidateQueries });
  });

  it('calls mutate with reason when requestDeletion is invoked', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: false });
    const { result } = renderHook(() => useRequestDeletion());
    result.current.requestDeletion('Not using the service');
    expect(mockMutate).toHaveBeenCalledWith({ reason: 'Not using the service' });
  });

  it('calls mutate with undefined reason when no reason provided', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: false });
    const { result } = renderHook(() => useRequestDeletion());
    result.current.requestDeletion();
    expect(mockMutate).toHaveBeenCalledWith({ reason: undefined });
  });

  it('returns isPending from mutation state', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: true });
    const { result } = renderHook(() => useRequestDeletion());
    expect(result.current.isPending).toBe(true);
  });

  it('calls onSuccess and invalidates queries on success', async () => {
    const deletionData = { requestId: 'del-123', status: 'PendingConfirmation' };
    let successCallback: ((data: typeof deletionData) => void) | undefined;
    mockUseMutation.mockImplementation((options) => {
      successCallback = options?.onSuccess;
      return { mutate: mockMutate, isPending: false };
    });
    const onSuccess = jest.fn();
    renderHook(() => useRequestDeletion({ onSuccess }));
    if (successCallback) successCallback(deletionData);
    await waitFor(() => {
      expect(mockInvalidateQueries).toHaveBeenCalledWith({ queryKey: DELETION_QUERY_KEY });
      expect(onSuccess).toHaveBeenCalledWith(deletionData);
    });
  });

  it('calls onError with Error when mutation fails', async () => {
    const testError = new Error('Deletion failed');
    let errorCallback: ((error: unknown) => void) | undefined;
    mockUseMutation.mockImplementation((options) => {
      errorCallback = options?.onError;
      return { mutate: mockMutate, isPending: false };
    });
    const onError = jest.fn();
    renderHook(() => useRequestDeletion({ onError }));
    if (errorCallback) errorCallback(testError);
    await waitFor(() => expect(onError).toHaveBeenCalledWith(testError));
  });
});

describe('useCancelDeletion', () => {
  const mockInvalidateQueries = jest.fn().mockResolvedValue(undefined);
  const mockMutate = jest.fn();

  beforeEach(() => {
    jest.clearAllMocks();
    mockUseQueryClient.mockReturnValue({ invalidateQueries: mockInvalidateQueries });
  });

  it('does not call mutate when requestId is undefined', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: false });
    const { result } = renderHook(() => useCancelDeletion(undefined));
    result.current.cancelDeletion();
    expect(mockMutate).not.toHaveBeenCalled();
  });

  it('calls mutate when requestId is provided', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: false });
    const { result } = renderHook(() => useCancelDeletion('del-123'));
    result.current.cancelDeletion();
    expect(mockMutate).toHaveBeenCalled();
  });

  it('returns isPending from mutation state', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: true });
    const { result } = renderHook(() => useCancelDeletion('del-123'));
    expect(result.current.isPending).toBe(true);
  });

  it('calls onSuccess and invalidates queries on success', async () => {
    let successCallback: (() => void) | undefined;
    mockUseMutation.mockImplementation((options) => {
      successCallback = options?.onSuccess;
      return { mutate: mockMutate, isPending: false };
    });
    const onSuccess = jest.fn();
    renderHook(() => useCancelDeletion('del-123', { onSuccess }));
    if (successCallback) successCallback();
    await waitFor(() => {
      expect(mockInvalidateQueries).toHaveBeenCalledWith({ queryKey: DELETION_QUERY_KEY });
      expect(onSuccess).toHaveBeenCalled();
    });
  });

  it('does not call mutate when requestId is empty string', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: false });
    const { result } = renderHook(() => useCancelDeletion(''));
    result.current.cancelDeletion();
    expect(mockMutate).not.toHaveBeenCalled();
  });
});
