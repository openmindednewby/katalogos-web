/**
 * React Query hooks for GDPR account deletion requests.
 * Wraps generated Orval API functions with app-specific logic.
 */
import { useCallback } from 'react';

import { useMutation, useQueryClient } from '@tanstack/react-query';

import {
  identityServiceAPIPrivacyRequestAccountDeletion,
  identityServiceAPIPrivacyConfirmAccountDeletion,
  identityServiceAPIPrivacyCancelAccountDeletion,
} from '../../../../server/autoGeneratedHooks/identity/privacy/privacy';
import DeletionStatus from '../enums/DeletionStatus';

import type { DeletionRequest, ConfirmDeletionRequest, RequestDeletionBody } from '../types';

const DELETION_QUERY_KEY = ['privacy', 'delete-request'] as const;

async function requestDeletion(body: RequestDeletionBody): Promise<DeletionRequest> {
  const response = await identityServiceAPIPrivacyRequestAccountDeletion({
    reason: body.reason ?? null,
  });
  return {
    requestId: response.requestId ?? '',
    status: DeletionStatus.PendingConfirmation,
    scheduledDeletionDate: response.scheduledDeletionDate,
  };
}

async function confirmDeletion(
  requestId: string,
  body: ConfirmDeletionRequest,
): Promise<undefined> {
  await identityServiceAPIPrivacyConfirmAccountDeletion(requestId, {
    confirmationToken: body.confirmationToken,
  });
  return undefined;
}

async function cancelDeletion(requestId: string): Promise<undefined> {
  await identityServiceAPIPrivacyCancelAccountDeletion(requestId);
  return undefined;
}

interface MutationCallbacks<T = undefined> {
  onSuccess?: (data: T) => void;
  onError?: (error: Error) => void;
}

interface UseRequestDeletionResult {
  requestDeletion: (reason?: string) => void;
  isPending: boolean;
}

export function useRequestDeletion(
  callbacks?: MutationCallbacks<DeletionRequest>,
): UseRequestDeletionResult {
  const queryClient = useQueryClient();

  const mutation = useMutation({
    mutationFn: requestDeletion,
    onSuccess: (data: DeletionRequest) => {
      queryClient.invalidateQueries({ queryKey: DELETION_QUERY_KEY }).catch(() => {});
      callbacks?.onSuccess?.(data);
    },
    onError: (error: unknown) => {
      const errorObj = error instanceof Error ? error : new Error('Deletion request failed');
      callbacks?.onError?.(errorObj);
    },
  });

  const handleRequest = useCallback(
    (reason?: string) => mutation.mutate({ reason }),
    [mutation],
  );

  return {
    requestDeletion: handleRequest,
    isPending: mutation.isPending,
  };
}

interface UseConfirmDeletionResult {
  confirmDeletion: (token: string) => void;
  isPending: boolean;
}

function createDeletionMutationCallbacks(
  queryClient: ReturnType<typeof useQueryClient>,
  callbacks: MutationCallbacks | undefined,
  failureMessage: string,
): { onSuccess: () => void; onError: (error: unknown) => void } {
  return {
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: DELETION_QUERY_KEY }).catch(() => {});
      callbacks?.onSuccess?.(undefined);
    },
    onError: (error: unknown) => {
      const errorObj = error instanceof Error ? error : new Error(failureMessage);
      callbacks?.onError?.(errorObj);
    },
  };
}

export function useConfirmDeletion(
  requestId: string | undefined,
  callbacks?: MutationCallbacks,
): UseConfirmDeletionResult {
  const queryClient = useQueryClient();
  const hasRequestId = typeof requestId === 'string' && requestId !== '';
  const safeRequestId = hasRequestId ? requestId : '';

  const mutation = useMutation({
    mutationFn: async (body: ConfirmDeletionRequest) =>
      confirmDeletion(safeRequestId, body),
    ...createDeletionMutationCallbacks(queryClient, callbacks, 'Confirmation failed'),
  });

  const handleConfirm = useCallback(
    (token: string) => {
      if (hasRequestId) mutation.mutate({ confirmationToken: token });
    },
    [mutation, hasRequestId],
  );

  return { confirmDeletion: handleConfirm, isPending: mutation.isPending };
}

interface UseCancelDeletionResult {
  cancelDeletion: () => void;
  isPending: boolean;
}

export function useCancelDeletion(
  requestId: string | undefined,
  callbacks?: MutationCallbacks,
): UseCancelDeletionResult {
  const queryClient = useQueryClient();
  const hasRequestId = typeof requestId === 'string' && requestId !== '';
  const safeRequestId = hasRequestId ? requestId : '';

  const mutation = useMutation({
    mutationFn: async () => cancelDeletion(safeRequestId),
    ...createDeletionMutationCallbacks(queryClient, callbacks, 'Cancellation failed'),
  });

  const handleCancel = useCallback(() => {
    if (hasRequestId) mutation.mutate();
  }, [mutation, hasRequestId]);

  return { cancelDeletion: handleCancel, isPending: mutation.isPending };
}

export { DELETION_QUERY_KEY };
