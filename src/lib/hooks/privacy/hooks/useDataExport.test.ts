/**
 * Unit tests for useDataExport hooks.
 * Tests logic and behavior, not rendering.
 */
import { useQueryClient } from '@tanstack/react-query';
import { renderHook, waitFor } from '@testing-library/react-native';

import { useRequestDataExport, useExportStatus, EXPORT_QUERY_KEY } from './useDataExport';

jest.mock('../../../../server/autoGeneratedHooks/identity/privacy/privacy', () => ({
  identityServiceAPIPrivacyRequestDataExport: jest.fn(),
  identityServiceAPIPrivacyGetDataExportStatus: jest.fn(),
}));

jest.mock('../../../../server/mutators/identityMutator', () => ({
  identityInstance: jest.fn(),
}));

jest.mock('@tanstack/react-query', () => ({
  ...jest.requireActual('@tanstack/react-query'),
  useQueryClient: jest.fn(),
  useQuery: jest.fn(),
  useMutation: jest.fn(),
}));

const { useQuery, useMutation } = jest.requireMock('@tanstack/react-query');
const mockUseQueryClient = useQueryClient as jest.Mock;
const mockUseQuery = useQuery as jest.Mock;
const mockUseMutation = useMutation as jest.Mock;

describe('useRequestDataExport', () => {
  const mockInvalidateQueries = jest.fn().mockResolvedValue(undefined);
  const mockMutate = jest.fn();

  beforeEach(() => {
    jest.clearAllMocks();
    mockUseQueryClient.mockReturnValue({ invalidateQueries: mockInvalidateQueries });
  });

  it('calls mutate when requestExport is invoked', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: false });
    const { result } = renderHook(() => useRequestDataExport());
    result.current.requestExport();
    expect(mockMutate).toHaveBeenCalled();
  });

  it('returns isPending from mutation state', () => {
    mockUseMutation.mockReturnValue({ mutate: mockMutate, isPending: true });
    const { result } = renderHook(() => useRequestDataExport());
    expect(result.current.isPending).toBe(true);
  });

  it('calls onSuccess with export data and invalidates queries', async () => {
    const exportData = { requestId: 'req-123', status: 'Pending' };
    let successCallback: ((data: typeof exportData) => void) | undefined;
    mockUseMutation.mockImplementation((options) => {
      successCallback = options?.onSuccess;
      return { mutate: mockMutate, isPending: false };
    });
    const onSuccess = jest.fn();
    renderHook(() => useRequestDataExport({ onSuccess }));
    if (successCallback) successCallback(exportData);
    await waitFor(() => {
      expect(mockInvalidateQueries).toHaveBeenCalledWith({ queryKey: EXPORT_QUERY_KEY });
      expect(onSuccess).toHaveBeenCalledWith(exportData);
    });
  });

  it('calls onError callback when mutation fails', async () => {
    const testError = new Error('Export failed');
    let errorCallback: ((error: unknown) => void) | undefined;
    mockUseMutation.mockImplementation((options) => {
      errorCallback = options?.onError;
      return { mutate: mockMutate, isPending: false };
    });
    const onError = jest.fn();
    renderHook(() => useRequestDataExport({ onError }));
    if (errorCallback) errorCallback(testError);
    await waitFor(() => expect(onError).toHaveBeenCalledWith(testError));
  });
});

describe('useExportStatus', () => {
  beforeEach(() => jest.clearAllMocks());

  it('disables query when requestId is undefined', () => {
    mockUseQuery.mockReturnValue({
      data: undefined, isLoading: false, isError: false,
    });
    renderHook(() => useExportStatus(undefined));
    expect(mockUseQuery).toHaveBeenCalledWith(
      expect.objectContaining({ enabled: false }),
    );
  });

  it('enables query when requestId is provided', () => {
    mockUseQuery.mockReturnValue({
      data: undefined, isLoading: true, isError: false,
    });
    renderHook(() => useExportStatus('req-123'));
    expect(mockUseQuery).toHaveBeenCalledWith(
      expect.objectContaining({ enabled: true }),
    );
  });

  it('returns export request data', () => {
    const exportData = { requestId: 'req-123', status: 'Completed' };
    mockUseQuery.mockReturnValue({
      data: exportData, isLoading: false, isError: false,
    });
    const { result } = renderHook(() => useExportStatus('req-123'));
    expect(result.current.exportRequest).toEqual(exportData);
    expect(result.current.isLoading).toBe(false);
  });

  it('includes requestId in query key', () => {
    mockUseQuery.mockReturnValue({
      data: undefined, isLoading: true, isError: false,
    });
    renderHook(() => useExportStatus('req-456'));
    expect(mockUseQuery).toHaveBeenCalledWith(
      expect.objectContaining({ queryKey: [...EXPORT_QUERY_KEY, 'req-456'] }),
    );
  });

  it('disables query when requestId is empty string', () => {
    mockUseQuery.mockReturnValue({
      data: undefined, isLoading: false, isError: false,
    });
    renderHook(() => useExportStatus(''));
    expect(mockUseQuery).toHaveBeenCalledWith(
      expect.objectContaining({ enabled: false }),
    );
  });
});
