/**
 * Unit tests for useNotificationPreferences hooks.
 * Tests logic and behavior, not rendering.
 */
import { useQueryClient } from '@tanstack/react-query';
import { renderHook, waitFor } from '@testing-library/react-native';

import {
  useGetNotificationPreferences,
  useUpdateNotificationPreferences,
} from './useNotificationPreferences';
import {
  useNotificationWebPreferencesGet,
  useNotificationWebPreferencesUpdate,
  getNotificationWebPreferencesGetQueryKey,
} from '../../../server/autoGeneratedHooks/notification/notifications/notifications';

jest.mock('../../../server/autoGeneratedHooks/notification/notifications/notifications');
jest.mock('@tanstack/react-query', () => ({
  ...jest.requireActual('@tanstack/react-query'),
  useQueryClient: jest.fn(),
}));

const mockUseNotificationWebPreferencesGet = useNotificationWebPreferencesGet as jest.Mock;
const mockUseNotificationWebPreferencesUpdate = useNotificationWebPreferencesUpdate as jest.Mock;
const mockUseQueryClient = useQueryClient as jest.Mock;
const mockGetQueryKey = getNotificationWebPreferencesGetQueryKey as jest.Mock;

describe('useGetNotificationPreferences', () => {
  beforeEach(() => jest.clearAllMocks());

  it('returns undefined preferences when data is loading', () => {
    mockUseNotificationWebPreferencesGet.mockReturnValue({
      data: undefined, isLoading: true, isError: false,
    });
    const { result } = renderHook(() => useGetNotificationPreferences());
    expect(result.current.preferences).toBeUndefined();
    expect(result.current.isLoading).toBe(true);
  });

  it('transforms API response to preferences format correctly', () => {
    mockUseNotificationWebPreferencesGet.mockReturnValue({
      data: {
        notificationsEnabled: true, quietHoursEnabled: false,
        questionnaireSubmittedDisplay: 3, templateUpdatedDisplay: 1,
        userInvitedDisplay: 2, menuUpdatedDisplay: 0, paymentDueDisplay: 3,
      },
      isLoading: false, isError: false,
    });
    const { result } = renderHook(() => useGetNotificationPreferences());
    expect(result.current.preferences?.notificationsEnabled).toBe(true);
    expect(result.current.preferences?.categoryPreferences.menuUpdated.enabled).toBe(false);
  });

  it('uses default values for missing API response fields', () => {
    mockUseNotificationWebPreferencesGet.mockReturnValue({
      data: {}, isLoading: false, isError: false,
    });
    const { result } = renderHook(() => useGetNotificationPreferences());
    expect(result.current.preferences?.notificationsEnabled).toBe(true);
    expect(result.current.preferences?.quietHoursEnabled).toBe(false);
  });
});

describe('useUpdateNotificationPreferences', () => {
  const mockInvalidateQueries = jest.fn().mockResolvedValue(undefined);
  const mockMutate = jest.fn();

  beforeEach(() => {
    jest.clearAllMocks();
    mockInvalidateQueries.mockResolvedValue(undefined);
    mockUseQueryClient.mockReturnValue({ invalidateQueries: mockInvalidateQueries });
    mockGetQueryKey.mockReturnValue(['test-query-key']);
  });

  it('calls mutate with transformed request data', () => {
    mockUseNotificationWebPreferencesUpdate.mockReturnValue({
      mutate: mockMutate, isPending: false, isError: false, isSuccess: false, error: null,
    });
    const { result } = renderHook(() => useUpdateNotificationPreferences());
    const preferences = {
      notificationsEnabled: true, quietHoursEnabled: false,
      quietHoursStart: null, quietHoursEnd: null, quietHoursTimezone: null,
      categoryPreferences: {
        questionnaireSubmitted: { enabled: true, displayPreference: 3 as const },
        templateUpdated: { enabled: false, displayPreference: 1 as const },
        userInvited: { enabled: true, displayPreference: 2 as const },
        menuUpdated: { enabled: true, displayPreference: 1 as const },
        paymentDue: { enabled: true, displayPreference: 3 as const },
      },
    };
    result.current.updatePreferences(preferences);
    expect(mockMutate).toHaveBeenCalledWith({
      data: expect.objectContaining({
        questionnaireSubmittedDisplay: 3, templateUpdatedDisplay: 0,
      }),
    });
  });

  it('returns isPending status from mutation', () => {
    mockUseNotificationWebPreferencesUpdate.mockReturnValue({
      mutate: mockMutate, isPending: true, isError: false, isSuccess: false, error: null,
    });
    const { result } = renderHook(() => useUpdateNotificationPreferences());
    expect(result.current.isPending).toBe(true);
  });

  it('calls onSuccess callback when mutation succeeds', async () => {
    let successCallback: (() => void) | undefined;
    mockUseNotificationWebPreferencesUpdate.mockImplementation((options) => {
      successCallback = options?.mutation?.onSuccess;
      return { mutate: mockMutate, isPending: false, isError: false, isSuccess: true, error: null };
    });
    const onSuccess = jest.fn();
    renderHook(() => useUpdateNotificationPreferences({ onSuccess }));
    if (successCallback) successCallback();
    await waitFor(() => {
      expect(mockInvalidateQueries).toHaveBeenCalled();
      expect(onSuccess).toHaveBeenCalled();
    });
  });

  it('calls onError callback when mutation fails', async () => {
    let errorCallback: ((error: Error) => void) | undefined;
    const testError = new Error('Test error');
    mockUseNotificationWebPreferencesUpdate.mockImplementation((options) => {
      errorCallback = options?.mutation?.onError;
      return { mutate: mockMutate, isPending: false, isError: true, isSuccess: false, error: testError };
    });
    const onError = jest.fn();
    renderHook(() => useUpdateNotificationPreferences({ onError }));
    if (errorCallback) errorCallback(testError);
    await waitFor(() => expect(onError).toHaveBeenCalledWith(testError));
  });

  it('transforms disabled categories to display preference 0', () => {
    mockUseNotificationWebPreferencesUpdate.mockReturnValue({
      mutate: mockMutate, isPending: false, isError: false, isSuccess: false, error: null,
    });
    const { result } = renderHook(() => useUpdateNotificationPreferences());
    const preferences = {
      notificationsEnabled: true, quietHoursEnabled: false,
      quietHoursStart: null, quietHoursEnd: null, quietHoursTimezone: null,
      categoryPreferences: {
        questionnaireSubmitted: { enabled: false, displayPreference: 3 as const },
        templateUpdated: { enabled: false, displayPreference: 1 as const },
        userInvited: { enabled: false, displayPreference: 2 as const },
        menuUpdated: { enabled: false, displayPreference: 1 as const },
        paymentDue: { enabled: false, displayPreference: 3 as const },
      },
    };
    result.current.updatePreferences(preferences);
    expect(mockMutate).toHaveBeenCalledWith({
      data: expect.objectContaining({
        questionnaireSubmittedDisplay: 0, templateUpdatedDisplay: 0,
        userInvitedDisplay: 0, menuUpdatedDisplay: 0, paymentDueDisplay: 0,
      }),
    });
  });
});
