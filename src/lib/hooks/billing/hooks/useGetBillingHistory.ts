/**
 * Hook to fetch paginated billing history.
 * Wraps the Orval-generated query and maps the DTO to frontend types.
 */
import { useMemo } from 'react';

import { usePaymentServiceAPIBillingGetBillingHistory } from '../../../../server/autoGeneratedHooks/payment/billing/billing';
import { isValueDefined } from '../../../../utils/is';
import { BILLING_HISTORY_PAGE_SIZE, FIRST_PAGE } from '../constants';
import { mapBillingHistory } from '../utils/mappers';

import type { BillingHistoryResponse } from '../types';

interface UseGetBillingHistoryResult {
  history: BillingHistoryResponse | undefined;
  isLoading: boolean;
  isError: boolean;
  error: Error | null;
}

export function useGetBillingHistory(
  page: number = FIRST_PAGE,
  pageSize: number = BILLING_HISTORY_PAGE_SIZE,
): UseGetBillingHistoryResult {
  const params = useMemo(() => ({ page, pageSize }), [page, pageSize]);

  const query = usePaymentServiceAPIBillingGetBillingHistory(params);

  const mapped = isValueDefined(query.data)
    ? mapBillingHistory(query.data, page, pageSize)
    : undefined;

  const queryError: unknown = query.error;

  return {
    history: mapped,
    isLoading: query.isLoading,
    isError: query.isError,
    error: queryError instanceof Error ? queryError : null,
  };
}
