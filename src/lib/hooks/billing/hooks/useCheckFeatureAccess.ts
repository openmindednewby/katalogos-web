/**
 * Hook to check if the current subscription has access to a feature.
 * Wraps the Orval-generated query and maps the DTO to frontend types.
 */
import { useMemo } from 'react';

import { usePaymentServiceAPISubscriptionsCheckFeatureAccess } from '../../../../server/autoGeneratedHooks/payment/subscriptions/subscriptions';
import { isValueDefined } from '../../../../utils/is';
import { mapFeatureAccess } from '../utils/mappers';

import type { FeatureAccessResult } from '../types';

interface UseCheckFeatureAccessResult {
  featureAccess: FeatureAccessResult | undefined;
  hasAccess: boolean;
  isLoading: boolean;
  isError: boolean;
  error: Error | null;
}

export function useCheckFeatureAccess(featureCode: string): UseCheckFeatureAccessResult {
  const hookOptions = useMemo(() => ({ query: { enabled: featureCode !== '' } }), [featureCode]);

  const query = usePaymentServiceAPISubscriptionsCheckFeatureAccess(
    featureCode,
    hookOptions,
  );

  const mapped = isValueDefined(query.data)
    ? mapFeatureAccess(query.data)
    : undefined;

  const queryError: unknown = query.error;

  return {
    featureAccess: mapped,
    hasAccess: mapped?.canAccess ?? false,
    isLoading: query.isLoading,
    isError: query.isError,
    error: queryError instanceof Error ? queryError : null,
  };
}
