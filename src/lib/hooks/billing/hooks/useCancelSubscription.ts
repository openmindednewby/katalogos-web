/**
 * Hook to cancel the current subscription.
 * Wraps the Orval-generated mutation and handles cache invalidation.
 */
import { useCallback, useMemo } from 'react';

import { useQueryClient } from '@tanstack/react-query';

import { SUBSCRIPTION_QUERY_KEY } from './useGetCurrentSubscription';
import { usePaymentServiceAPISubscriptionsCancelSubscription } from '../../../../server/autoGeneratedHooks/payment/subscriptions/subscriptions';

interface MutationCallbacks {
  onSuccess?: () => void;
  onError?: (error: Error) => void;
}

interface UseCancelSubscriptionResult {
  cancelSubscription: () => void;
  isPending: boolean;
  isError: boolean;
  error: Error | null;
}

function toError(error: unknown): Error {
  return error instanceof Error ? error : new Error('Subscription cancellation failed');
}

export function useCancelSubscription(callbacks?: MutationCallbacks): UseCancelSubscriptionResult {
  const queryClient = useQueryClient();

  const hookOptions = useMemo(() => ({
    mutation: {
      onSuccess: () => {
        queryClient.invalidateQueries({ queryKey: SUBSCRIPTION_QUERY_KEY }).catch(() => {});
        callbacks?.onSuccess?.();
      },
      onError: (error: unknown) => callbacks?.onError?.(toError(error)),
    },
  }), [queryClient, callbacks]);

  const mutation = usePaymentServiceAPISubscriptionsCancelSubscription(hookOptions);
  const mutationError: unknown = mutation.error;

  const cancelSubscription = useCallback(() => mutation.mutate(), [mutation]);

  return {
    cancelSubscription,
    isPending: mutation.isPending,
    isError: mutation.isError,
    error: mutationError instanceof Error ? mutationError : null,
  };
}
