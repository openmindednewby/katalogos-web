/**
 * Hook to change the current subscription plan.
 * Wraps the Orval-generated mutation and handles cache invalidation.
 */
import { useCallback, useMemo } from 'react';

import { useQueryClient } from '@tanstack/react-query';

import { SUBSCRIPTION_QUERY_KEY } from './useGetCurrentSubscription';
import { usePaymentServiceAPISubscriptionsChangePlan } from '../../../../server/autoGeneratedHooks/payment/subscriptions/subscriptions';

import type { ChangePlanRequest } from '../types';

interface MutationCallbacks {
  onSuccess?: (data: { planName: string }) => void;
  onError?: (error: Error) => void;
}

interface UseChangePlanResult {
  changePlan: (request: ChangePlanRequest) => void;
  isPending: boolean;
  isError: boolean;
  error: Error | null;
}

function toError(error: unknown): Error {
  return error instanceof Error ? error : new Error('Plan change failed');
}

export function useChangePlan(callbacks?: MutationCallbacks): UseChangePlanResult {
  const queryClient = useQueryClient();

  const hookOptions = useMemo(() => ({
    mutation: {
      onSuccess: () => {
        queryClient.invalidateQueries({ queryKey: SUBSCRIPTION_QUERY_KEY }).catch(() => {});
        callbacks?.onSuccess?.({ planName: '' });
      },
      onError: (error: unknown) => callbacks?.onError?.(toError(error)),
    },
  }), [queryClient, callbacks]);

  const mutation = usePaymentServiceAPISubscriptionsChangePlan(hookOptions);
  const mutationError: unknown = mutation.error;

  const changePlan = useCallback(
    (request: ChangePlanRequest) =>
      mutation.mutate({ data: { newPlanExternalId: request.planId, billingCycle: request.billingCycle } }),
    [mutation],
  );

  return {
    changePlan,
    isPending: mutation.isPending,
    isError: mutation.isError,
    error: mutationError instanceof Error ? mutationError : null,
  };
}
