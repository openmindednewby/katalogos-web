/**
 * Hook to fetch the current tenant subscription.
 * Wraps the Orval-generated hook and maps the DTO to frontend types.
 */
import {
  usePaymentServiceAPISubscriptionsGetCurrentSubscription,
  getPaymentServiceAPISubscriptionsGetCurrentSubscriptionQueryKey,
} from '../../../../server/autoGeneratedHooks/payment/subscriptions/subscriptions';
import { isValueDefined } from '../../../../utils/is';
import { mapSubscription } from '../utils/mappers';

import type { Subscription } from '../types';

interface UseGetCurrentSubscriptionResult {
  subscription: Subscription | undefined;
  isLoading: boolean;
  isError: boolean;
  error: Error | null;
}

/** Query key for subscription — reexported for invalidation. */
export const SUBSCRIPTION_QUERY_KEY =
  getPaymentServiceAPISubscriptionsGetCurrentSubscriptionQueryKey();

export function useGetCurrentSubscription(): UseGetCurrentSubscriptionResult {
  const query = usePaymentServiceAPISubscriptionsGetCurrentSubscription();

  const mapped = isValueDefined(query.data)
    ? mapSubscription(query.data)
    : undefined;

  const queryError: unknown = query.error;

  return {
    subscription: mapped,
    isLoading: query.isLoading,
    isError: query.isError,
    error: queryError instanceof Error ? queryError : null,
  };
}
