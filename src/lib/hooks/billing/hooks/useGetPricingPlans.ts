/**
 * Hook to fetch available pricing plans.
 * Wraps the Orval-generated hook and maps DTOs to frontend types.
 */
import { useMemo } from 'react';

import { usePaymentServiceAPIPricingPlansGetPricingPlans } from '../../../../server/autoGeneratedHooks/payment/pricing-plans/pricing-plans';
import { mapPricingPlan } from '../utils/mappers';

import type { PricingPlan } from '../types';

interface UseGetPricingPlansResult {
  plans: PricingPlan[];
  isLoading: boolean;
  isError: boolean;
  error: Error | null;
}

export function useGetPricingPlans(): UseGetPricingPlansResult {
  const query = usePaymentServiceAPIPricingPlansGetPricingPlans();

  const plans = useMemo(
    () => (query.data ?? []).map(mapPricingPlan),
    [query.data],
  );

  const queryError: unknown = query.error;

  return {
    plans,
    isLoading: query.isLoading,
    isError: query.isError,
    error: queryError instanceof Error ? queryError : null,
  };
}
