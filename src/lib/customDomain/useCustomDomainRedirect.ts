import { useEffect } from 'react';

import { Platform } from 'react-native';

import { isCustomDomainHost } from './isCustomDomainHost';
import { onlineMenuWebCustomDomainsGetByDomain } from '../../server/autoGeneratedHooks/onlinemenu/custom-domains/custom-domains';
import { isNotEmptyString } from '../../utils/is';
import { logger } from '../../utils/logger';

/** Path prefix of the public menu viewer route (`/public/menu/{externalId}`). */
const PUBLIC_MENU_PATH_PREFIX = '/public/menu/';

/** Minimal router contract — just what the glue needs (decoupled for testing). */
interface RedirectRouter {
  replace: (path: string) => void;
}

/**
 * Custom-domain resolve-glue (Batch C). On web, when the SPA loads on a tenant's bring-your-own
 * domain (e.g. `menu.acme.com` — see {@link isCustomDomainHost}), resolve that host to its menu
 * via `GET /api/v1/public/domains/resolve` and replace-navigate to the public menu viewer.
 *
 * No-op on the canonical platform hosts and local dev, and when already on a `/public/menu/`
 * route (so a reload of the resolved page doesn't loop). Resolution failures are swallowed —
 * the SPA's normal landing renders instead.
 */
export function useCustomDomainRedirect(router: RedirectRouter): void {
  useEffect(() => {
    if (Platform.OS !== 'web' || typeof window === 'undefined') return;

    const { hostname, pathname } = window.location;
    if (!isCustomDomainHost(hostname)) return;
    if (pathname.startsWith(PUBLIC_MENU_PATH_PREFIX)) return;

    const controller = new AbortController();
    onlineMenuWebCustomDomainsGetByDomain({ domain: hostname }, controller.signal)
      .then((res) => {
        const menuExternalId = res.menuExternalId;
        if (isNotEmptyString(menuExternalId)) router.replace(`${PUBLIC_MENU_PATH_PREFIX}${menuExternalId}`);
      })
      .catch((err: unknown) => {
        logger.warn('customDomain', 'Failed to resolve custom domain', err);
      });

    return () => controller.abort();
  }, [router]);
}
