/**
 * Tests for useMenuQueries hook.
 * Focus on testing the callback logic, not the underlying mutations.
 */
import { renderHook, act } from '@testing-library/react-native';

import { useMenuQueries } from './useMenuPageHandlers';

// Mock dependencies
jest.mock('../lib/notifications', () => ({
  notify: jest.fn(),
  notifySuccess: jest.fn(),
}));

jest.mock('../utils/logger', () => ({
  logger: {
    error: jest.fn(),
    warn: jest.fn(),
    info: jest.fn(),
  },
}));

jest.mock('react-native', () => ({
  Linking: { openURL: jest.fn() },
  Platform: { OS: 'web' },
}));

// Mock the auto-generated hooks
jest.mock('../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus', () => ({
  useOnlineMenuWebMenuList: jest.fn(() => ({
    data: [],
    isLoading: false,
    refetch: jest.fn().mockResolvedValue({}),
  })),
  useOnlineMenuWebMenuUpdate: jest.fn(() => ({
    mutate: jest.fn(),
    isLoading: false,
  })),
  useOnlineMenuWebMenuDelete: jest.fn(() => ({
    mutate: jest.fn(),
    isLoading: false,
  })),
  useOnlineMenuWebTenantMenusCreate: jest.fn(() => ({
    mutate: jest.fn(),
    isLoading: false,
  })),
}));

jest.mock('./useMenuActions', () => ({
  useActivateMenu: jest.fn(() => ({
    mutate: jest.fn(),
    isLoading: false,
  })),
  useDeactivateMenu: jest.fn(() => ({
    mutate: jest.fn(),
    isLoading: false,
  })),
}));

describe('useMenuQueries', () => {
  beforeEach(() => {
    jest.clearAllMocks();
    jest.useFakeTimers();
  });

  afterEach(() => {
    jest.useRealTimers();
  });

  it('returns all query and mutation objects', () => {
    const { result } = renderHook(() => useMenuQueries());

    expect(result.current.listQuery).toBeDefined();
    expect(result.current.createMutation).toBeDefined();
    expect(result.current.updateMutation).toBeDefined();
    expect(result.current.deleteMutation).toBeDefined();
    expect(result.current.activateMutation).toBeDefined();
    expect(result.current.deactivateMutation).toBeDefined();
    expect(result.current.refetchMenusSoon).toBeDefined();
    expect(typeof result.current.refetchMenusSoon).toBe('function');
  });

  it('refetchMenusSoon calls refetch immediately', () => {
    const mockRefetch = jest.fn().mockResolvedValue({});
    const { useOnlineMenuWebMenuList } = require('../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus');
    useOnlineMenuWebMenuList.mockReturnValue({
      data: [],
      isLoading: false,
      refetch: mockRefetch,
    });

    const { result } = renderHook(() => useMenuQueries());

    act(() => {
      result.current.refetchMenusSoon();
    });

    expect(mockRefetch).toHaveBeenCalledTimes(1);
  });

  it('refetchMenusSoon calls refetch again after delay', () => {
    const mockRefetch = jest.fn().mockResolvedValue({});
    const { useOnlineMenuWebMenuList } = require('../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus');
    useOnlineMenuWebMenuList.mockReturnValue({
      data: [],
      isLoading: false,
      refetch: mockRefetch,
    });

    const { result } = renderHook(() => useMenuQueries());

    act(() => {
      result.current.refetchMenusSoon();
    });

    expect(mockRefetch).toHaveBeenCalledTimes(1);

    const REFETCH_DELAY_MS = 250;
    act(() => {
      jest.advanceTimersByTime(REFETCH_DELAY_MS);
    });

    expect(mockRefetch).toHaveBeenCalledTimes(2);
  });

  it('refetchMenusSoon maintains stable identity across re-renders (BUG-MENU-001)', () => {
    const mockRefetch = jest.fn().mockResolvedValue({});
    const { useOnlineMenuWebMenuList } = require('../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus');
    useOnlineMenuWebMenuList.mockReturnValue({
      data: [],
      isLoading: false,
      refetch: mockRefetch,
    });

    const { result, rerender } = renderHook(() => useMenuQueries());
    const firstRef = result.current.refetchMenusSoon;

    rerender({});
    const secondRef = result.current.refetchMenusSoon;

    expect(firstRef).toBe(secondRef);
  });

  it('refetchMenusSoon uses latest refetch via ref after re-render (BUG-MENU-001)', () => {
    const firstRefetch = jest.fn().mockResolvedValue({});
    const secondRefetch = jest.fn().mockResolvedValue({});
    const { useOnlineMenuWebMenuList } = require('../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus');

    useOnlineMenuWebMenuList.mockReturnValue({
      data: [],
      isLoading: false,
      refetch: firstRefetch,
    });

    const { result, rerender } = renderHook(() => useMenuQueries());

    // Update the mock to return a new refetch function
    useOnlineMenuWebMenuList.mockReturnValue({
      data: [{ id: 1 }],
      isLoading: false,
      refetch: secondRefetch,
    });

    rerender({});

    act(() => {
      result.current.refetchMenusSoon();
    });

    // Should call the latest refetch, not the stale one
    expect(secondRefetch).toHaveBeenCalled();
    expect(firstRefetch).not.toHaveBeenCalled();
  });
});
