import { useCallback, useMemo, useState } from 'react';

import { useTenantMutations } from './tenants';
import { useIdentityServiceAPITenantsListTenants } from '../server/autoGeneratedHooks/identity/tenants/tenants';

import type { TenantFormPayload } from './tenants';
import type { IdentityServiceAPITenantsTenantDto } from '../server/autoGeneratedHooks/identity/models/identityServiceAPITenantsTenantDto';

interface UseTenantActionsReturn {
  tenants: IdentityServiceAPITenantsTenantDto[];
  isLoading: boolean;
  isError: boolean;
  editingId: string | null;
  showCreateModal: boolean;
  isCreating: boolean;
  isUpdating: boolean;
  handleRefetch: () => void;
  handleDelete: (id: string) => void;
  handleStartEdit: (id: string) => void;
  handleCancelEdit: () => void;
  handleUpdate: (tenantId: string, payload: TenantFormPayload) => void;
  handleCreate: (payload: TenantFormPayload) => void;
  handleShowCreateModal: () => void;
  handleHideCreateModal: () => void;
}

interface UseTenantActionsOptions {
  enabled: boolean;
}

/** Create state setters for modal visibility */
function useModalState(): {
  showCreateModal: boolean;
  handleShowCreateModal: () => void;
  handleHideCreateModal: () => void;
  setShowCreateModal: (show: boolean) => void;
} {
  const [showCreateModal, setShowCreateModal] = useState(false);
  const handleShowCreateModal = useCallback((): void => setShowCreateModal(true), []);
  const handleHideCreateModal = useCallback((): void => setShowCreateModal(false), []);
  return { showCreateModal, handleShowCreateModal, handleHideCreateModal, setShowCreateModal };
}

/** Create state setters for editing */
function useEditingState(): {
  editingId: string | null;
  handleStartEdit: (id: string) => void;
  handleCancelEdit: () => void;
  setEditingId: (id: string | null) => void;
} {
  const [editingId, setEditingId] = useState<string | null>(null);
  const handleStartEdit = useCallback((id: string): void => setEditingId(id), []);
  const handleCancelEdit = useCallback((): void => setEditingId(null), []);
  return { editingId, handleStartEdit, handleCancelEdit, setEditingId };
}

/** Build the mutations params object */
function useMutationsParams(
  handleRefetch: () => void,
  setEditingId: (id: string | null) => void,
  setShowCreateModal: (show: boolean) => void,
): { refetch: () => void; setEditingId: (id: string | null) => void; setShowCreateModal: (show: boolean) => void } {
  return useMemo(() => ({
    refetch: handleRefetch,
    setEditingId,
    setShowCreateModal,
  }), [handleRefetch, setEditingId, setShowCreateModal]);
}

/**
 * Hook that encapsulates all tenant CRUD operations and state management.
 */
export function useTenantActions({ enabled }: UseTenantActionsOptions): UseTenantActionsReturn {
  const listQuery = useIdentityServiceAPITenantsListTenants({ query: { enabled } });
  const { showCreateModal, handleShowCreateModal, handleHideCreateModal, setShowCreateModal } = useModalState();
  const { editingId, handleStartEdit, handleCancelEdit, setEditingId } = useEditingState();
  const handleRefetch = useCallback((): void => { listQuery.refetch().catch(() => {}); }, [listQuery]);
  const mutationsParams = useMutationsParams(handleRefetch, setEditingId, setShowCreateModal);
  const { isCreating, isUpdating, handleDelete, handleUpdate, handleCreate } = useTenantMutations(mutationsParams);

  return {
    tenants: listQuery.data?.tenants ?? [],
    isLoading: listQuery.isLoading,
    isError: listQuery.isError,
    editingId, showCreateModal, isCreating, isUpdating,
    handleRefetch, handleDelete, handleStartEdit, handleCancelEdit,
    handleUpdate, handleCreate, handleShowCreateModal, handleHideCreateModal,
  };
}

export type { TenantFormPayload };
