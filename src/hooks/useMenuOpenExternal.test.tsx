/**
 * Tests for useMenuOpenExternal hook.
 * Focus on testing the callback logic, not the underlying mutations.
 */
import { Linking } from 'react-native';

import { renderHook, act } from '@testing-library/react-native';

import { useMenuOpenExternal } from './useMenuPageHandlers';
import { notify } from '../lib/notifications';
import { logger } from '../utils/logger';
import { setPlatformOS, resetPlatformOS } from './__mocks__/reactNativePlatform';
import { mockT, setupCommonMocks } from './menuPageHandlers.testUtils';

// Mock dependencies
jest.mock('../lib/notifications', () => ({
  notify: jest.fn(),
  notifySuccess: jest.fn(),
}));

jest.mock('../utils/logger', () => ({
  logger: {
    error: jest.fn(),
    warn: jest.fn(),
    info: jest.fn(),
  },
}));

jest.mock('react-native', () => require('./__mocks__/reactNativePlatform'));

jest.mock('../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus', () => ({
  useOnlineMenuWebMenuList: jest.fn(() => ({ data: [], isLoading: false, refetch: jest.fn() })),
  useOnlineMenuWebMenuUpdate: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
  useOnlineMenuWebMenuDelete: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
  useOnlineMenuWebTenantMenusCreate: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
}));

jest.mock('./useMenuActions', () => ({
  useActivateMenu: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
  useDeactivateMenu: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
}));

describe('useMenuOpenExternal', () => {
  beforeEach(() => {
    setupCommonMocks();
    resetPlatformOS();
  });

  it('opens URL with correct path on web', () => {
    const mockOpenURL = Linking.openURL as jest.Mock;
    mockOpenURL.mockResolvedValue(undefined);

    const { result } = renderHook(() => useMenuOpenExternal(mockT));

    act(() => {
      result.current('menu-123');
    });

    expect(mockOpenURL).toHaveBeenCalledWith('https://test.com/public/menu/menu-123');
  });

  it('opens URL with empty base on native', () => {
    setPlatformOS('ios');
    const mockOpenURL = Linking.openURL as jest.Mock;
    mockOpenURL.mockResolvedValue(undefined);

    const { result } = renderHook(() => useMenuOpenExternal(mockT));

    act(() => {
      result.current('menu-456');
    });

    expect(mockOpenURL).toHaveBeenCalledWith('/public/menu/menu-456');
  });

  it('notifies error when openURL fails', async () => {
    const mockOpenURL = Linking.openURL as jest.Mock;
    mockOpenURL.mockRejectedValue(new Error('Could not open URL'));

    const { result } = renderHook(() => useMenuOpenExternal(mockT));

    await act(async () => {
      result.current('menu-123');
      await Promise.resolve();
    });

    expect(notify).toHaveBeenCalledWith('error', 'Failed to open external link: Could not open URL');
    expect(logger.error).toHaveBeenCalled();
  });
});
