/**
 * Tests for useCreateUser hook.
 * Focus on testing create mutation callbacks and notifications.
 */
import './userPageHandlers.setupMocks';

import { renderHook, act } from '@testing-library/react-native';

import { useCreateUser } from './useUserPageHandlers';
import { notify, notifySuccess } from '../lib/notifications';

import type { CreateUserCallbacks } from './userPageHandlers.testTypes';
import type { UserFormPayload } from '../components/Users/UserForm';
import type { useIdentityServiceAPIUsersCreateUser } from '../server/autoGeneratedHooks/identity/users/users';

type CreateMutation = ReturnType<typeof useIdentityServiceAPIUsersCreateUser>;

const createMockPayload = (): UserFormPayload => ({
  username: 'newuser',
  email: 'new@test.com',
  phoneNumber: '1234567890',
  firstName: 'New',
  lastName: 'User',
  password: 'password123',
  enabled: true,
  tenantId: 'tenant-1',
  roles: ['user'],
});

describe('useCreateUser', () => {
  beforeEach(() => {
    jest.clearAllMocks();
  });

  it('calls mutation with correct payload', () => {
    const mockMutate = jest.fn();
    const createMutation = { mutate: mockMutate } as unknown as CreateMutation;
    const refetchUsers = jest.fn();
    const callbacks: CreateUserCallbacks = { onSuccess: jest.fn() };

    const { result } = renderHook(() =>
      useCreateUser(createMutation, refetchUsers, callbacks)
    );

    act(() => {
      result.current(createMockPayload());
    });

    expect(mockMutate).toHaveBeenCalledWith(
      expect.objectContaining({
        data: expect.objectContaining({
          username: 'newuser',
          email: 'new@test.com',
          phoneNumber: '1234567890',
          firstName: 'New',
          lastName: 'User',
          password: 'password123',
          enabled: true,
          tenantId: 'tenant-1',
          roles: ['user'],
        }),
      }),
      expect.any(Object)
    );
  });

  it('handles null tenantId', () => {
    const mockMutate = jest.fn();
    const createMutation = { mutate: mockMutate } as unknown as CreateMutation;
    const refetchUsers = jest.fn();
    const callbacks: CreateUserCallbacks = { onSuccess: jest.fn() };

    const { result } = renderHook(() =>
      useCreateUser(createMutation, refetchUsers, callbacks)
    );

    act(() => {
      result.current({ ...createMockPayload(), tenantId: undefined });
    });

    expect(mockMutate).toHaveBeenCalledWith(
      expect.objectContaining({
        data: expect.objectContaining({ tenantId: null }),
      }),
      expect.any(Object)
    );
  });

  it('notifies success and refetches on successful create', () => {
    const mockMutate = jest.fn((_, options) => {
      options.onSuccess({ success: true });
    });
    const createMutation = { mutate: mockMutate } as unknown as CreateMutation;
    const refetchUsers = jest.fn();
    const callbacks: CreateUserCallbacks = { onSuccess: jest.fn() };

    const { result } = renderHook(() =>
      useCreateUser(createMutation, refetchUsers, callbacks)
    );

    act(() => {
      result.current(createMockPayload());
    });

    expect(notifySuccess).toHaveBeenCalledWith('users.messages.createSuccess');
    expect(callbacks.onSuccess).toHaveBeenCalled();
    expect(refetchUsers).toHaveBeenCalled();
  });

  it('notifies error when success is false', () => {
    const mockMutate = jest.fn((_, options) => {
      options.onSuccess({ success: false, errorMessage: 'Username already exists' });
    });
    const createMutation = { mutate: mockMutate } as unknown as CreateMutation;
    const refetchUsers = jest.fn();
    const callbacks: CreateUserCallbacks = { onSuccess: jest.fn() };

    const { result } = renderHook(() =>
      useCreateUser(createMutation, refetchUsers, callbacks)
    );

    act(() => {
      result.current(createMockPayload());
    });

    expect(notify).toHaveBeenCalledWith('error', {
      message: 'users.errors.createFailed:Username already exists',
    });
    expect(callbacks.onSuccess).not.toHaveBeenCalled();
  });

  it('uses default error message when errorMessage is null', () => {
    const mockMutate = jest.fn((_, options) => {
      options.onSuccess({ success: false, errorMessage: null });
    });
    const createMutation = { mutate: mockMutate } as unknown as CreateMutation;
    const refetchUsers = jest.fn();
    const callbacks: CreateUserCallbacks = { onSuccess: jest.fn() };

    const { result } = renderHook(() =>
      useCreateUser(createMutation, refetchUsers, callbacks)
    );

    act(() => {
      result.current(createMockPayload());
    });

    expect(notify).toHaveBeenCalledWith('error', {
      message: 'users.errors.createFailed:',
    });
  });

  it('notifies error on mutation error', () => {
    const mockMutate = jest.fn((_, options) => {
      options.onError(new Error('Network error'));
    });
    const createMutation = { mutate: mockMutate } as unknown as CreateMutation;
    const refetchUsers = jest.fn();
    const callbacks: CreateUserCallbacks = { onSuccess: jest.fn() };

    const { result } = renderHook(() =>
      useCreateUser(createMutation, refetchUsers, callbacks)
    );

    act(() => {
      result.current(createMockPayload());
    });

    expect(notify).toHaveBeenCalledWith('error', {
      message: 'users.errors.serverError:Network error',
    });
  });
});
