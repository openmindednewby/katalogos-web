/**
 * User query hooks for fetching and managing user data.
 */
import { useCallback, useMemo } from 'react';

import { useIdentityServiceAPITenantsListTenants } from '../../server/autoGeneratedHooks/identity/tenants/tenants';
import {
  useIdentityServiceAPIUsersListUsers,
  useIdentityServiceAPIUsersCreateUser,
  useIdentityServiceAPIUsersDeleteUser,
  useIdentityServiceAPIUsersSetUserEnabled,
  useIdentityServiceAPIUsersUpdatePassword,
} from '../../server/autoGeneratedHooks/identity/users/users';
import { isValueDefined } from '../../utils/is';
import { logger } from '../../utils/logger';
import { useGetRole } from '../useGetRole';

import type { UserItem } from '../../components/Users/UserListItem';
import type { IdentityServiceAPIUsersListUsersResponse } from '../../server/autoGeneratedHooks/identity/models';

function parseCreatedTimestamp(value: unknown): number | undefined {
  if (typeof value === 'number') return Number.isFinite(value) ? value : undefined;
  if (typeof value !== 'string' || value === '') return undefined;
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : undefined;
}

interface UserQueries {
  tenantsQuery: ReturnType<typeof useIdentityServiceAPITenantsListTenants>;
  usersQuery: ReturnType<typeof useIdentityServiceAPIUsersListUsers>;
  createMutation: ReturnType<typeof useIdentityServiceAPIUsersCreateUser>;
  deleteMutation: ReturnType<typeof useIdentityServiceAPIUsersDeleteUser>;
  setEnabledMutation: ReturnType<typeof useIdentityServiceAPIUsersSetUserEnabled>;
  updatePasswordMutation: ReturnType<typeof useIdentityServiceAPIUsersUpdatePassword>;
  refetchUsers: () => void;
  refetchTenants: () => void;
}

export function useUserQueries(): UserQueries {
  const { isSuperAdmin } = useGetRole();

  // Only fetch tenants if user is a superAdmin (platform administrator)
  // Tenant admins don't have permission to list all tenants
  const tenantsQuery = useIdentityServiceAPITenantsListTenants({ query: { enabled: isSuperAdmin } });
  const usersQuery = useIdentityServiceAPIUsersListUsers(undefined, { query: { enabled: true } });
  const createMutation = useIdentityServiceAPIUsersCreateUser();
  const deleteMutation = useIdentityServiceAPIUsersDeleteUser();
  const setEnabledMutation = useIdentityServiceAPIUsersSetUserEnabled();
  const updatePasswordMutation = useIdentityServiceAPIUsersUpdatePassword();

  const refetchUsers = useCallback((): void => {
    usersQuery.refetch().catch((e: unknown) => logger.warn('UsersPage', 'Failed to refetch users', e));
  }, [usersQuery]);

  const refetchTenants = useCallback((): void => {
    if (isSuperAdmin) 
      tenantsQuery.refetch().catch((e: unknown) => logger.warn('UsersPage', 'Failed to refetch tenants', e));
    
  }, [tenantsQuery, isSuperAdmin]);

  return {
    tenantsQuery,
    usersQuery,
    createMutation,
    deleteMutation,
    setEnabledMutation,
    updatePasswordMutation,
    refetchUsers,
    refetchTenants,
  };
}

function mapUserData(u: {
  id?: string | null;
  username?: string | null;
  email?: string | null;
  firstName?: string | null;
  lastName?: string | null;
  enabled?: boolean | null;
  createdTimestamp?: unknown;
  roles?: string[];
  tenantId?: string | null;
}): UserItem {
  return {
    id: u.id ?? '',
    username: u.username ?? '',
    email: u.email ?? undefined,
    firstName: u.firstName ?? undefined,
    lastName: u.lastName ?? undefined,
    enabled: u.enabled ?? false,
    createdTimestamp: parseCreatedTimestamp(u.createdTimestamp),
    roles: u.roles,
    tenantId: u.tenantId ?? undefined,
  };
}

export function useUsersList(
  usersQuery: ReturnType<typeof useIdentityServiceAPIUsersListUsers>,
  selectedTenantId: string | null,
): UserItem[] {
  // Cast to the generated response type to access `users` property
  // eslint-disable-next-line @typescript-eslint/consistent-type-assertions -- auto-generated hook returns opaque type
  const responseData = usersQuery.data as IdentityServiceAPIUsersListUsersResponse | undefined;
  const usersData = responseData?.users;
  const allUsers: UserItem[] = useMemo(
    () => {
      if (!Array.isArray(usersData)) return [];
      return usersData.map((u) => mapUserData(u));
    },
    [usersData],
  );

  return useMemo(() => {
    if (!isValueDefined(selectedTenantId)) return allUsers;
    return allUsers.filter((u) => u.tenantId === selectedTenantId);
  }, [allUsers, selectedTenantId]);
}
