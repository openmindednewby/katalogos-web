/**
 * User mutation handlers for create, delete, and toggle operations.
 */
import { useCallback } from 'react';

import { Alert } from 'react-native';

import { notify, notifySuccess } from '../../lib/notifications';
import { FM } from '../../localization/helpers';
import {
  type useIdentityServiceAPIUsersCreateUser,
  type useIdentityServiceAPIUsersDeleteUser,
  type useIdentityServiceAPIUsersSetUserEnabled,
} from '../../server/autoGeneratedHooks/identity/users/users';

import type { UserFormPayload } from '../../components/Users/UserForm';

function getErrorMsg(err: unknown): string {
  return err instanceof Error ? err.message : String(err ?? '');
}

interface CreateUserCallbacks {
  onSuccess: () => void;
}

interface CreateUserPayload {
  data: {
    username: string;
    email?: string;
    phoneNumber?: string;
    firstName?: string;
    lastName?: string;
    password?: string;
    enabled: boolean;
    tenantId: string | null;
    roles: string[];
  };
}

function buildCreateUserPayload(payload: UserFormPayload): CreateUserPayload {
  return {
    data: {
      username: payload.username,
      email: payload.email,
      phoneNumber: payload.phoneNumber,
      firstName: payload.firstName,
      lastName: payload.lastName,
      password: payload.password,
      enabled: payload.enabled,
      tenantId: payload.tenantId ?? null,
      roles: payload.roles,
    },
  };
}

function handleCreateUserSuccess(
  response: { success?: boolean; errorMessage?: string | null },
  callbacks: CreateUserCallbacks,
  refetchUsers: () => void,
): void {
  if (response.success === true) {
    callbacks.onSuccess();
    refetchUsers();
    notifySuccess(FM('users.messages.createSuccess'));
  } else
    notify('error', { message: FM('users.errors.createFailed', response.errorMessage ?? '') });

}

export function useCreateUser(
  createMutation: ReturnType<typeof useIdentityServiceAPIUsersCreateUser>,
  refetchUsers: () => void,
  callbacks: CreateUserCallbacks,
): (payload: UserFormPayload) => void {
  return useCallback(
    (payload: UserFormPayload): void => {
      createMutation.mutate(buildCreateUserPayload(payload), {
        onSuccess: (response) => handleCreateUserSuccess(response, callbacks, refetchUsers),
        onError: (err: unknown) => {
          notify('error', { message: FM('users.errors.serverError', getErrorMsg(err)) });
        },
      });
    },
    [createMutation, refetchUsers, callbacks],
  );
}

interface DeleteUserState {
  setDeletingUserId: (id: string | null) => void;
}

function executeDelete(
  userId: string,
  deleteMutation: ReturnType<typeof useIdentityServiceAPIUsersDeleteUser>,
  refetchUsers: () => void,
  state: DeleteUserState,
): void {
  state.setDeletingUserId(userId);
  deleteMutation.mutate(
    { userId },
    {
      onSuccess: () => {
        refetchUsers();
        notifySuccess(FM('users.messages.deleteSuccess'));
        state.setDeletingUserId(null);
      },
      onError: (err: unknown) => {
        notify('error', { message: FM('users.errors.deleteFailed', getErrorMsg(err)) });
        state.setDeletingUserId(null);
      },
    },
  );
}

export function useDeleteUser(
  deleteMutation: ReturnType<typeof useIdentityServiceAPIUsersDeleteUser>,
  refetchUsers: () => void,
  state: DeleteUserState,
): (userId: string) => void {
  return useCallback(
    (userId: string): void => {
      Alert.alert(
        FM('users.deleteConfirmTitle'),
        FM('users.deleteConfirmMessage'),
        [
          { text: FM('users.deleteConfirmCancel'), style: 'cancel' },
          {
            text: FM('users.deleteConfirmButton'),
            style: 'destructive',
            onPress: () => executeDelete(userId, deleteMutation, refetchUsers, state),
          },
        ],
      );
    },
    [deleteMutation, refetchUsers, state],
  );
}

interface ToggleEnabledState {
  setTogglingUserId: (id: string | null) => void;
}

export function useToggleUserEnabled(
  setEnabledMutation: ReturnType<typeof useIdentityServiceAPIUsersSetUserEnabled>,
  refetchUsers: () => void,
  state: ToggleEnabledState,
): (userId: string, enabled: boolean) => void {
  return useCallback(
    (userId: string, enabled: boolean): void => {
      state.setTogglingUserId(userId);
      setEnabledMutation.mutate(
        { userId, data: { enabled } },
        {
          onSuccess: () => {
            refetchUsers();
            notifySuccess(FM(enabled ? 'users.messages.enableSuccess' : 'users.messages.disableSuccess'));
            state.setTogglingUserId(null);
          },
          onError: (err: unknown) => {
            notify('error', { message: FM('users.errors.toggleFailed', getErrorMsg(err)) });
            state.setTogglingUserId(null);
          },
        },
      );
    },
    [setEnabledMutation, refetchUsers, state],
  );
}
