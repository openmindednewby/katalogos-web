/**
 * Tests for useMenuDelete hook.
 * Focus on testing the callback logic, not the underlying mutations.
 */
import { renderHook, act } from '@testing-library/react-native';

import {
  mockT,
  createMockMutation,
  createMockItem,
} from './menuPageHandlers.testUtils';
import { useMenuDelete } from './useMenuPageHandlers';
import { notify, notifySuccess } from '../lib/notifications';
import { type useOnlineMenuWebMenuDelete } from '../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus';
import { logger } from '../utils/logger';

type DeleteMutation = ReturnType<typeof useOnlineMenuWebMenuDelete>;

// Mock dependencies
jest.mock('../lib/notifications', () => ({
  notify: jest.fn(),
  notifySuccess: jest.fn(),
}));

jest.mock('../utils/logger', () => ({
  logger: {
    error: jest.fn(),
    warn: jest.fn(),
    info: jest.fn(),
  },
}));

jest.mock('react-native', () => ({
  Linking: { openURL: jest.fn() },
  Platform: { OS: 'web' },
}));

jest.mock('../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus', () => ({
  useOnlineMenuWebMenuList: jest.fn(() => ({ data: [], isLoading: false, refetch: jest.fn() })),
  useOnlineMenuWebMenuUpdate: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
  useOnlineMenuWebMenuDelete: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
  useOnlineMenuWebTenantMenusCreate: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
}));

jest.mock('./useMenuActions', () => ({
  useActivateMenu: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
  useDeactivateMenu: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
}));

describe('useMenuDelete', () => {
  beforeEach(() => {
    jest.clearAllMocks();
  });

  it('notifies error when externalId is missing', () => {
    const deleteMutation = createMockMutation() as unknown as DeleteMutation;
    const refetchMenusSoon = jest.fn();

    const { result } = renderHook(() =>
      useMenuDelete({ deleteMutation, refetchMenusSoon, t: mockT })
    );

    act(() => {
      result.current(createMockItem({ externalId: '' }));
    });

    expect(notify).toHaveBeenCalledWith('error', 'Failed to delete menu');
    expect(deleteMutation.mutate).not.toHaveBeenCalled();
  });

  it('notifies error when externalId is not a string', () => {
    const deleteMutation = createMockMutation() as unknown as DeleteMutation;
    const refetchMenusSoon = jest.fn();

    const { result } = renderHook(() =>
      useMenuDelete({ deleteMutation, refetchMenusSoon, t: mockT })
    );

    act(() => {
      result.current(createMockItem({ externalId: undefined }));
    });

    expect(notify).toHaveBeenCalledWith('error', 'Failed to delete menu');
    expect(deleteMutation.mutate).not.toHaveBeenCalled();
  });

  it('calls mutation with correct externalId', () => {
    const deleteMutation = createMockMutation() as unknown as DeleteMutation;
    const refetchMenusSoon = jest.fn();

    const { result } = renderHook(() =>
      useMenuDelete({ deleteMutation, refetchMenusSoon, t: mockT })
    );

    act(() => {
      result.current(createMockItem({ externalId: 'menu-456' }));
    });

    expect(deleteMutation.mutate).toHaveBeenCalledWith(
      { externalId: 'menu-456' },
      expect.objectContaining({
        onSuccess: expect.any(Function),
        onError: expect.any(Function),
      })
    );
  });

  it('notifies success and refetches on successful delete', () => {
    const mockMutate = jest.fn((_params, options) => {
      options.onSuccess();
    });
    const deleteMutation = createMockMutation(mockMutate) as unknown as DeleteMutation;
    const refetchMenusSoon = jest.fn();

    const { result } = renderHook(() =>
      useMenuDelete({ deleteMutation, refetchMenusSoon, t: mockT })
    );

    act(() => {
      result.current(createMockItem());
    });

    expect(notifySuccess).toHaveBeenCalledWith('Menu deleted successfully');
    expect(refetchMenusSoon).toHaveBeenCalled();
  });

  it('notifies error on delete failure', () => {
    const testError = new Error('Network error');
    const mockMutate = jest.fn((_params, options) => {
      options.onError(testError);
    });
    const deleteMutation = createMockMutation(mockMutate) as unknown as DeleteMutation;
    const refetchMenusSoon = jest.fn();

    const { result } = renderHook(() =>
      useMenuDelete({ deleteMutation, refetchMenusSoon, t: mockT })
    );

    act(() => {
      result.current(createMockItem());
    });

    expect(notify).toHaveBeenCalledWith('error', 'Failed to delete menu: Network error');
    expect(logger.error).toHaveBeenCalled();
  });

  it('handles error with message property', () => {
    const testError = { message: 'Custom error message' };
    const mockMutate = jest.fn((_params, options) => {
      options.onError(testError);
    });
    const deleteMutation = createMockMutation(mockMutate) as unknown as DeleteMutation;
    const refetchMenusSoon = jest.fn();

    const { result } = renderHook(() =>
      useMenuDelete({ deleteMutation, refetchMenusSoon, t: mockT })
    );

    act(() => {
      result.current(createMockItem());
    });

    expect(notify).toHaveBeenCalledWith('error', 'Failed to delete menu: Custom error message');
  });

  it('handles string error', () => {
    const mockMutate = jest.fn((_params, options) => {
      options.onError('String error');
    });
    const deleteMutation = createMockMutation(mockMutate) as unknown as DeleteMutation;
    const refetchMenusSoon = jest.fn();

    const { result } = renderHook(() =>
      useMenuDelete({ deleteMutation, refetchMenusSoon, t: mockT })
    );

    act(() => {
      result.current(createMockItem());
    });

    expect(notify).toHaveBeenCalledWith('error', 'Failed to delete menu: String error');
  });

  it('uses default error message for unknown error type', () => {
    const mockMutate = jest.fn((_params, options) => {
      options.onError(null);
    });
    const deleteMutation = createMockMutation(mockMutate) as unknown as DeleteMutation;
    const refetchMenusSoon = jest.fn();

    const { result } = renderHook(() =>
      useMenuDelete({ deleteMutation, refetchMenusSoon, t: mockT })
    );

    act(() => {
      result.current(createMockItem());
    });

    expect(notify).toHaveBeenCalledWith('error', 'Failed to delete menu: Error loading menus');
  });
});
