/**
 * Tests for useToggleUserEnabled hook.
 * Focus on testing enable/disable toggle callbacks and notifications.
 */
import './userPageHandlers.setupMocks';

import { renderHook, act } from '@testing-library/react-native';

import { useToggleUserEnabled } from './useUserPageHandlers';
import { notify, notifySuccess } from '../lib/notifications';

import type { ToggleUserState } from './userPageHandlers.testTypes';
import type { useIdentityServiceAPIUsersSetUserEnabled } from '../server/autoGeneratedHooks/identity/users/users';

type SetEnabledMutation = ReturnType<typeof useIdentityServiceAPIUsersSetUserEnabled>;

describe('useToggleUserEnabled', () => {
  beforeEach(() => {
    jest.clearAllMocks();
  });

  it('calls mutation with correct params for enabling', () => {
    const mockMutate = jest.fn();
    const setEnabledMutation = { mutate: mockMutate } as unknown as SetEnabledMutation;
    const refetchUsers = jest.fn();
    const state: ToggleUserState = { setTogglingUserId: jest.fn() };

    const { result } = renderHook(() =>
      useToggleUserEnabled(setEnabledMutation, refetchUsers, state)
    );

    act(() => {
      result.current('user-123', true);
    });

    expect(state.setTogglingUserId).toHaveBeenCalledWith('user-123');
    expect(mockMutate).toHaveBeenCalledWith(
      { userId: 'user-123', data: { enabled: true } },
      expect.any(Object)
    );
  });

  it('notifies success for enabling user', () => {
    const mockMutate = jest.fn((_, options) => {
      options.onSuccess();
    });
    const setEnabledMutation = { mutate: mockMutate } as unknown as SetEnabledMutation;
    const refetchUsers = jest.fn();
    const state: ToggleUserState = { setTogglingUserId: jest.fn() };

    const { result } = renderHook(() =>
      useToggleUserEnabled(setEnabledMutation, refetchUsers, state)
    );

    act(() => {
      result.current('user-123', true);
    });

    expect(notifySuccess).toHaveBeenCalledWith('users.messages.enableSuccess');
    expect(refetchUsers).toHaveBeenCalled();
    expect(state.setTogglingUserId).toHaveBeenCalledWith(null);
  });

  it('notifies success for disabling user', () => {
    const mockMutate = jest.fn((_, options) => {
      options.onSuccess();
    });
    const setEnabledMutation = { mutate: mockMutate } as unknown as SetEnabledMutation;
    const refetchUsers = jest.fn();
    const state: ToggleUserState = { setTogglingUserId: jest.fn() };

    const { result } = renderHook(() =>
      useToggleUserEnabled(setEnabledMutation, refetchUsers, state)
    );

    act(() => {
      result.current('user-123', false);
    });

    expect(notifySuccess).toHaveBeenCalledWith('users.messages.disableSuccess');
  });

  it('notifies error on failure', () => {
    const mockMutate = jest.fn((_, options) => {
      options.onError(new Error('Update failed'));
    });
    const setEnabledMutation = { mutate: mockMutate } as unknown as SetEnabledMutation;
    const refetchUsers = jest.fn();
    const state: ToggleUserState = { setTogglingUserId: jest.fn() };

    const { result } = renderHook(() =>
      useToggleUserEnabled(setEnabledMutation, refetchUsers, state)
    );

    act(() => {
      result.current('user-123', true);
    });

    expect(notify).toHaveBeenCalledWith('error', {
      message: 'users.errors.toggleFailed:Update failed',
    });
    expect(state.setTogglingUserId).toHaveBeenCalledWith(null);
  });
});
