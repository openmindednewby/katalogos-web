/**
 * Tests for useCurrentTenant hook.
 *
 * Focuses on the resolution logic — how the hook normalises the API response
 * (empty/whitespace-only names → null, missing tenantId → disabled query).
 * The autogen GET hook is mocked so we never hit the network.
 */
import { renderHook } from '@testing-library/react-native';

import { useCurrentTenant } from './useCurrentTenant';

const mockUseGetTenantById = jest.fn();
const mockUseAuth = jest.fn();

jest.mock('@/auth/AuthProvider', () => ({
  useAuth: (): unknown => mockUseAuth(),
}));

jest.mock('@/server/autoGeneratedHooks/identity/tenants/tenants', () => ({
  useIdentityServiceAPITenantsGetTenantById: (tenantId: string): unknown => mockUseGetTenantById(tenantId),
}));

describe('useCurrentTenant', () => {
  beforeEach(() => {
    mockUseAuth.mockReset();
    mockUseGetTenantById.mockReset();
  });

  it('returns the tenant name when API responds with a non-empty string', () => {
    mockUseAuth.mockReturnValue({ userInfo: { tenantId: 'tenant-1' } });
    mockUseGetTenantById.mockReturnValue({ data: { name: "Joe's Pizza" }, isLoading: false });

    const { result } = renderHook(() => useCurrentTenant());

    expect(result.current.tenantName).toBe("Joe's Pizza");
    expect(result.current.isLoading).toBe(false);
  });

  it('returns null when the API name is an empty string', () => {
    mockUseAuth.mockReturnValue({ userInfo: { tenantId: 'tenant-1' } });
    mockUseGetTenantById.mockReturnValue({ data: { name: '' }, isLoading: false });

    const { result } = renderHook(() => useCurrentTenant());

    expect(result.current.tenantName).toBeNull();
  });

  it('returns null when the API name is whitespace only', () => {
    mockUseAuth.mockReturnValue({ userInfo: { tenantId: 'tenant-1' } });
    mockUseGetTenantById.mockReturnValue({ data: { name: '   ' }, isLoading: false });

    const { result } = renderHook(() => useCurrentTenant());

    expect(result.current.tenantName).toBeNull();
  });

  it('returns null when tenantId is missing on userInfo', () => {
    mockUseAuth.mockReturnValue({ userInfo: { tenantId: undefined } });
    mockUseGetTenantById.mockReturnValue({ data: undefined, isLoading: false });

    const { result } = renderHook(() => useCurrentTenant());

    expect(result.current.tenantName).toBeNull();
    expect(result.current.isLoading).toBe(false);
  });

  it('returns null when userInfo is null (logged-out state)', () => {
    mockUseAuth.mockReturnValue({ userInfo: null });
    mockUseGetTenantById.mockReturnValue({ data: undefined, isLoading: false });

    const { result } = renderHook(() => useCurrentTenant());

    expect(result.current.tenantName).toBeNull();
    expect(result.current.isLoading).toBe(false);
  });

  it('passes the tenantId to the autogen hook when set', () => {
    mockUseAuth.mockReturnValue({ userInfo: { tenantId: 'tenant-xyz' } });
    mockUseGetTenantById.mockReturnValue({ data: undefined, isLoading: true });

    renderHook(() => useCurrentTenant());

    expect(mockUseGetTenantById).toHaveBeenCalledWith('tenant-xyz');
  });

  it('passes empty string to the autogen hook when tenantId is missing', () => {
    mockUseAuth.mockReturnValue({ userInfo: undefined });
    mockUseGetTenantById.mockReturnValue({ data: undefined, isLoading: false });

    renderHook(() => useCurrentTenant());

    expect(mockUseGetTenantById).toHaveBeenCalledWith('');
  });

  it('reports loading=true while the tenantId is set and the query is in flight', () => {
    mockUseAuth.mockReturnValue({ userInfo: { tenantId: 'tenant-1' } });
    mockUseGetTenantById.mockReturnValue({ data: undefined, isLoading: true });

    const { result } = renderHook(() => useCurrentTenant());

    expect(result.current.isLoading).toBe(true);
    expect(result.current.tenantName).toBeNull();
  });

  it('reports loading=false when no tenantId even if the query helper says loading=true', () => {
    // The autogen hook's enabled=!!tenantId means the query never actually runs,
    // but defensive: we don't want a stale loading=true to gate UI when there is
    // no tenant to load.
    mockUseAuth.mockReturnValue({ userInfo: { tenantId: '' } });
    mockUseGetTenantById.mockReturnValue({ data: undefined, isLoading: true });

    const { result } = renderHook(() => useCurrentTenant());

    expect(result.current.isLoading).toBe(false);
  });
});
