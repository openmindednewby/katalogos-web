import { useCallback } from 'react';

import { notify, notifySuccess } from '../../../lib/notifications';
import { FM } from '../../../localization/helpers';
import {
  useIdentityServiceAPITenantsCreateTenant,
  useIdentityServiceAPITenantsDeleteTenant,
  useIdentityServiceAPITenantsUpdateTenant,
} from '../../../server/autoGeneratedHooks/identity/tenants/tenants';
import { getErrorMessage } from '../../../utils/errorMessage';
import { buildTenantData } from '../tenantTypes';

import type { TenantFormPayload } from '../tenantTypes';

interface UseTenantMutationsParams {
  refetch: () => void;
  setEditingId: (id: string | null) => void;
  setShowCreateModal: (show: boolean) => void;
}

interface UseTenantMutationsReturn {
  isCreating: boolean;
  isUpdating: boolean;
  handleDelete: (id: string) => void;
  handleUpdate: (tenantId: string, payload: TenantFormPayload) => void;
  handleCreate: (payload: TenantFormPayload) => void;
}

/** Create success handler for delete operation */
function createDeleteSuccessHandler(refetch: () => void): () => void {
  return () => {
    refetch();
    try {
      notifySuccess(FM('tenants.messages.deleteSuccess'));
    } catch {
      // ignore notifications failure
    }
  };
}

/** Create error handler for mutation operations */
function createErrorHandler(): (err: unknown) => void {
  return (err: unknown) => {
    const msg = getErrorMessage(err, 'Unknown error');
    notify('error', { message: FM('tenants.errors.server', msg) });
  };
}

/** Create success handler for update operation */
function createUpdateSuccessHandler(setEditingId: (id: string | null) => void, refetch: () => void): () => void {
  return () => {
    setEditingId(null);
    refetch();
    notifySuccess(FM('tenants.messages.updateSuccess'));
  };
}

/** Create success handler for create operation */
function createCreateSuccessHandler(setShowCreateModal: (show: boolean) => void, refetch: () => void): () => void {
  return () => {
    setShowCreateModal(false);
    refetch();
    notifySuccess(FM('tenants.messages.createSuccess'));
  };
}

/** Hook for delete handler */
function useDeleteHandler(
  deleteMutation: ReturnType<typeof useIdentityServiceAPITenantsDeleteTenant>,
  refetch: () => void,
): (id: string) => void {
  return useCallback(
    (id: string): void => {
      const onSuccess = createDeleteSuccessHandler(refetch);
      deleteMutation.mutate({ tenantId: id }, { onSuccess });
    },
    [deleteMutation, refetch],
  );
}

/** Hook for update handler */
function useUpdateHandler(
  updateMutation: ReturnType<typeof useIdentityServiceAPITenantsUpdateTenant>,
  setEditingId: (id: string | null) => void,
  refetch: () => void,
): (tenantId: string, payload: TenantFormPayload) => void {
  return useCallback(
    (tenantId: string, payload: TenantFormPayload): void => {
      const onSuccess = createUpdateSuccessHandler(setEditingId, refetch);
      const onError = createErrorHandler();
      updateMutation.mutate({ data: { tenantId, ...buildTenantData(payload) } }, { onSuccess, onError });
    },
    [refetch, setEditingId, updateMutation],
  );
}

/** Hook for create handler */
function useCreateHandler(
  createMutation: ReturnType<typeof useIdentityServiceAPITenantsCreateTenant>,
  setShowCreateModal: (show: boolean) => void,
  refetch: () => void,
): (payload: TenantFormPayload) => void {
  return useCallback(
    (payload: TenantFormPayload): void => {
      const onSuccess = createCreateSuccessHandler(setShowCreateModal, refetch);
      const onError = createErrorHandler();
      createMutation.mutate({ data: buildTenantData(payload) }, { onSuccess, onError });
    },
    [createMutation, refetch, setShowCreateModal],
  );
}

/**
 * Hook that encapsulates tenant mutation operations (create, update, delete).
 */
export function useTenantMutations(params: UseTenantMutationsParams): UseTenantMutationsReturn {
  const { refetch, setEditingId, setShowCreateModal } = params;

  const createMutation = useIdentityServiceAPITenantsCreateTenant();
  const updateMutation = useIdentityServiceAPITenantsUpdateTenant();
  const deleteMutation = useIdentityServiceAPITenantsDeleteTenant();

  const handleDelete = useDeleteHandler(deleteMutation, refetch);
  const handleUpdate = useUpdateHandler(updateMutation, setEditingId, refetch);
  const handleCreate = useCreateHandler(createMutation, setShowCreateModal, refetch);

  return {
    isCreating: createMutation.status === 'pending',
    isUpdating: updateMutation.status === 'pending',
    handleDelete,
    handleUpdate,
    handleCreate,
  };
}
