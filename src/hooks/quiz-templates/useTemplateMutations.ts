import { useCallback, useMemo } from 'react';

import { notify, notifySuccess } from '../../lib/notifications';
import { FM } from '../../localization/helpers';
import {
  useQuestionerWebQuestionerTemplatesCreate,
  useQuestionerWebQuestionerTemplatesDelete,
  useQuestionerWebQuestionerTemplatesUpdate,
} from '../../server/autoGeneratedHooks/questioner/questionertemplates/questionertemplates';
import AnalyticsEventName from '../../shared/enums/AnalyticsEventName';
import { getErrorMessage } from '../../utils/errorMessage';
import { isValueDefined } from '../../utils/is';
import { logger } from '../../utils/logger';

import type { AnalyticsTrackFn } from '../../lib/analytics';
import type { QuestionerTemplateDto } from '../../server/autoGeneratedHooks/questioner/models/questionerTemplateDto';
import type { UpdateQuestionerTemplateRequest } from '../../server/autoGeneratedHooks/questioner/models/updateQuestionerTemplateRequest';

interface UseTemplateMutationsParams {
  editingItem: QuestionerTemplateDto | null;
  items: QuestionerTemplateDto[];
  refetchTemplatesSoon: () => void;
  setIsModalVisible: (visible: boolean) => void;
  setEditingItem: (item: QuestionerTemplateDto | null) => void;
  analyticsTrack?: AnalyticsTrackFn;
}

interface UseTemplateMutationsReturn {
  isCreating: boolean;
  handleSaveEdit: (payload: UpdateQuestionerTemplateRequest) => void;
  handleDelete: (id: string) => void;
  handleActivate: (id: string, current?: boolean | number) => void;
  handleCreate: (payload: UpdateQuestionerTemplateRequest) => void;
}

/** Safely show success notification with error handling */
function showSuccessNotification(messageKey: string): void {
  try {
    notifySuccess(FM(messageKey));
  } catch (_err) {
    logger.warn('useTemplateMutations', 'notifySuccess failed');
  }
}

/** Create success handler for save edit operation */
function createSaveEditSuccessHandler(
  refetchTemplatesSoon: () => void,
  setIsModalVisible: (visible: boolean) => void,
  setEditingItem: (item: QuestionerTemplateDto | null) => void,
): () => void {
  return () => {
    refetchTemplatesSoon();
    setIsModalVisible(false);
    setEditingItem(null);
    showSuccessNotification('quizTemplates.messages.updateSuccess');
  };
}

/** Create success handler for delete operation */
function createDeleteSuccessHandler(refetchTemplatesSoon: () => void): () => void {
  return () => {
    refetchTemplatesSoon();
    showSuccessNotification('quizTemplates.messages.deleteSuccess');
  };
}

/** Create handlers for activate operation */
interface ActivateHandlers {
  onSuccess: () => void;
  onError: (err: unknown) => void;
}

function createActivateHandlers(refetchTemplatesSoon: () => void): ActivateHandlers {
  return {
    onSuccess: () => {
      refetchTemplatesSoon();
      showSuccessNotification('quizTemplates.messages.activateSuccess');
    },
    onError: (err: unknown) => {
      const apiMessage = getErrorMessage(err, 'Error updating template');
      notify('error', { message: apiMessage });
      logger.error('useTemplateMutations', `Activation failed: ${apiMessage}`);
    },
  };
}

/** Create success handler for create operation */
function createCreateSuccessHandler(refetchTemplatesSoon: () => void): () => void {
  return () => {
    refetchTemplatesSoon();
    showSuccessNotification('quizTemplates.messages.createSuccess');
  };
}

/** Build activate mutation data from item */
function buildActivateMutationData(
  id: string,
  item: QuestionerTemplateDto,
  newIsActive: boolean,
): { externalId: string; data: UpdateQuestionerTemplateRequest } {
  return {
    externalId: id,
    data: { name: item.name, description: item.description, contents: item.contents, isActive: newIsActive },
  };
}

/** Options for save/edit and create handlers */
interface SaveAndCreateOptions {
  editingItem: QuestionerTemplateDto | null;
  refetchTemplatesSoon: () => void;
  setIsModalVisible: (visible: boolean) => void;
  setEditingItem: (item: QuestionerTemplateDto | null) => void;
  updateMutation: ReturnType<typeof useQuestionerWebQuestionerTemplatesUpdate>;
  createMutation: ReturnType<typeof useQuestionerWebQuestionerTemplatesCreate>;
  analyticsTrack?: AnalyticsTrackFn;
}

/** Hook for save/edit and create operations */
function useSaveAndCreateHandlers(
  options: SaveAndCreateOptions,
): { handleSaveEdit: (p: UpdateQuestionerTemplateRequest) => void; handleCreate: (p: UpdateQuestionerTemplateRequest) => void } {
  const { editingItem, refetchTemplatesSoon, setIsModalVisible, setEditingItem, updateMutation, createMutation, analyticsTrack } = options;
  const handleSaveEdit = useCallback(
    (payload: UpdateQuestionerTemplateRequest): void => {
      const externalId = editingItem?.externalId;
      if (!isValueDefined(externalId) || externalId.length === 0) return;
      const onSuccess = createSaveEditSuccessHandler(refetchTemplatesSoon, setIsModalVisible, setEditingItem);
      updateMutation.mutate({ externalId, data: payload }, { onSuccess });
    },
    [editingItem?.externalId, refetchTemplatesSoon, setEditingItem, setIsModalVisible, updateMutation],
  );

  const handleCreate = useCallback(
    (payload: UpdateQuestionerTemplateRequest): void => {
      const onSuccess = createCreateSuccessHandler(refetchTemplatesSoon);
      createMutation.mutate({ data: payload }, {
        onSuccess: () => {
          onSuccess();
          analyticsTrack?.(AnalyticsEventName.QuizTemplateCreated);
        },
      });
    },
    [createMutation, refetchTemplatesSoon, analyticsTrack],
  );

  return { handleSaveEdit, handleCreate };
}

/** Options for delete and activate handlers */
interface DeleteAndActivateOptions {
  items: QuestionerTemplateDto[];
  refetchTemplatesSoon: () => void;
  updateMutation: ReturnType<typeof useQuestionerWebQuestionerTemplatesUpdate>;
  deleteMutation: ReturnType<typeof useQuestionerWebQuestionerTemplatesDelete>;
}

/** Hook for delete and activate operations */
function useDeleteAndActivateHandlers(
  options: DeleteAndActivateOptions,
): { handleDelete: (id: string) => void; handleActivate: (id: string, current?: boolean | number) => void } {
  const { items, refetchTemplatesSoon, updateMutation, deleteMutation } = options;
  const handleDelete = useCallback(
    (id: string): void => {
      if (id.length === 0) return;
      const onSuccess = createDeleteSuccessHandler(refetchTemplatesSoon);
      deleteMutation.mutate({ externalId: id }, { onSuccess });
    },
    [deleteMutation, refetchTemplatesSoon],
  );

  const handleActivate = useCallback(
    (id: string, current?: boolean | number): void => {
      if (id.length === 0) return;
      const currentBool = current === true || current === 1;
      const item = items.find((i) => i.externalId === id);
      if (!isValueDefined(item)) return;
      const mutationData = buildActivateMutationData(id, item, !currentBool);
      updateMutation.mutate(mutationData, createActivateHandlers(refetchTemplatesSoon));
    },
    [items, refetchTemplatesSoon, updateMutation],
  );

  return { handleDelete, handleActivate };
}

/**
 * Hook that encapsulates template mutation operations (create, update, delete, activate).
 */
export function useTemplateMutations(params: UseTemplateMutationsParams): UseTemplateMutationsReturn {
  const { editingItem, items, refetchTemplatesSoon, setIsModalVisible, setEditingItem, analyticsTrack } = params;

  const createMutation = useQuestionerWebQuestionerTemplatesCreate();
  const updateMutation = useQuestionerWebQuestionerTemplatesUpdate();
  const deleteMutation = useQuestionerWebQuestionerTemplatesDelete();

  const saveAndCreateOptions = useMemo(() => ({
    editingItem, refetchTemplatesSoon, setIsModalVisible, setEditingItem, updateMutation, createMutation, analyticsTrack,
  }), [editingItem, refetchTemplatesSoon, setIsModalVisible, setEditingItem, updateMutation, createMutation, analyticsTrack]);
  const { handleSaveEdit, handleCreate } = useSaveAndCreateHandlers(saveAndCreateOptions);
  const deleteAndActivateOptions = useMemo(() => ({
    items, refetchTemplatesSoon, updateMutation, deleteMutation,
  }), [items, refetchTemplatesSoon, updateMutation, deleteMutation]);
  const { handleDelete, handleActivate } = useDeleteAndActivateHandlers(deleteAndActivateOptions);

  return { isCreating: createMutation.status === 'pending', handleSaveEdit, handleDelete, handleActivate, handleCreate };
}
