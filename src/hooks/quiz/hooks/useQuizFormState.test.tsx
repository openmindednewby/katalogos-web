/**
 * Tests for useQuizFormState hook - covers BUG-QUIZ-002, BUG-QUIZ-004, BUG-QUIZ-005 fixes.
 */

import { renderHook, act } from '@testing-library/react-native';

import { useQuizFormState } from './useQuizFormState';
import QuestionType from '../../../shared/enums/QuestionType';

import type { Question as ApiQuestionModel } from '../../../server/autoGeneratedHooks/questioner/models';

function createTemplateData(questions: ApiQuestionModel[] = []): { externalId: string; name: string; contents: { questions: ApiQuestionModel[] } } {
  return { externalId: 'tpl-1', name: 'Test Quiz', contents: { questions } };
}

type TFunction = (k: string, d?: string) => string;
const mockT: TFunction = jest.fn((k: string, d?: string) => d ?? k);

describe('useQuizFormState', () => {
  beforeEach(() => { jest.clearAllMocks(); });

  describe('BUG-QUIZ-002: t function change does not reset form', () => {
    it('does not reset answers when t reference changes', () => {
      const data = createTemplateData([
        { id: 'q1', name: 'Q1', type: QuestionType.Text, page: 1, order: 1, isRequired: false },
      ]);
      const t1: TFunction = jest.fn((k: string, d?: string) => d ?? k);
      const { result, rerender } = renderHook(
        ({ tFn }: { tFn: TFunction }) => useQuizFormState(data, tFn),
        { initialProps: { tFn: t1 } },
      );

      act(() => { result.current.updateAnswer('q1', 'my-answer'); });
      const answeredQuestion = result.current.form?.questions.find((q) => q.id === 'q1');
      expect(answeredQuestion?.answer).toBe('my-answer');

      const t2: TFunction = jest.fn((k: string, d?: string) => d ?? k);
      rerender({ tFn: t2 });

      const afterRerender = result.current.form?.questions.find((q) => q.id === 'q1');
      expect(afterRerender?.answer).toBe('my-answer');
    });
  });

  describe('BUG-QUIZ-004: validateAllPages checks all pages', () => {
    it('returns false when required fields on earlier pages are unanswered', () => {
      const data = createTemplateData([
        { id: 'q1', name: 'Q1', type: QuestionType.Text, page: 1, order: 1, isRequired: true },
        { id: 'q2', name: 'Q2', type: QuestionType.Text, page: 2, order: 2, isRequired: true },
      ]);
      const { result } = renderHook(() => useQuizFormState(data, mockT));

      act(() => { result.current.updateAnswer('q2', 'answered'); });

      let isValid = false;
      act(() => { isValid = result.current.validateAllPages(); });
      expect(isValid).toBe(false);
      expect(result.current.errors).toHaveProperty('q1');
      expect(result.current.errors).not.toHaveProperty('q2');
    });

    it('returns true when all required fields across all pages are answered', () => {
      const data = createTemplateData([
        { id: 'q1', name: 'Q1', type: QuestionType.Text, page: 1, order: 1, isRequired: true },
        { id: 'q2', name: 'Q2', type: QuestionType.Text, page: 2, order: 2, isRequired: true },
      ]);
      const { result } = renderHook(() => useQuizFormState(data, mockT));

      act(() => { result.current.updateAnswer('q1', 'answer1'); });
      act(() => { result.current.updateAnswer('q2', 'answer2'); });

      let isValid = false;
      act(() => { isValid = result.current.validateAllPages(); });
      expect(isValid).toBe(true);
      expect(Object.keys(result.current.errors)).toHaveLength(0);
    });

    it('skips skipped questions during full validation', () => {
      const data = createTemplateData([
        { id: 'trigger', name: 'Trigger', type: QuestionType.Text, page: 1, order: 1, isRequired: false },
        {
          id: 'skipped', name: 'Skipped', type: QuestionType.Text, page: 1, order: 2, isRequired: true,
          skipConditions: [{ questionId: 'trigger', questionAnswer: 'skip-value' }],
        },
      ]);
      const { result } = renderHook(() => useQuizFormState(data, mockT));

      act(() => { result.current.updateAnswer('trigger', 'skip-value'); });

      let isValid = false;
      act(() => { isValid = result.current.validateAllPages(); });
      expect(isValid).toBe(true);
    });

    it('validatePage only shows errors for the given page', () => {
      const data = createTemplateData([
        { id: 'q1', name: 'Q1', type: QuestionType.Text, page: 1, order: 1, isRequired: true },
        { id: 'q2', name: 'Q2', type: QuestionType.Text, page: 2, order: 2, isRequired: true },
      ]);
      const { result } = renderHook(() => useQuizFormState(data, mockT));

      let isValid = false;
      act(() => { isValid = result.current.validatePage(1); });
      expect(isValid).toBe(false);
      expect(result.current.errors).toHaveProperty('q1');
      expect(result.current.errors).not.toHaveProperty('q2');
    });
  });

  describe('BUG-QUIZ-005: no double navigation from updateAnswer', () => {
    it('updateAnswer does not directly trigger navigation', () => {
      const data = createTemplateData([
        { id: 'q1', name: 'Q1', type: QuestionType.Radio, page: 1, order: 1, isRequired: true, options: [{ label: 'A', value: 'a' }] },
        { id: 'q2', name: 'Q2', type: QuestionType.Text, page: 2, order: 2, isRequired: false },
      ]);
      const { result } = renderHook(() => useQuizFormState(data, mockT));

      act(() => { result.current.updateAnswer('q1', 'a'); });

      const q1 = result.current.form?.questions.find((q) => q.id === 'q1');
      expect(q1?.answer).toBe('a');
      expect(result.current.currentPage).toBe(1);
    });
  });

  describe('form initialization', () => {
    it('initializes form from template data', () => {
      const data = createTemplateData([
        { id: 'q1', name: 'Question 1', type: QuestionType.Text, page: 1, order: 1, isRequired: true },
      ]);
      const { result } = renderHook(() => useQuizFormState(data, mockT));

      expect(result.current.form).not.toBeNull();
      expect(result.current.form?.questions).toHaveLength(1);
      expect(result.current.currentPage).toBe(1);
    });

    it('returns null form when data is undefined', () => {
      const { result } = renderHook(() => useQuizFormState(undefined, mockT));
      expect(result.current.form).toBeNull();
    });
  });
});
