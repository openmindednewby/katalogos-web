/**
 * Helper functions for quiz-active page.
 * Extracted to reduce component size and improve maintainability.
 */
import { QuestionType } from '../../../components/DynamicForm';
import { QuestionType as ApiQuestionType } from '../../../server/autoGeneratedHooks/questioner/models/questionType';
import { isValueDefined } from '../../../utils/is';


import type { DynamicQuiz, Answer, Question as UiQuestion } from '../../../components/DynamicForm';
import type {
  Question as ApiQuestionModel,
  QuestionType as ApiQuestionTypeT,
  Option as ApiOption,
  SkipCondition as ApiSkipCondition,
  CreateCompletedQuestionerRequest,
} from '../../../server/autoGeneratedHooks/questioner/models';

type ApiAnswerPayload =
  | { boolValue: boolean }
  | { numericValue: number }
  | { stringValue: string }
  | { multiValues: string[] };

export function apiToUiType(typeValue: number | undefined): QuestionType {
  switch (typeValue) {
    case undefined:
      return QuestionType.Text;
    case ApiQuestionType.MultipleChoice:
      return QuestionType.MultipleChoice;
    case ApiQuestionType.Checkbox:
      return QuestionType.Checkbox;
    case ApiQuestionType.Radio:
      return QuestionType.Radio;
    case ApiQuestionType.Dropdown:
      return QuestionType.Dropdown;
    case ApiQuestionType.Text:
    default:
      return QuestionType.Text;
  }
}

export function uiToApiType(typeValue: QuestionType): ApiQuestionTypeT {
  switch (typeValue) {
    case QuestionType.MultipleChoice:
      return ApiQuestionType.MultipleChoice;
    case QuestionType.Checkbox:
      return ApiQuestionType.Checkbox;
    case QuestionType.Radio:
      return ApiQuestionType.Radio;
    case QuestionType.Dropdown:
      return ApiQuestionType.Dropdown;
    case QuestionType.Text:
    default:
      return ApiQuestionType.Text;
  }
}

export function shouldSkipForForm(form: DynamicQuiz, question: UiQuestion): boolean {
  if (!Array.isArray(question.skipConditions) || question.skipConditions.length === 0) return false;
  return question.skipConditions.some((condition) => {
    const trigger = form.questions.find((q) => q.id === condition.questionId);
    if (!isValueDefined(trigger)) return false;
    const answer = trigger.answer;
    if (!isValueDefined(answer)) return false;
    if (Array.isArray(answer)) 
      return answer.some((v) => String(v) === String(condition.questionAnswer));
    
    return String(answer) === String(condition.questionAnswer);
  });
}

export function pageHasVisibleQuestion(form: DynamicQuiz, page: number): boolean {
  return form.questions.some((q) => q.page === page && !shouldSkipForForm(form, q));
}

export function getOrderedPages(form: DynamicQuiz): number[] {
  return [...new Set(form.questions.map((q) => q.page))].sort((a, b) => a - b);
}

export function findPageInDirection(form: DynamicQuiz, startPage: number, direction: 1 | -1): number | null {
  const orderedPages = getOrderedPages(form);
  if (direction > 0) 
    for (const page of orderedPages) {
      if (page > startPage && pageHasVisibleQuestion(form, page)) 
        return page;
      
    }
   else 
    for (let idx = orderedPages.length - 1; idx >= 0; idx -= 1) {
      const page = orderedPages[idx];
      if (page < startPage && pageHasVisibleQuestion(form, page)) 
        return page;
      
    }
  
  return null;
}

export function isRequiredAnswerMissing(answer: Answer | undefined): boolean {
  if (!isValueDefined(answer)) return true;
  if (answer === '') return true;
  if (Array.isArray(answer)) return answer.length === 0;
  return false;
}

export function isPageComplete(form: DynamicQuiz, page: number): boolean {
  const questionsOnPage = form.questions.filter((q) => q.page === page);
  const hasMissing = questionsOnPage.some((q) => {
    if (shouldSkipForForm(form, q)) return false;
    if (q.isRequired !== true) return false;
    return isRequiredAnswerMissing(q.answer);
  });
  return !hasMissing;
}

export function toApiAnswer(value: Answer | undefined): ApiAnswerPayload | null {
  if (!isValueDefined(value)) return null;
  if (typeof value === 'boolean') return { boolValue: value };
  if (typeof value === 'number') return { numericValue: value };
  if (typeof value === 'string') return { stringValue: value };
  if (Array.isArray(value)) return { multiValues: value.map((x) => String(x)) };
  return null;
}

export function mapApiQuestionToUi(q: ApiQuestionModel, idx: number): UiQuestion {
  const apiTypeNum: number | undefined = typeof q.type === 'number' ? q.type : undefined;
  let uiType = apiToUiType(apiTypeNum);
  // Heuristic: if server says Text but provides options, treat as dropdown for better UX
  if (uiType === QuestionType.Text && (q.options?.length ?? 0) > 0)
    uiType = QuestionType.Dropdown;
  
  const pageRaw = q.page ?? 1;
  const pageUi = pageRaw <= 0 ? 1 : pageRaw; // normalize 0-based to 1-based
  return {
    id: q.id ?? `q_${idx}`,
    name: q.name ?? '',
    type: uiType,
    options: (q.options ?? [])
      .filter(Boolean)
      .map((opt: ApiOption) => ({ label: String(opt.label ?? ''), value: String(opt.value ?? '') })),
    page: pageUi,
    answer: undefined,
    isRequired: Boolean(q.isRequired),
    skipConditions: (q.skipConditions ?? [])
      .filter(Boolean)
      .map((sc: ApiSkipCondition) => ({
        questionId: String(sc.questionId ?? ''),
        questionAnswer: String(sc.questionAnswer ?? ''),
      })),
    order: q.order ?? idx + 1,
  };
}

export function buildSubmissionPayload(
  form: DynamicQuiz,
  tpl: { name?: string | null; description?: string | null; externalId?: string | null }
): CreateCompletedQuestionerRequest {
  return {
    name: tpl.name ?? form.name,
    description: tpl.description ?? form.description ?? null,
    questionerTemplateExternalId: tpl.externalId ?? undefined,
    contents: {
      questions: form.questions.map((q) => {
        const typeValue = uiToApiType(q.type);
        return {
          id: q.id,
          name: q.name,
          type: typeValue,
          options: (q.options ?? []).map((o) => ({ label: o.label, value: String(o.value) })),
          page: q.page,
          answer: toApiAnswer(q.answer),
          isRequired: q.isRequired ?? false,
          skipConditions: (q.skipConditions ?? []).map((sc) => ({
            questionId: sc.questionId,
            questionAnswer: String(sc.questionAnswer),
          })),
          order: q.order,
        };
      }),
    },
  };
}
