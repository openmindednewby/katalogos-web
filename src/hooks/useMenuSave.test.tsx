/**
 * Tests for useMenuSave hook.
 * Focus on testing the callback logic, not the underlying mutations.
 */
import { renderHook, act } from '@testing-library/react-native';

import { mockT, createMockMutation, createMockSaveCallbacks } from './menuPageHandlers.testUtils';
import { useMenuSave } from './useMenuPageHandlers';
import { notify, notifySuccess } from '../lib/notifications';
import { logger } from '../utils/logger';

import type { MockSaveCallbacks } from './menuPageHandlers.testUtils';
import type { useOnlineMenuWebMenuUpdate , useOnlineMenuWebTenantMenusCreate } from '../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus';
import type { TenantMenusDto } from '../types/menuTypes';

jest.mock('../lib/notifications', () => ({ notify: jest.fn(), notifySuccess: jest.fn() }));
jest.mock('../utils/logger', () => ({ logger: { error: jest.fn(), warn: jest.fn(), info: jest.fn() } }));
jest.mock('react-native', () => ({ Linking: { openURL: jest.fn() }, Platform: { OS: 'web' } }));
jest.mock('../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus', () => ({
  useOnlineMenuWebMenuList: jest.fn(() => ({ data: [], isLoading: false, refetch: jest.fn() })),
  useOnlineMenuWebMenuUpdate: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
  useOnlineMenuWebMenuDelete: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
  useOnlineMenuWebTenantMenusCreate: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
}));
jest.mock('./useMenuActions', () => ({
  useActivateMenu: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
  useDeactivateMenu: jest.fn(() => ({ mutate: jest.fn(), isLoading: false })),
}));

type CreateMutation = ReturnType<typeof useOnlineMenuWebTenantMenusCreate>;
type UpdateMutation = ReturnType<typeof useOnlineMenuWebMenuUpdate>;

interface SaveHookArgs {
  createMutation: CreateMutation;
  updateMutation: UpdateMutation;
  callbacks: MockSaveCallbacks;
  editingItem?: TenantMenusDto | null;
}

// eslint-disable-next-line @typescript-eslint/explicit-function-return-type
const renderSaveHook = (args: SaveHookArgs) =>
  renderHook(() =>
    useMenuSave({
      editingItem: args.editingItem ?? null,
      createMutation: args.createMutation,
      updateMutation: args.updateMutation,
      callbacks: args.callbacks,
      t: mockT,
    })
  );

describe('useMenuSave', () => {
  beforeEach(() => {
    jest.clearAllMocks();
  });

  it('calls create mutation when editingItem is null', () => {
    const mockCreate = jest.fn();
    const { result } = renderSaveHook({
      createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
      updateMutation: createMockMutation() as unknown as UpdateMutation,
      callbacks: createMockSaveCallbacks(),
    });

    act(() => {
      result.current({ name: 'New Menu', contents: { categories: [] } });
    });

    expect(mockCreate).toHaveBeenCalledWith(
      expect.objectContaining({ data: expect.objectContaining({ name: 'New Menu' }) }),
      expect.any(Object)
    );
  });

  it('calls update mutation when editingItem has externalId', () => {
    const mockUpdate = jest.fn();
    const editingItem: TenantMenusDto = { externalId: 'menu-123', name: 'Existing Menu', isActive: false };
    const { result } = renderSaveHook({
      createMutation: createMockMutation() as unknown as CreateMutation,
      updateMutation: createMockMutation(mockUpdate) as unknown as UpdateMutation,
      callbacks: createMockSaveCallbacks(),
      editingItem,
    });

    act(() => {
      result.current({ name: 'Updated Menu', description: 'Updated', contents: { categories: [] } });
    });

    expect(mockUpdate).toHaveBeenCalledWith(
      expect.objectContaining({
        data: expect.objectContaining({ externalId: 'menu-123', name: 'Updated Menu', description: 'Updated' }),
      }),
      expect.any(Object)
    );
  });

  it('notifies success and closes modal on create success', () => {
    const mockCreate = jest.fn((_p, opts) => opts.onSuccess());
    const callbacks = createMockSaveCallbacks();
    const { result } = renderSaveHook({
      createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
      updateMutation: createMockMutation() as unknown as UpdateMutation,
      callbacks,
    });

    act(() => {
      result.current({ name: 'New Menu', contents: { categories: [] } });
    });

    expect(notifySuccess).toHaveBeenCalledWith('Menu created successfully');
    expect(callbacks.onCloseModal).toHaveBeenCalled();
    expect(callbacks.refetchMenusSoon).toHaveBeenCalled();
  });

  it('notifies success on update success', () => {
    const mockUpdate = jest.fn((_p, opts) => opts.onSuccess());
    const editingItem: TenantMenusDto = { externalId: 'menu-123', name: 'Menu', isActive: false };
    const { result } = renderSaveHook({
      createMutation: createMockMutation() as unknown as CreateMutation,
      updateMutation: createMockMutation(mockUpdate) as unknown as UpdateMutation,
      callbacks: createMockSaveCallbacks(),
      editingItem,
    });

    act(() => {
      result.current({ name: 'Updated Menu', contents: { categories: [] } });
    });

    expect(notifySuccess).toHaveBeenCalledWith('Menu updated successfully');
  });

  it('notifies error on save failure', () => {
    const mockCreate = jest.fn((_p, opts) => opts.onError(new Error('Save failed')));
    const { result } = renderSaveHook({
      createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
      updateMutation: createMockMutation() as unknown as UpdateMutation,
      callbacks: createMockSaveCallbacks(),
    });

    act(() => {
      result.current({ name: 'New Menu', contents: { categories: [] } });
    });

    expect(notify).toHaveBeenCalledWith('error', 'Failed to save menu: Save failed');
    expect(logger.error).toHaveBeenCalled();
  });

  it('shows duplicate name error for 409 conflict response', () => {
    const conflictError = { response: { status: 409 }, message: 'Conflict' };
    const mockCreate = jest.fn((_p, opts) => opts.onError(conflictError));
    const { result } = renderSaveHook({
      createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
      updateMutation: createMockMutation() as unknown as UpdateMutation,
      callbacks: createMockSaveCallbacks(),
    });

    act(() => {
      result.current({ name: 'Duplicate Menu', contents: { categories: [] } });
    });

    expect(notify).toHaveBeenCalledWith('error', 'A menu with this name already exists');
    expect(logger.error).toHaveBeenCalled();
  });

  it('handles null description correctly', () => {
    const mockCreate = jest.fn();
    const { result } = renderSaveHook({
      createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
      updateMutation: createMockMutation() as unknown as UpdateMutation,
      callbacks: createMockSaveCallbacks(),
    });

    act(() => {
      result.current({ name: 'Menu', description: null, contents: { categories: [] } });
    });

    expect(mockCreate).toHaveBeenCalledWith(
      expect.objectContaining({ data: expect.objectContaining({ description: undefined }) }),
      expect.any(Object)
    );
  });

  describe('create with contents (two-step process)', () => {
    it('calls create then update when creating a menu with categories', () => {
      const mockCreate = jest.fn();
      const mockUpdate = jest.fn();

      // Simulate create success returning externalId
      mockCreate.mockImplementation((_payload, options) => {
        options.onSuccess({ externalId: 'new-menu-123' });
      });

      const callbacks = createMockSaveCallbacks();
      const { result } = renderSaveHook({
        createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
        updateMutation: createMockMutation(mockUpdate) as unknown as UpdateMutation,
        callbacks,
      });

      const contentsWithCategories = {
        categories: [{ name: 'Category 1', items: [], displayOrder: 0 }],
      };

      act(() => {
        result.current({ name: 'New Menu', contents: contentsWithCategories });
      });

      // Create should be called first
      expect(mockCreate).toHaveBeenCalledWith(
        expect.objectContaining({ data: expect.objectContaining({ name: 'New Menu' }) }),
        expect.any(Object)
      );

      // Update should be called with the externalId from create response
      expect(mockUpdate).toHaveBeenCalledWith(
        expect.objectContaining({
          data: expect.objectContaining({
            externalId: 'new-menu-123',
            name: 'New Menu',
            contents: contentsWithCategories,
          }),
        }),
        expect.any(Object)
      );
    });

    it('does not call update when creating menu without categories', () => {
      const mockCreate = jest.fn((_payload, options) => options.onSuccess({}));
      const mockUpdate = jest.fn();

      const { result } = renderSaveHook({
        createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
        updateMutation: createMockMutation(mockUpdate) as unknown as UpdateMutation,
        callbacks: createMockSaveCallbacks(),
      });

      act(() => {
        result.current({ name: 'Simple Menu', contents: { categories: [] } });
      });

      expect(mockCreate).toHaveBeenCalled();
      expect(mockUpdate).not.toHaveBeenCalled();
    });

    it('notifies success only after update completes for menu with contents', () => {
      const mockCreate = jest.fn();
      const mockUpdate = jest.fn();

      // Simulate create success
      mockCreate.mockImplementation((_payload, options) => {
        options.onSuccess({ externalId: 'new-menu-123' });
      });

      // Simulate update success
      mockUpdate.mockImplementation((_payload, options) => {
        options.onSuccess();
      });

      const callbacks = createMockSaveCallbacks();
      const { result } = renderSaveHook({
        createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
        updateMutation: createMockMutation(mockUpdate) as unknown as UpdateMutation,
        callbacks,
      });

      act(() => {
        result.current({
          name: 'New Menu',
          contents: { categories: [{ name: 'Cat', items: [], displayOrder: 0 }] },
        });
      });

      expect(notifySuccess).toHaveBeenCalledWith('Menu created successfully');
      expect(callbacks.onCloseModal).toHaveBeenCalled();
      expect(callbacks.refetchMenusSoon).toHaveBeenCalled();
    });

    it('notifies error if create fails before update', () => {
      const mockCreate = jest.fn((_payload, options) => options.onError(new Error('Create failed')));
      const mockUpdate = jest.fn();

      const { result } = renderSaveHook({
        createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
        updateMutation: createMockMutation(mockUpdate) as unknown as UpdateMutation,
        callbacks: createMockSaveCallbacks(),
      });

      act(() => {
        result.current({
          name: 'New Menu',
          contents: { categories: [{ name: 'Cat', items: [], displayOrder: 0 }] },
        });
      });

      expect(notify).toHaveBeenCalledWith('error', 'Failed to save menu: Create failed');
      expect(mockUpdate).not.toHaveBeenCalled();
    });

    it('notifies error if update fails after create', () => {
      const mockCreate = jest.fn();
      const mockUpdate = jest.fn();

      mockCreate.mockImplementation((_payload, options) => {
        options.onSuccess({ externalId: 'new-menu-123' });
      });

      mockUpdate.mockImplementation((_payload, options) => {
        options.onError(new Error('Update failed'));
      });

      const { result } = renderSaveHook({
        createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
        updateMutation: createMockMutation(mockUpdate) as unknown as UpdateMutation,
        callbacks: createMockSaveCallbacks(),
      });

      act(() => {
        result.current({
          name: 'New Menu',
          contents: { categories: [{ name: 'Cat', items: [], displayOrder: 0 }] },
        });
      });

      expect(notify).toHaveBeenCalledWith('error', 'Failed to save menu: Update failed');
    });

    it('notifies error if create response has no externalId', () => {
      const mockCreate = jest.fn((_payload, options) => options.onSuccess({}));
      const mockUpdate = jest.fn();

      const { result } = renderSaveHook({
        createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
        updateMutation: createMockMutation(mockUpdate) as unknown as UpdateMutation,
        callbacks: createMockSaveCallbacks(),
      });

      act(() => {
        result.current({
          name: 'New Menu',
          contents: { categories: [{ name: 'Cat', items: [], displayOrder: 0 }] },
        });
      });

      expect(notify).toHaveBeenCalledWith('error', 'Failed to save menu: No externalId returned from create');
      expect(mockUpdate).not.toHaveBeenCalled();
    });

    it('includes imageContentId in contents when saving category with image', () => {
      const mockCreate = jest.fn();
      const mockUpdate = jest.fn();

      mockCreate.mockImplementation((_payload, options) => {
        options.onSuccess({ externalId: 'new-menu-123' });
      });

      const { result } = renderSaveHook({
        createMutation: createMockMutation(mockCreate) as unknown as CreateMutation,
        updateMutation: createMockMutation(mockUpdate) as unknown as UpdateMutation,
        callbacks: createMockSaveCallbacks(),
      });

      const contentsWithImage = {
        categories: [{
          name: 'Category with Image',
          items: [],
          displayOrder: 0,
          imageContentId: 'img-content-456',
        }],
      };

      act(() => {
        result.current({ name: 'Menu with Image', contents: contentsWithImage });
      });

      expect(mockUpdate).toHaveBeenCalledWith(
        expect.objectContaining({
          data: expect.objectContaining({
            contents: expect.objectContaining({
              categories: expect.arrayContaining([
                expect.objectContaining({ imageContentId: 'img-content-456' }),
              ]),
            }),
          }),
        }),
        expect.any(Object)
      );
    });
  });
});
