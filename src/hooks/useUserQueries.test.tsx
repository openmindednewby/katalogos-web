/**
 * Tests for useUserQueries hook.
 * Focus on testing query and mutation object availability.
 */
import './userPageHandlers.setupMocks';

import { renderHook, act } from '@testing-library/react-native';

import { useUserQueries } from './useUserPageHandlers';

describe('useUserQueries', () => {
  beforeEach(() => {
    jest.clearAllMocks();
  });

  it('returns all query and mutation objects', () => {
    const { result } = renderHook(() => useUserQueries());

    expect(result.current.tenantsQuery).toBeDefined();
    expect(result.current.usersQuery).toBeDefined();
    expect(result.current.createMutation).toBeDefined();
    expect(result.current.deleteMutation).toBeDefined();
    expect(result.current.setEnabledMutation).toBeDefined();
    expect(result.current.updatePasswordMutation).toBeDefined();
    expect(result.current.refetchUsers).toBeDefined();
    expect(result.current.refetchTenants).toBeDefined();
  });

  it('refetchUsers calls refetch on usersQuery', () => {
    const mockRefetch = jest.fn().mockResolvedValue({});
    const {
      useIdentityServiceAPIUsersListUsers,
    } = require('../server/autoGeneratedHooks/identity/users/users');
    useIdentityServiceAPIUsersListUsers.mockReturnValue({
      data: { users: [] },
      isLoading: false,
      refetch: mockRefetch,
    });

    const { result } = renderHook(() => useUserQueries());

    act(() => {
      result.current.refetchUsers();
    });

    expect(mockRefetch).toHaveBeenCalled();
  });

  it('refetchTenants calls refetch when isSuperAdmin', () => {
    const mockRefetch = jest.fn().mockResolvedValue({});
    const {
      useIdentityServiceAPITenantsListTenants,
    } = require('../server/autoGeneratedHooks/identity/tenants/tenants');
    useIdentityServiceAPITenantsListTenants.mockReturnValue({
      data: { tenants: [] },
      isLoading: false,
      refetch: mockRefetch,
    });

    const { result } = renderHook(() => useUserQueries());

    act(() => {
      result.current.refetchTenants();
    });

    expect(mockRefetch).toHaveBeenCalled();
  });

  it('refetchTenants does not call refetch when not superAdmin', () => {
    const { useGetRole } = require('./useGetRole');
    useGetRole.mockReturnValue({
      isSuperAdmin: false,
      isTenantAdmin: true,
      role: 'tenantAdmin',
    });

    const mockRefetch = jest.fn().mockResolvedValue({});
    const {
      useIdentityServiceAPITenantsListTenants,
    } = require('../server/autoGeneratedHooks/identity/tenants/tenants');
    useIdentityServiceAPITenantsListTenants.mockReturnValue({
      data: { tenants: [] },
      isLoading: false,
      refetch: mockRefetch,
    });

    const { result } = renderHook(() => useUserQueries());

    act(() => {
      result.current.refetchTenants();
    });

    expect(mockRefetch).not.toHaveBeenCalled();

    // Reset mock
    useGetRole.mockReturnValue({
      isSuperAdmin: true,
      isTenantAdmin: false,
      role: 'superAdmin',
    });
  });
});
