import { useCallback, useMemo, useState } from 'react';

import { useTemplateMutations } from './quiz-templates';
import { useAnalytics } from '../lib/analytics';
import { useQuestionerWebQuestionerTemplatesList } from '../server/autoGeneratedHooks/questioner/questionertemplates/questionertemplates';

import type { QuestionerTemplateDto } from '../server/autoGeneratedHooks/questioner/models/questionerTemplateDto';
import type { UpdateQuestionerTemplateRequest } from '../server/autoGeneratedHooks/questioner/models/updateQuestionerTemplateRequest';

/** Delay in ms before second refetch to ensure cache refresh */
const REFETCH_DELAY_MS = 250;

interface UseQuizTemplateActionsReturn {
  items: QuestionerTemplateDto[];
  isLoading: boolean;
  isError: boolean;
  error: unknown;
  editingItem: QuestionerTemplateDto | null;
  isModalVisible: boolean;
  isCreating: boolean;
  handleRefetch: () => void;
  handleEdit: (item: QuestionerTemplateDto) => void;
  handleCancelEdit: () => void;
  handleSaveEdit: (payload: UpdateQuestionerTemplateRequest) => void;
  handleDelete: (id: string) => void;
  handleActivate: (id: string, current?: boolean | number) => void;
  handleCreate: (payload: UpdateQuestionerTemplateRequest) => void;
  handleDeleteInactiveSuccess: () => void;
}

/** Sort templates by creation date descending */
function sortTemplatesByDate(templates: QuestionerTemplateDto[]): QuestionerTemplateDto[] {
  return [...templates].sort((a, b) => {
    const aTime = typeof a.createdDate === 'string' ? new Date(a.createdDate).getTime() : 0;
    const bTime = typeof b.createdDate === 'string' ? new Date(b.createdDate).getTime() : 0;
    return bTime - aTime;
  });
}

/** Hook for template editing modal state */
function useTemplateModalState(): {
  editingItem: QuestionerTemplateDto | null;
  isModalVisible: boolean;
  setEditingItem: (item: QuestionerTemplateDto | null) => void;
  setIsModalVisible: (visible: boolean) => void;
  handleEdit: (item: QuestionerTemplateDto) => void;
  handleCancelEdit: () => void;
} {
  const [editingItem, setEditingItem] = useState<QuestionerTemplateDto | null>(null);
  const [isModalVisible, setIsModalVisible] = useState(false);

  const handleEdit = useCallback((item: QuestionerTemplateDto): void => {
    setEditingItem(item);
    setIsModalVisible(true);
  }, []);

  const handleCancelEdit = useCallback((): void => {
    setIsModalVisible(false);
    setEditingItem(null);
  }, []);

  return { editingItem, isModalVisible, setEditingItem, setIsModalVisible, handleEdit, handleCancelEdit };
}

/** Build return object for useQuizTemplateActions */
interface BuildReturnParams {
  items: QuestionerTemplateDto[];
  listQuery: ReturnType<typeof useQuestionerWebQuestionerTemplatesList>;
  modalState: ReturnType<typeof useTemplateModalState>;
  mutations: ReturnType<typeof useTemplateMutations>;
  handleRefetch: () => void;
  handleDeleteInactiveSuccess: () => void;
}

function buildReturnObject(params: BuildReturnParams): UseQuizTemplateActionsReturn {
  const { items, listQuery, modalState, mutations, handleRefetch, handleDeleteInactiveSuccess } = params;
  return {
    items,
    isLoading: listQuery.isLoading,
    isError: listQuery.isError,
    error: listQuery.error,
    editingItem: modalState.editingItem,
    isModalVisible: modalState.isModalVisible,
    isCreating: mutations.isCreating,
    handleRefetch,
    handleEdit: modalState.handleEdit,
    handleCancelEdit: modalState.handleCancelEdit,
    handleSaveEdit: mutations.handleSaveEdit,
    handleDelete: mutations.handleDelete,
    handleActivate: mutations.handleActivate,
    handleCreate: mutations.handleCreate,
    handleDeleteInactiveSuccess,
  };
}

/** Execute delayed refetch */
function executeDelayedRefetch(refetchFn: () => void): void {
  if (typeof setTimeout === 'function') 
    setTimeout(refetchFn, REFETCH_DELAY_MS);
  
}

/**
 * Hook that encapsulates all quiz template CRUD operations and state management.
 */
export function useQuizTemplateActions(): UseQuizTemplateActionsReturn {
  const listQuery = useQuestionerWebQuestionerTemplatesList();
  const modalState = useTemplateModalState();
  const { track } = useAnalytics();

  const doRefetch = useCallback((): void => {
    listQuery.refetch().catch(() => {});
  }, [listQuery]);

  const refetchTemplatesSoon = useCallback(() => {
    doRefetch();
    executeDelayedRefetch(doRefetch);
  }, [doRefetch]);

  const items = useMemo(() => sortTemplatesByDate(listQuery.data?.questionerTemplates ?? []), [listQuery.data?.questionerTemplates]);

  const templateMutationsParams = useMemo(() => ({
    editingItem: modalState.editingItem,
    items,
    refetchTemplatesSoon,
    setIsModalVisible: modalState.setIsModalVisible,
    setEditingItem: modalState.setEditingItem,
    analyticsTrack: track,
  }), [modalState.editingItem, items, refetchTemplatesSoon, modalState.setIsModalVisible, modalState.setEditingItem, track]);
  const mutations = useTemplateMutations(templateMutationsParams);

  const handleDeleteInactiveSuccess = useCallback((): void => { refetchTemplatesSoon(); }, [refetchTemplatesSoon]);

  return buildReturnObject({ items, listQuery, modalState, mutations, handleRefetch: doRefetch, handleDeleteInactiveSuccess });
}
