/**
 * Menu save mutation handlers for create and update operations.
 */
import { useCallback } from 'react';

import { getErrorMessage, isDuplicateNameError } from './menuErrorUtils';
import { notify, notifySuccess } from '../../lib/notifications';
import { type useOnlineMenuWebMenuUpdate , type useOnlineMenuWebTenantMenusCreate } from '../../server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus';
import AnalyticsEventName from '../../shared/enums/AnalyticsEventName';
import { isValueDefined } from '../../utils/is';
import { logger } from '../../utils/logger';

import type { AnalyticsTrackFn } from '../../lib/analytics';
import type { TranslateFn } from '../../localization/types';
import type { CreateTenantMenusResponse } from '../../server/autoGeneratedHooks/onlinemenu/models/createTenantMenusResponse';
import type { MenuContents } from '../../server/autoGeneratedHooks/onlinemenu/models/menuContents';
import type { UpdateTenantMenusRequest } from '../../server/autoGeneratedHooks/onlinemenu/models/updateTenantMenusRequest';
import type { TenantMenusDto } from '../../types/menuTypes';

interface MenuMutationCallbacks {
  onCloseModal: () => void;
  refetchMenusSoon: () => void;
}

interface UseMenuSaveArgs {
  editingItem: TenantMenusDto | null;
  createMutation: ReturnType<typeof useOnlineMenuWebTenantMenusCreate>;
  updateMutation: ReturnType<typeof useOnlineMenuWebMenuUpdate>;
  callbacks: MenuMutationCallbacks;
  t: TranslateFn;
  analyticsTrack?: AnalyticsTrackFn;
}

interface MenuSaveData {
  name: string;
  description?: string | null;
  contents: MenuContents;
}

function handleMenuUpdate(
  request: UpdateTenantMenusRequest,
  updateMutation: ReturnType<typeof useOnlineMenuWebMenuUpdate>,
  onSuccess: () => void,
  onError: (err: unknown) => void,
): void {
  updateMutation.mutate({ data: request }, { onSuccess, onError });
}

/** Checks if contents have any categories to save */
function hasContentsToSave(contents: MenuContents): boolean {
  return isValueDefined(contents.categories) && contents.categories.length > 0;
}

/** Parameters for handleMenuCreate */
interface HandleMenuCreateParams {
  data: MenuSaveData;
  createMutation: ReturnType<typeof useOnlineMenuWebTenantMenusCreate>;
  updateMutation: ReturnType<typeof useOnlineMenuWebMenuUpdate>;
  onSuccess: () => void;
  onError: (err: unknown) => void;
}

/** Handler for the create success callback when contents need to be saved */
function createWithContentsSuccessHandler(
  params: Omit<HandleMenuCreateParams, 'createMutation'>,
): (response: CreateTenantMenusResponse) => void {
  const { data, updateMutation, onSuccess, onError } = params;
  return (response: CreateTenantMenusResponse) => {
    const externalId = response.externalId;
    if (!isValueDefined(externalId)) {
      onError(new Error('No externalId returned from create'));
      return;
    }
    const updateRequest: UpdateTenantMenusRequest = {
      externalId,
      name: data.name,
      description: data.description ?? undefined,
      contents: data.contents,
    };
    updateMutation.mutate({ data: updateRequest }, { onSuccess, onError });
  };
}

function handleMenuCreate(params: HandleMenuCreateParams): void {
  const { data, createMutation, updateMutation, onSuccess, onError } = params;
  const needsContentsUpdate = hasContentsToSave(data.contents);

  if (needsContentsUpdate) {
    // Two-step process: create menu, then update with contents
    const onCreateSuccess = createWithContentsSuccessHandler({ data, updateMutation, onSuccess, onError });
    createMutation.mutate(
      { data: { name: data.name, description: data.description ?? undefined } },
      { onSuccess: onCreateSuccess, onError },
    );
  } else
    // Simple create without contents
    createMutation.mutate(
      { data: { name: data.name, description: data.description ?? undefined } },
      { onSuccess, onError },
    );
}

function createSuccessHandler(t: TranslateFn, callbacks: MenuMutationCallbacks) {
  return (messageKey: string): void => {
    notifySuccess(t(messageKey));
    callbacks.onCloseModal();
    callbacks.refetchMenusSoon();
  };
}

function createErrorHandler(t: TranslateFn) {
  return (err: unknown, logContext: string): void => {
    if (isDuplicateNameError(err)) 
      notify('error', t('onlineMenus.errors.duplicateName'));
     else {
      const message = getErrorMessage(err);
      notify('error', `${t('onlineMenus.errors.saveFailed')}: ${message}`);
    }
    logger.error('OnlineMenusPage', logContext, err);
  };
}

function buildUpdateRequest(editingItem: TenantMenusDto, data: MenuSaveData): UpdateTenantMenusRequest {
  return {
    externalId: editingItem.externalId,
    name: data.name,
    description: data.description ?? undefined,
    contents: data.contents,
  };
}

interface PerformUpdateArgs {
  editingItem: TenantMenusDto;
  data: MenuSaveData;
  updateMutation: ReturnType<typeof useOnlineMenuWebMenuUpdate>;
  onSuccess: (key: string) => void;
  onError: (err: unknown, ctx: string) => void;
  analyticsTrack?: AnalyticsTrackFn;
}

function performUpdate(args: PerformUpdateArgs): void {
  const { editingItem, data, updateMutation, onSuccess, onError, analyticsTrack } = args;
  const request = buildUpdateRequest(editingItem, data);
  handleMenuUpdate(
    request,
    updateMutation,
    () => {
      onSuccess('onlineMenus.messages.updateSuccess');
      analyticsTrack?.(AnalyticsEventName.MenuUpdated, { menuId: editingItem.externalId });
    },
    (err) => onError(err, 'Update error:'),
  );
}

export function useMenuSave(args: UseMenuSaveArgs): (data: MenuSaveData) => void {
  const { editingItem, createMutation, updateMutation, callbacks, t, analyticsTrack } = args;
  return useCallback(
    (data: MenuSaveData) => {
      const onSuccess = createSuccessHandler(t, callbacks);
      const onError = createErrorHandler(t);
      const isEditing = isValueDefined(editingItem) && typeof editingItem.externalId === 'string';

      if (isEditing)
        performUpdate({ editingItem, data, updateMutation, onSuccess, onError, analyticsTrack });
      else
        handleMenuCreate({
          data,
          createMutation,
          updateMutation,
          onSuccess: () => {
            onSuccess('onlineMenus.messages.createSuccess');
            analyticsTrack?.(AnalyticsEventName.MenuCreated);
          },
          onError: (err) => onError(err, 'Create error:'),
        });

    },
    [editingItem, createMutation, updateMutation, callbacks, t, analyticsTrack],
  );
}
