/**
 * Custom hook for fetching a public menu by ID.
 * This hook does NOT require authentication and is used for public menu pages.
 */
import { useQuery } from '@tanstack/react-query';

import env from '../../config/environment';
import { get } from '../../lib/http/utils/methods';

import type { PublicMenuDto } from '../autoGeneratedHooks/onlinemenu/models';
import type { UseQueryOptions, UseQueryResult } from '@tanstack/react-query';

/**
 * Time constants for stale time calculation.
 */
const MINUTES_5 = 5;
const SECONDS_PER_MINUTE = 60;
const MS_PER_SECOND = 1000;

/**
 * Stale time for public menu data (5 minutes).
 */
const PUBLIC_MENU_STALE_TIME_MS = MINUTES_5 * SECONDS_PER_MINUTE * MS_PER_SECOND;

interface PublicMenuQueryParams {
  lang?: string;
  locationId?: string;
}

/** Type guard: returns true when the string is non-empty. */
function hasValue(value: string | undefined): value is string {
  if (typeof value !== 'string') return false;
  return value.length > 0;
}

/**
 * Query key for public menu queries.
 * Includes language and locationId so React Query refetches when either changes.
 */
export const getPublicMenuQueryKey = (
  externalId: string,
  lang?: string,
  locationId?: string,
): string[] => [
  'public',
  'menus',
  externalId,
  ...(hasValue(lang) ? [lang] : []),
  ...(hasValue(locationId) ? [`loc:${locationId}`] : []),
];

/**
 * Fetches a public menu by external ID without authentication.
 * Optionally passes a language code and/or locationId for filtered content.
 */
async function fetchPublicMenu(
  externalId: string,
  lang?: string,
  locationId?: string,
  signal?: AbortSignal,
): Promise<PublicMenuDto> {
  const queryParams: PublicMenuQueryParams = {};
  if (hasValue(lang)) queryParams.lang = lang;
  if (hasValue(locationId)) queryParams.locationId = locationId;

  const hasParams = Object.keys(queryParams).length > 0;

  // MUST be the VERSIONED path. The OnlineMenu API applies a global FastEndpoints
  // `RoutePrefix = "api/v1"`, so the AllowAnonymous public endpoint registers at
  // `/api/v1/public/menus/{id}`. The unversioned `/public/menus/{id}` matches no
  // route → the fallback auth policy 401s it (the diner "Failed to load menu"
  // bug). Keep in sync with the generated `onlineMenuWebPublicMenusGetById` path.
  return get<PublicMenuQueryParams | undefined, PublicMenuDto>(
    `/api/v1/public/menus/${externalId}`,
    hasParams ? queryParams : undefined,
    {
      withToken: false,
      withCredentials: false,
      baseURL: env.API_URL,
      signal,
    },
  );
}

/**
 * Hook for fetching a public menu by external ID.
 * This hook does NOT require authentication and is intended for public-facing menu pages
 * where users may not be logged in.
 *
 * @param externalId - The external ID of the menu to fetch
 * @param lang - Optional BCP-47 language code for translated content
 * @param locationId - Optional location ID for location-specific overrides
 * @param options - Additional React Query options
 *
 * @example
 * ```tsx
 * const { data: menu, isLoading, isError } = usePublicMenuGetById(menuId, 'es', 'loc-1');
 *
 * if (isLoading) return <Loading />;
 * if (isError) return <Error />;
 * if (menu) return <MenuDisplay menu={menu} />;
 * ```
 */
export function usePublicMenuGetById(
  externalId: string,
  lang?: string,
  locationId?: string,
  options?: Omit<UseQueryOptions<PublicMenuDto>, 'queryKey' | 'queryFn'>,
): UseQueryResult<PublicMenuDto> {
  // Check if external ID is a non-empty string
  const isValidId = externalId !== '';

  return useQuery({
    queryKey: getPublicMenuQueryKey(externalId, lang, locationId),
    queryFn: async ({ signal }) => fetchPublicMenu(externalId, lang, locationId, signal),
    enabled: isValidId,
    staleTime: PUBLIC_MENU_STALE_TIME_MS,
    ...options,
  });
}

export default usePublicMenuGetById;
