/** ISO 8601 day of week (0=Monday through 6=Sunday). */
const enum DayOfWeek {
  Monday = 0,
  Tuesday = 1,
  Wednesday = 2,
  Thursday = 3,
  Friday = 4,
  Saturday = 5,
  Sunday = 6,
}

/** Total number of days in a week. */
export const DAYS_IN_WEEK = 7;

export default DayOfWeek;
