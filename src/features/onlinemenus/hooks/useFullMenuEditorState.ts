/**
 * State management hook for FullMenuEditor.
 * Wraps useUndoRedo to provide named setters and integrates keyboard shortcuts.
 */
import { useCallback, useEffect, useMemo, useRef, useState } from 'react';

import { useUndoRedo } from '@/components/OnlineMenus/hooks/useUndoRedo';
import type { EditorSnapshot } from '@/components/OnlineMenus/hooks/useUndoRedo';
import { useEditorKeyboardShortcuts } from '@/hooks/useEditorKeyboardShortcuts';
import type { TenantMenusDto } from '@/server/autoGeneratedHooks/onlinemenu/models/tenantMenusDto';
import { ensureMenuContentsHaveIds } from '@/types/menuTypes';
import type { MenuContents } from '@/types/menuTypes';
import { isValueDefined } from '@/utils/is';

import { toLocalMenuContents } from '../components/menuContentsAdapter';
import EditorTab from '../enums/EditorTab';

function buildSnapshot(item: TenantMenusDto | null): EditorSnapshot {
  return {
    name: item?.name ?? '',
    description: item?.description ?? '',
    menuContents: ensureMenuContentsHaveIds(toLocalMenuContents(item?.contents)),
  };
}

interface UseFullMenuEditorStateReturn {
  activeTab: EditorTab;
  setActiveTab: (tab: EditorTab) => void;
  name: string;
  description: string;
  menuContents: MenuContents;
  nameError: string;
  setNameError: (error: string) => void;
  handleNameChange: (value: string) => void;
  handleDescriptionChange: (value: string) => void;
  handleMenuContentsChange: (value: MenuContents) => void;
  canUndo: boolean;
  canRedo: boolean;
  undo: () => void;
  redo: () => void;
  activeCategoryId: string | null;
  setActiveCategoryId: (id: string | null) => void;
  collapseAllRef: React.MutableRefObject<(() => void) | null>;
}

/**
 * Snapshot reset: replace the editor's present state from `item` when (a) the
 * user opens a different menu (externalId changes) or (b) the user hasn't
 * started editing yet (canUndo === false). Skipping the reset once `canUndo`
 * is true preserves in-flight edits when a background list refetch returns a
 * new `item` reference for the same menu.
 */
function useSnapshotReset(visible: boolean, item: TenantMenusDto | null, reset: (s: EditorSnapshot) => void, canUndo: boolean): void {
  const lastResetIdRef = useRef<string | undefined>();
  const externalId = item?.externalId;
  useEffect(() => {
    if (!visible) {
      lastResetIdRef.current = undefined;
      return;
    }
    const externalIdChanged = lastResetIdRef.current !== externalId;
    if (externalIdChanged || !canUndo) {
      lastResetIdRef.current = externalId;
      reset(buildSnapshot(item));
    }
  }, [visible, item, externalId, reset, canUndo]);
}

/** Tab, name-error, and active-category local state. */
function useTabState(visible: boolean, item: TenantMenusDto | null, reset: (s: EditorSnapshot) => void, canUndo: boolean): {
  activeTab: EditorTab;
  setActiveTab: (tab: EditorTab) => void;
  nameError: string;
  setNameError: (error: string) => void;
  activeCategoryId: string | null;
  setActiveCategoryId: (id: string | null) => void;
} {
  const [activeTab, setActiveTab] = useState<EditorTab>(EditorTab.Metadata);
  const [nameError, setNameError] = useState('');
  const [activeCategoryId, setActiveCategoryId] = useState<string | null>(null);
  const externalId = item?.externalId;

  useSnapshotReset(visible, item, reset, canUndo);

  // UI reset: only fires when the editor opens (visible false→true) or the user
  // switches to a different menu (externalId changes). Doesn't snap the user
  // back to the Details tab just because a background refetch returned an
  // otherwise-equivalent item reference.
  useEffect(() => {
    if (visible) {
      setNameError('');
      setActiveTab(EditorTab.Metadata);
      setActiveCategoryId(null);
    }
  }, [visible, externalId]);

  return { activeTab, setActiveTab, nameError, setNameError, activeCategoryId, setActiveCategoryId };
}

/** Clear activeCategoryId if the category was deleted from content. */
function useStaleCategoryGuard(
  categories: MenuContents['categories'],
  activeCategoryId: string | null,
  setActiveCategoryId: (id: string | null) => void,
): void {
  useEffect(() => {
    if (!isValueDefined(activeCategoryId)) return;
    const ids = (categories ?? []).map((c) => String(c.id ?? ''));
    if (!ids.includes(activeCategoryId)) setActiveCategoryId(null);
  }, [categories, activeCategoryId, setActiveCategoryId]);
}

/** Stable callbacks that push field changes into the undo stack. */
function useFieldHandlers(present: EditorSnapshot, push: (s: EditorSnapshot) => void): {
  handleNameChange: (value: string) => void;
  handleDescriptionChange: (value: string) => void;
  handleMenuContentsChange: (value: MenuContents) => void;
} {
  const handleNameChange = useCallback((value: string) => { push({ ...present, name: value }); }, [present, push]);
  const handleDescriptionChange = useCallback((value: string) => { push({ ...present, description: value }); }, [present, push]);
  const handleMenuContentsChange = useCallback((value: MenuContents) => { push({ ...present, menuContents: value }); }, [present, push]);
  return { handleNameChange, handleDescriptionChange, handleMenuContentsChange };
}

export function useFullMenuEditorState(
  visible: boolean,
  item: TenantMenusDto | null,
): UseFullMenuEditorStateReturn {
  const initialSnapshot = buildSnapshot(item);
  const { present, canUndo, canRedo, push, undo, redo, reset } = useUndoRedo(initialSnapshot);
  const { activeTab, setActiveTab, nameError, setNameError, activeCategoryId, setActiveCategoryId } = useTabState(visible, item, reset, canUndo);
  const collapseAllRef = useRef<(() => void) | null>(null);
  const { handleNameChange, handleDescriptionChange, handleMenuContentsChange } = useFieldHandlers(present, push);

  useStaleCategoryGuard(present.menuContents.categories, activeCategoryId, setActiveCategoryId);

  const shortcutParams = useMemo(() => ({ onUndo: undo, onRedo: redo, enabled: visible }), [undo, redo, visible]);
  useEditorKeyboardShortcuts(shortcutParams);

  return {
    activeTab, setActiveTab,
    name: present.name, description: present.description, menuContents: present.menuContents,
    nameError, setNameError,
    handleNameChange, handleDescriptionChange, handleMenuContentsChange,
    canUndo, canRedo, undo, redo,
    activeCategoryId, setActiveCategoryId, collapseAllRef,
  };
}
