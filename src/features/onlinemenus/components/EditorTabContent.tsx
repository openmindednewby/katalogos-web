/**
 * Renders the active tab content inside the FullMenuEditor.
 * Extracted to reduce cyclomatic complexity of the parent component.
 */
import React from 'react';

import MenuContentEditor from '@/components/OnlineMenus/MenuContentEditor';
import MenuLivePreview from '@/components/OnlineMenus/MenuLivePreview';
import MetadataTab from '@/components/OnlineMenus/MetadataTab';
import type { MenuTheme } from '@/components/OnlineMenus/ThemeSelector';
import { TranslationManager } from '@/components/OnlineMenus/TranslationManager/TranslationManager';
import type { TenantMenusDto } from '@/server/autoGeneratedHooks/onlinemenu/models/tenantMenusDto';
import type { MenuContents } from '@/types/menuTypes';

import VersionHistoryPanel from './VersionHistoryPanel';
import EditorTab from '../enums/EditorTab';

interface EditorTabContentProps {
  activeTab: EditorTab;
  item: TenantMenusDto | null;
  name: string;
  description: string;
  menuContents: MenuContents;
  nameError: string;
  backgroundColor: string;
  borderColor: string;
  errorColor: string;
  textColor: string;
  textSecondary: string;
  hasPremiumThemes: boolean;
  primaryColor: string;
  onMenuRestored: () => void;
  setNameError: (error: string) => void;
  onNameChange: (value: string) => void;
  onDescriptionChange: (value: string) => void;
  onMenuContentsChange: (value: MenuContents) => void;
  onThemeSelect: (theme: MenuTheme) => void;
  onPremiumThemeBlocked: () => void;
  onCategoryFocus?: (id: string | null) => void;
  collapseAllRef?: React.MutableRefObject<(() => void) | null>;
}

const EditorTabContent = (props: EditorTabContentProps): React.ReactElement | null => {
  const { activeTab } = props;

  if (activeTab === EditorTab.Metadata)
    return (
      <MetadataTab
        backgroundColor={props.backgroundColor} borderColor={props.borderColor}
        description={props.description} errorColor={props.errorColor}
        hasPremiumThemes={props.hasPremiumThemes} menuContents={props.menuContents}
        name={props.name} nameError={props.nameError}
        setDescription={props.onDescriptionChange} setMenuContents={props.onMenuContentsChange}
        setName={props.onNameChange} setNameError={props.setNameError}
        textColor={props.textColor} textSecondary={props.textSecondary}
        onPremiumThemeBlocked={props.onPremiumThemeBlocked} onThemeSelect={props.onThemeSelect}
      />
    );

  if (activeTab === EditorTab.Content)
    return (
      <MenuContentEditor
        collapseAllRef={props.collapseAllRef} contents={props.menuContents}
        menuExternalId={props.item?.externalId}
        onCategoryFocus={props.onCategoryFocus} onChange={props.onMenuContentsChange}
      />
    );

  if (activeTab === EditorTab.Preview)
    return <MenuLivePreview contents={props.menuContents} menuDescription={props.description} menuName={props.name} />;

  if (activeTab === EditorTab.Translations)
    return <TranslationManager menuExternalId={props.item?.externalId} />;

  return (
    <VersionHistoryPanel
      backgroundColor={props.backgroundColor}
      borderColor={props.borderColor}
      menuId={props.item?.externalId ?? ''}
      primaryColor={props.primaryColor}
      textColor={props.textColor}
      onMenuRestored={props.onMenuRestored}
    />
  );
};

export default EditorTabContent;
