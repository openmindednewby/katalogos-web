import React from 'react';

import { StyleSheet, Text, TextInput, TouchableOpacity, View } from 'react-native';

import { FM } from '@/localization/helpers';
import type { Option } from '@/server/autoGeneratedHooks/questioner/models';
import { useTheme } from '@/theme/hooks/useTheme';

interface Props {
  options: Option[];
  onChange: (opts: Option[]) => void;
}

const styles = StyleSheet.create({
  optionRow: {
    marginBottom: 6,
  },
  input: {
    padding: 6,
  },
  inputSpacing: {
    marginTop: 4,
  },
});

const OptionEditor = ({ options, onChange }: Props): React.ReactElement => {
  const { theme } = useTheme();
  const primary = theme.palette.primary['500'];
  const errorColor = theme.semantic.error['500'];

  function setOpt(idx: number, key: keyof Option, value: string): void {
    const copy = [...options];
    copy[idx] = { ...copy[idx], [key]: value };
    onChange(copy);
  }

  function add(): void {
    onChange([...options, { label: FM('quizTemplates.option'), value: '' }]);
  }

  function remove(idx: number): void {
    const copy = [...options];
    copy.splice(idx, 1);
    onChange(copy);
  }

  const removeTextStyle = React.useMemo(() => ({ color: errorColor }), [errorColor]);
  const addTextStyle = React.useMemo(() => ({ color: primary }), [primary]);

  return (
    <View>
      {options.map((o, i) => (
        // eslint-disable-next-line react/no-array-index-key -- Options lack stable IDs; index prevents key collisions on blank entries
        <View key={`${i}-${o.value ?? ''}|${o.label ?? ''}`} style={styles.optionRow}>
          <TextInput
            accessibilityHint={FM('quizTemplates.optionLabelInputHint')}
            accessibilityLabel={FM('quizTemplates.optionLabelInputLabel')}
            placeholder={FM('quizTemplates.optionLabel')}
            style={styles.input}
            value={o.label ?? ''}
            onChangeText={(v) => setOpt(i, 'label', v)}
          />
          <TextInput
            accessibilityHint={FM('quizTemplates.optionValueInputHint')}
            accessibilityLabel={FM('quizTemplates.optionValueInputLabel')}
            placeholder={FM('quizTemplates.optionValue')}
            style={[styles.input, styles.inputSpacing]}
            value={o.value ?? ''}
            onChangeText={(v) => setOpt(i, 'value', v)}
          />
          <TouchableOpacity accessibilityRole="button" onPress={() => remove(i)}>
            <Text style={removeTextStyle}>{FM('quizTemplates.removeOption')}</Text>
          </TouchableOpacity>
        </View>
      ))}

      <TouchableOpacity accessibilityRole="button" onPress={add}>
        <Text style={addTextStyle}>{FM('quizTemplates.addOption')}</Text>
      </TouchableOpacity>
    </View>
  );
};

export default OptionEditor;
