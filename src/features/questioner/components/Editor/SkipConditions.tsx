import React from 'react';

import { StyleSheet, Text, TextInput, TouchableOpacity, View } from 'react-native';

import { FM } from '@/localization/helpers';
import type { SkipCondition } from '@/server/autoGeneratedHooks/questioner/models';
import { useTheme } from '@/theme/hooks/useTheme';

interface Props {
  skipConditions: SkipCondition[];
  onChange: (s: SkipCondition[]) => void;
}

const styles = StyleSheet.create({
  row: {
    marginBottom: 8,
  },
  input: {
    padding: 6,
  },
  inputSpacing: {
    marginTop: 4,
  },
});

const SkipConditionsEditor = ({ skipConditions, onChange }: Props): React.ReactElement => {
  const { theme } = useTheme();
  const primary = theme.palette.primary['500'];
  const errorColor = theme.semantic.error['500'];

  function setField(idx: number, key: keyof SkipCondition, value: string | null): void {
    const copy = [...skipConditions];
    copy[idx] = { ...copy[idx], [key]: value };
    onChange(copy);
  }

  function add(): void {
    onChange([...skipConditions, { questionId: '', questionAnswer: '' }]);
  }

  function remove(idx: number): void {
    const copy = [...skipConditions];
    copy.splice(idx, 1);
    onChange(copy);
  }

  const removeTextStyle = React.useMemo(() => ({ color: errorColor }), [errorColor]);
  const addTextStyle = React.useMemo(() => ({ color: primary }), [primary]);

  return (
    <View>
      {skipConditions.map((s, i) => (
        // eslint-disable-next-line react/no-array-index-key -- Skip conditions lack stable IDs; index prevents key collisions on blank entries
        <View key={`${i}-${s.questionId ?? ''}|${s.questionAnswer ?? ''}`} style={styles.row}>
          <TextInput
            accessibilityHint={FM('quizTemplates.skipQuestionIdInputHint')}
            accessibilityLabel={FM('quizTemplates.skipQuestionIdInputLabel')}
            placeholder={FM('quizTemplates.skipQuestionId')}
            style={styles.input}
            value={s.questionId ?? ''}
            onChangeText={(v) => setField(i, 'questionId', v)}
          />
          <TextInput
            accessibilityHint={FM('quizTemplates.skipAnswerInputHint')}
            accessibilityLabel={FM('quizTemplates.skipAnswerInputLabel')}
            placeholder={FM('quizTemplates.skipAnswer')}
            style={[styles.input, styles.inputSpacing]}
            value={s.questionAnswer ?? ''}
            onChangeText={(v) => setField(i, 'questionAnswer', v)}
          />
          <TouchableOpacity accessibilityRole="button" onPress={() => remove(i)}>
            <Text style={removeTextStyle}>{FM('quizTemplates.removeSkip')}</Text>
          </TouchableOpacity>
        </View>
      ))}

      <TouchableOpacity accessibilityRole="button" onPress={add}>
        <Text style={addTextStyle}>{FM('quizTemplates.addSkip')}</Text>
      </TouchableOpacity>
    </View>
  );
};

export default SkipConditionsEditor;
