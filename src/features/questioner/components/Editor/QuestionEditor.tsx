import React from 'react';

import { StyleSheet, TextInput, View } from 'react-native';


import Checkbox from '@/components/Shared/Checkbox';
import ChoicePill from '@/components/Shared/ChoicePill';
import FieldLabel from '@/components/Shared/FieldLabel';
import Heading from '@/components/Shared/Heading';
import Section from '@/components/Shared/Section';
import { FM } from '@/localization/helpers';
import type { Question } from '@/server/autoGeneratedHooks/questioner/models';
import { QuestionType } from '@/server/autoGeneratedHooks/questioner/models/questionType';
import { useTheme } from '@/theme/hooks/useTheme';

import AnswerEditor from './AnswerEditor';
import OptionEditor from './OptionEditor';
import SkipConditionsEditor from './SkipConditions';

interface Props {
  question: Question;
  onChange: (q: Question) => void;
  enableAnswerSection: boolean;
}

const styles = StyleSheet.create({
  nameInput: {
    padding: 6,
    marginTop: 6,
  },
  typeRow: {
    flexDirection: 'row',
    marginTop: 6,
    flexWrap: 'wrap',
  },
});

const QuestionEditor = ({ question, onChange, enableAnswerSection }: Props): React.ReactElement => {
  const { theme } = useTheme();
  const colors = theme.colors;

  function setField<K extends keyof Question>(key: K, value: Question[K]): void {
    onChange({ ...question, [key]: value });
  }

  const questionTypeEntries = React.useMemo((): Array<[string, Question['type']]> => {
    const entries: Array<[string, Question['type']]> = Object.entries(QuestionType);
    return entries;
  }, []);

  const questionName = question.name ?? '';
  const questionIdLabel = question.id ?? '';
  const questionOptions = Array.isArray(question.options) ? question.options : [];
  const questionSkipConditions = Array.isArray(question.skipConditions) ? question.skipConditions : [];
  const isRequired = question.isRequired === true;

  const inputStyle = React.useMemo(
    () => [styles.nameInput, { backgroundColor: colors.surface, color: colors.text }],
    [colors.surface, colors.text],
  );

  return (
    <Section>
      <Heading text={FM('quizTemplates.question', { p1: questionIdLabel })} />
      <TextInput
        accessibilityHint={FM('quizTemplates.questionNameInputHint')}
        accessibilityLabel={FM('quizTemplates.questionNameInputLabel')}
        placeholder={FM('quizTemplates.questionName')}
        style={inputStyle}
        value={questionName}
        onChangeText={(v) => setField('name', v)}
      />

      <FieldLabel text={FM('quizTemplates.type')} />
      <View style={styles.typeRow}>
        {questionTypeEntries.map(([key, value]) => (
          <ChoicePill
            key={String(key)}
            label={FM(`quizTemplates.type.${String(key)}`)}
            selected={question.type === value}
            onPress={() => setField('type', value)}
          />
        ))}
      </View>
      <FieldLabel text={FM('quizTemplates.options')} />
      <OptionEditor options={questionOptions} onChange={(opts) => setField('options', opts)} />

      {enableAnswerSection ? (
        <>
          <FieldLabel text={FM('quizTemplates.answer')} />
          <AnswerEditor answer={question.answer} onChange={(a) => setField('answer', a)} />
        </>
      ) : null}

      <FieldLabel text={FM('quizTemplates.skipConditions')} />
      <SkipConditionsEditor
        skipConditions={questionSkipConditions}
        onChange={(s) => setField('skipConditions', s)}
      />

      <Checkbox
        baseLabel={FM('quizTemplates.required')}
        isChecked={isRequired}
        onPress={() => setField('isRequired', !isRequired)}
      />
    </Section>
  );
};

export default QuestionEditor;
