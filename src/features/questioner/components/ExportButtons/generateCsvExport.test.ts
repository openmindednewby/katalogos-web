import { QuestionType } from '@/server/autoGeneratedHooks/questioner/models/questionType';
import { type CompletedQuestionerWithUser } from '@/server/customHooks/useCompletedQuestionersWithUsers';

import { generateCsvExport } from './generateCsvExport';

jest.mock("react-native", () => ({
  Platform: { OS: "web" },
}));

describe("generateCsvExport", () => {
  let restoreUrl: (() => void) | undefined;

  async function blobToText(blob: Blob): Promise<string> {
    if (typeof blob.text === 'function') return await blob.text();
    return await new Promise<string>((resolve, reject) => {
      const reader = new FileReader();
      reader.onload = () => resolve(String(reader.result ?? ""));
      reader.onerror = () => reject(reader.error instanceof Error ? reader.error : new Error('FileReader error'));
      reader.readAsText(blob);
    });
  }

  function setupDomSpies(): {
    clickSpy: jest.Mock;
    createObjectURLSpy: jest.Mock;
    revokeObjectURLSpy: jest.Mock;
  } {
    const clickSpy = jest.fn<undefined, []>();

    const originalCreateElement = document.createElement.bind(document);
    jest
      .spyOn(document, "createElement")
      .mockImplementation((tagName: string) => {
        const el = originalCreateElement(tagName);
        if (String(tagName).toLowerCase() === 'a') {
          const anchor = el as HTMLAnchorElement;
          anchor.click = clickSpy as unknown as () => void;
        }
        return el;
      });

    const originalURL = globalThis.URL;
    const createObjectURLSpy = jest.fn((): string => 'blob:mock-url');
    const revokeObjectURLSpy = jest.fn();
    const baseURL = originalURL;
    Object.defineProperty(globalThis, "URL", {
      value: {
        ...baseURL,
        createObjectURL: createObjectURLSpy,
        revokeObjectURL: revokeObjectURLSpy,
      },
      configurable: true,
    });
    restoreUrl = () => {
      Object.defineProperty(globalThis, "URL", { value: originalURL, configurable: true });
    };

    return { clickSpy, createObjectURLSpy, revokeObjectURLSpy };
  }

  afterEach(() => {
    jest.restoreAllMocks();
    restoreUrl?.();
    restoreUrl = undefined;
  });

  it("should generate valid CSV from records", async () => {
    const { clickSpy, createObjectURLSpy, revokeObjectURLSpy } = setupDomSpies();

    const records: CompletedQuestionerWithUser[] = [
      {
        id: "r1",
        externalId: "ext-r1",
        name: "Survey 1",
        completedAt: "2026-01-01",
        userId: "u1",
        user: {
          username: "user1",
          email: "user1@example.com",
          firstName: "Alice",
          lastName: "Smith",
        },
        contents: {
          questions: [
            { id: "q1", name: "Age", answer: { numericValue: 30 } },
            { id: "q2", name: "Fruits", type: QuestionType.MultipleChoice, answer: { multiValues: ["Apple", "Banana"] } },
          ],
        },
      },
      {
        id: "r2",
        externalId: "ext-r2",
        name: "Survey 2",
        completedAt: "2026-01-02",
        userId: "u2",
        user: { username: "user2" },
        contents: {
          questions: [
            { id: "q2", name: "Fruits", type: QuestionType.MultipleChoice, answer: { multiValues: ["Kiwi", "Mango", "Pear"] } },
            { id: "q3", name: "Comment", answer: { stringValue: "OK" } },
          ],
        },
      },
    ];

    generateCsvExport(records);

    expect(createObjectURLSpy).toHaveBeenCalledTimes(1);
    expect(clickSpy).toHaveBeenCalledTimes(1);
    expect(revokeObjectURLSpy).toHaveBeenCalledWith("blob:mock-url");

    const blob = createObjectURLSpy.mock.calls[0]?.[0] as Blob;
    const csv = await blobToText(blob);
    const [header, row1, row2] = csv.split("\n");

    expect(header).toBe(
      [
        '"id"',
        '"name"',
        '"completedAt"',
        '"respondentId"',
        '"respondentUsername"',
        '"respondentEmail"',
        '"respondentFirstName"',
        '"respondentLastName"',
        '"Age"',
        '"Fruits"',
        '"Fruits"',
        '"Fruits"',
        '"Comment"',
      ].join(",")
    );

    expect(row1).toBe(
      [
        '"ext-r1"',
        '"Survey 1"',
        '"2026-01-01"',
        '"u1"',
        '"user1"',
        '"user1@example.com"',
        '"Alice"',
        '"Smith"',
        '"30"',
        '"Apple"',
        '"Banana"',
        '""',
        '""',
      ].join(",")
    );

    expect(row2).toBe(
      [
        '"ext-r2"',
        '"Survey 2"',
        '"2026-01-02"',
        '"u2"',
        '"user2"',
        '""',
        '""',
        '""',
        '""',
        '"Kiwi"',
        '"Mango"',
        '"Pear"',
        '"OK"',
      ].join(",")
    );
  });

  it("should handle empty array input", () => {
    const { clickSpy, createObjectURLSpy } = setupDomSpies();

    generateCsvExport([]);

    expect(createObjectURLSpy).not.toHaveBeenCalled();
    expect(clickSpy).not.toHaveBeenCalled();
  });

  it("should escape special characters in CSV", async () => {
    const { createObjectURLSpy } = setupDomSpies();

    const records: CompletedQuestionerWithUser[] = [
      {
        id: "r1",
        externalId: "ext-r1",
        name: 'Report "One", line\nbreak',
        completedAt: "2026-01-01",
        userId: 'user,1',
        user: {
          username: 'u"ser',
          email: "user@example.com",
          firstName: 'Alice "A"',
          lastName: "Smith",
        },
        contents: {
          questions: [
            { id: "q1", name: "Comment", answer: { stringValue: 'He said "hello", then left' } },
          ],
        },
      },
    ];

    generateCsvExport(records);

    const blob = createObjectURLSpy.mock.calls[0]?.[0] as Blob;
    const csv = await blobToText(blob);

    expect(csv).toContain('"user,1"');
    expect(csv).toContain('"Report ""One"", line\nbreak"');
    expect(csv).toContain('"u""ser"');
    expect(csv).toContain('"Alice ""A"""');
    expect(csv).toContain('"He said ""hello"", then left"');
  });

  it("should use externalId for id column and userId for respondentId column", async () => {
    const { createObjectURLSpy } = setupDomSpies();

    const records: CompletedQuestionerWithUser[] = [
      {
        externalId: "record-ext-123",
        name: "Test",
        userId: "user-456",
        user: { username: "tester" },
        contents: { questions: [] },
      },
    ];

    generateCsvExport(records);

    const blob = createObjectURLSpy.mock.calls[0]?.[0] as Blob;
    const csv = await blobToText(blob);
    const [_header, row] = csv.split("\n");
    const columns = String(row).split(",");
    const idColumn = columns[0];
    const respondentIdColumn = columns[3];

    expect(idColumn).toBe('"record-ext-123"');
    expect(respondentIdColumn).toBe('"user-456"');
  });
});
