import React, { useEffect, useLayoutEffect, useMemo, useRef, useState } from 'react';

import { Alert } from 'react-native';


import ModalShell from '@/components/Shared/ModalShell';
import Tabs from '@/components/Shared/Tabs';
import { FM } from '@/localization/helpers';
import type { QuestionerContents } from '@/server/autoGeneratedHooks/questioner/models/questionerContents';
import type { QuestionerTemplateDto } from '@/server/autoGeneratedHooks/questioner/models/questionerTemplateDto';
import type { UpdateQuestionerTemplateRequest } from '@/server/autoGeneratedHooks/questioner/models/updateQuestionerTemplateRequest';
import { isValueDefined } from '@/utils/is';

import TemplateEditorForm from './TemplateEditorForm';
import TemplateJsonEditor from './TemplateJsonEditor';

const JSON_PARSE_DEBOUNCE_MS = 300;

interface Props {
  visible: boolean;
  item: QuestionerTemplateDto | null;
  onCancel: () => void;
  onSave: (payload: UpdateQuestionerTemplateRequest) => void;
  enableAnswerSection?: boolean;
  readOnly?: boolean;
}

function isQuestionerContents(value: unknown): value is QuestionerContents {
  return typeof value === 'object' && isValueDefined(value) && !Array.isArray(value);
}

function parseContents(text: string): QuestionerContents | null {
  try {
    const parsed: unknown = JSON.parse(text);
    if (isQuestionerContents(parsed)) return parsed;
    return null;
  } catch {
    return null;
  }
}

interface UpdatePayloadParams {
  name: string;
  description: string;
  isActive: boolean;
  contents: QuestionerContents | null;
  itemContents: QuestionerContents | null | undefined;
}

/**
 * Creates the update payload from current state.
 */
function createUpdatePayload({ name, description, isActive, contents, itemContents }: UpdatePayloadParams): UpdateQuestionerTemplateRequest {
  const trimmedDescription = description.trim();
  return {
    name,
    description: trimmedDescription === '' ? null : description,
    isActive,
    contents: contents ?? itemContents ?? null,
  };
}

const TemplateEditorModal = ({ visible, item, onCancel, onSave, enableAnswerSection = false, readOnly = false }: Props): React.ReactElement => {

  // BUG-QUIZ-003 fix: Initialize state directly from the item prop.
  // This component only mounts when isModalVisible is true, and the item prop
  // is always set before isModalVisible becomes true (both set in handleEdit).
  // Initializing from the prop avoids the race condition where TemplateForm
  // captures an empty initial state before the useLayoutEffect can run.
  const initialContents = item?.contents ?? {};
  const [activeTab, setActiveTab] = useState<'form' | 'json'>('form');
  const [jsonText, setJsonText] = useState<string>(() => JSON.stringify(initialContents, null, 2));
  const [contents, setContents] = useState<QuestionerContents | null>(() => initialContents);
  const [name, setName] = useState<string>(() => item?.name ?? '');
  const [description, setDescription] = useState<string>(() => item?.description ?? '');
  const [isActive, setIsActive] = useState<boolean>(() => Boolean(item?.isActive));

  // BUG-QUIZ-006: Track whether we just switched to the JSON tab
  const justSwitchedToJsonRef = useRef(false);
  const prevActiveTabRef = useRef(activeTab);
  const prevItemIdRef = useRef<string | undefined>(item?.externalId);

  // Sync state when the item changes while the modal stays open.
  // On first mount, prevItemIdRef matches item.externalId so this is skipped,
  // avoiding a redundant state update that would fight with the initial values.
  useLayoutEffect(() => {
    const currentId = item?.externalId;
    if (currentId === prevItemIdRef.current) return;
    prevItemIdRef.current = currentId;

    if (!isValueDefined(item)) {
      setContents(null);
      setJsonText('');
      setName('');
      setDescription('');
      setIsActive(false);
      return;
    }

    const nextContents = item.contents ?? {};
    setContents(nextContents);
    setJsonText(JSON.stringify(nextContents, null, 2));
    setName(item.name ?? '');
    setDescription(item.description ?? '');
    setIsActive(Boolean(item.isActive));
    setActiveTab('form');
  }, [item, visible]);

  // BUG-QUIZ-006 fix: Detect tab switch to JSON and set the flag
  useEffect(() => {
    const switchedToJson = prevActiveTabRef.current !== 'json' && activeTab === 'json';
    prevActiveTabRef.current = activeTab;
    if (switchedToJson) justSwitchedToJsonRef.current = true;
  }, [activeTab]);

  // BUG-QUIZ-006 fix: Only sync contents -> jsonText when FIRST switching to JSON tab.
  // While on JSON tab, jsonText is the source of truth. No back-sync from contents.
  useEffect(() => {
    if (!justSwitchedToJsonRef.current) return;
    justSwitchedToJsonRef.current = false;
    setJsonText(JSON.stringify(contents ?? item?.contents ?? {}, null, 2));
  }, [activeTab, contents, item?.contents]);

  // Parse jsonText -> contents with debounce (only while on JSON tab)
  useEffect(() => {
    const isJsonTabEmpty = activeTab !== 'json' || jsonText.trim() === '';
    if (isJsonTabEmpty) return;

    const id = setTimeout(() => {
      const parsed = parseContents(jsonText);
      if (isValueDefined(parsed)) setContents(parsed);
    }, JSON_PARSE_DEBOUNCE_MS);

    return () => clearTimeout(id);
  }, [activeTab, jsonText]);

  function handleTabChange(key: string): void {
    const isValidTab = key === 'form' || key === 'json';
    if (isValidTab) setActiveTab(key);
  }

  function handleSaveFromForm(): void {
    const payload = createUpdatePayload({ name, description, isActive, contents, itemContents: item?.contents });
    onSave(payload);
  }

  function handleSaveFromJson(): void {
    const parsed = parseContents(jsonText);
    if (!isValueDefined(parsed)) {
      Alert.alert(
        FM('quizTemplates.errors.invalidJson'),
        FM('quizTemplates.errors.invalidJsonDescription'),
      );
      return;
    }
    const payload = createUpdatePayload({ name, description, isActive, contents: parsed, itemContents: item?.contents });
    onSave(payload);
  }

  function handleFormChange({ name: n, description: d, isActive: a }: { name: string; description: string; isActive: boolean }): void {
    setName(n);
    setDescription(d);
    setIsActive(a);
  }

  function handleQuestionsChange(qs: QuestionerContents['questions']): void {
    setContents({ ...(contents ?? item?.contents ?? {}), questions: qs });
  }

  const modalTitle = useMemo(() => FM('quizTemplates.edit', FM('tenants.edit')), []);
  const tabLabels = useMemo(
    () => [
      { key: 'form', label: FM('quizTemplates.tab.form') },
      { key: 'json', label: FM('quizTemplates.tab.json') },
    ],
    [],
  );

  const questions = contents?.questions ?? item?.contents?.questions ?? [];

  return (
    <ModalShell title={modalTitle} visible={visible} onCancel={onCancel}>
      <Tabs activeKey={activeTab} tabs={tabLabels} onChange={handleTabChange} />

      {activeTab === 'form' ? (
        <TemplateEditorForm
          description={description}
          enableAnswerSection={enableAnswerSection}
          isActive={isActive}
          itemKey={item?.externalId}
          name={name}
          questions={questions}
          readOnly={readOnly}
          saving={false}
          onCancel={onCancel}
          onChange={handleFormChange}
          onQuestionsChange={handleQuestionsChange}
          onSave={handleSaveFromForm}
        />
      ) : (
        <TemplateJsonEditor jsonText={jsonText} readOnly={readOnly} onCancel={onCancel} onJsonTextChange={setJsonText} onSave={handleSaveFromJson} />
      )}
    </ModalShell>
  );
};

export default TemplateEditorModal;
