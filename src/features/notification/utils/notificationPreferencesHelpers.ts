



/**
 * Get default notification preferences
 */
/**
 * Helper functions for NotificationPreferencesScreen.
 */
import type {
  NotificationPreferences,
  NotificationCategoryKey,
} from '@/lib/hooks/notification';
import { type DisplayPreference } from '@/server/autoGeneratedHooks/notification/models';


const DEFAULT_DISPLAY_PREFERENCE_BOTH: DisplayPreference = 3;

export function getDefaultPreferences(): NotificationPreferences {
  const defaultCategory = { enabled: true, displayPreference: DEFAULT_DISPLAY_PREFERENCE_BOTH };
  return {
    notificationsEnabled: true,
    quietHoursEnabled: false,
    quietHoursStart: null,
    quietHoursEnd: null,
    quietHoursTimezone: null,
    categoryPreferences: {
      questionnaireSubmitted: { ...defaultCategory },
      templateUpdated: { ...defaultCategory },
      userInvited: { ...defaultCategory },
      menuUpdated: { ...defaultCategory },
      paymentDue: { ...defaultCategory },
    },
  };
}

/**
 * Category label translation keys
 */
export const CATEGORY_LABEL_KEYS: Record<NotificationCategoryKey, string> = {
  questionnaireSubmitted: 'settings.notificationPreferences.categories.questionnaireSubmitted',
  templateUpdated: 'settings.notificationPreferences.categories.templateUpdated',
  userInvited: 'settings.notificationPreferences.categories.userInvited',
  menuUpdated: 'settings.notificationPreferences.categories.menuUpdated',
  paymentDue: 'settings.notificationPreferences.categories.paymentDue',
};
