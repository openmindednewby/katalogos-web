/**
 * Display preference dropdown component.
 * Thin wrapper around ModalDropdown, typed to DisplayPreference.
 */
import React, { useCallback } from 'react';

import ModalDropdown from '@/components/Shared/ModalDropdown';
import type { DisplayPreference as DisplayPreferenceType } from '@/lib/hooks/notification';
import { FM } from '@/localization/helpers';
import { DisplayPreference } from '@/server/autoGeneratedHooks/notification/models/displayPreference';


const DISPLAY_PREFERENCE_VALUES: ReadonlySet<number> = new Set([
  DisplayPreference.None,
  DisplayPreference.InApp,
  DisplayPreference.OsNotification,
  DisplayPreference.Both,
]);

/**
 * Type guard to check if a number is a valid DisplayPreference.
 */
function isDisplayPreference(value: number): value is DisplayPreferenceType {
  return DISPLAY_PREFERENCE_VALUES.has(value);
}

interface DropdownOption {
  readonly label: string;
  readonly value: number;
}

interface Props {
  testID?: string;
  value: DisplayPreferenceType;
  options: readonly DropdownOption[];
  onChange: (value: DisplayPreferenceType) => void;
}

const DisplayPreferenceDropdown = ({
  testID = 'display-preference-dropdown',
  value,
  options,
  onChange,
}: Props): React.ReactElement => {
  const handleChange = useCallback(
    (newValue: number) => {
      if (isDisplayPreference(newValue)) onChange(newValue);
    },
    [onChange],
  );

  return (
    <ModalDropdown<number>
      accessibilityHint={FM('settings.notificationPreferences.toggleCategoryHint')}
      accessibilityLabel={FM('settings.notificationPreferences.displayAs')}
      options={options}
      testID={testID}
      value={value}
      onChange={handleChange}
    />
  );
};

export default DisplayPreferenceDropdown;
