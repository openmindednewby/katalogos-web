/**
 * Unit tests for NotificationPreferencesScreen.
 * Tests logic and behavior, not rendering.
 */

import { renderHook, act } from '@testing-library/react-native';

import {
  useGetNotificationPreferences,
  useUpdateNotificationPreferences,
} from '@/lib/hooks/notification';
import type { NotificationPreferences } from '@/lib/hooks/notification/types';
import { DisplayPreference } from '@/server/autoGeneratedHooks/notification/models/displayPreference';


// Mock the hooks
jest.mock('@/lib/hooks/notification');
jest.mock('@/lib/notifications');

const mockUseGetNotificationPreferences = useGetNotificationPreferences as jest.Mock;
const mockUseUpdateNotificationPreferences = useUpdateNotificationPreferences as jest.Mock;

describe('NotificationPreferencesScreen logic', () => {
  const mockUpdatePreferences = jest.fn();

  const defaultPreferences: NotificationPreferences = {
    notificationsEnabled: true,
    quietHoursEnabled: false,
    quietHoursStart: null,
    quietHoursEnd: null,
    quietHoursTimezone: null,
    categoryPreferences: {
      questionnaireSubmitted: { enabled: true, displayPreference: DisplayPreference.Both },
      templateUpdated: { enabled: true, displayPreference: DisplayPreference.Both },
      userInvited: { enabled: true, displayPreference: DisplayPreference.Both },
      menuUpdated: { enabled: true, displayPreference: DisplayPreference.Both },
      paymentDue: { enabled: true, displayPreference: DisplayPreference.Both },
    },
  };

  beforeEach(() => {
    jest.clearAllMocks();
    mockUseGetNotificationPreferences.mockReturnValue({
      preferences: defaultPreferences,
      isLoading: false,
      isError: false,
    });
    mockUseUpdateNotificationPreferences.mockReturnValue({
      updatePreferences: mockUpdatePreferences,
      isPending: false,
      isError: false,
      isSuccess: false,
    });
  });

  it('should show loading state when preferences are loading', () => {
    mockUseGetNotificationPreferences.mockReturnValue({
      preferences: undefined,
      isLoading: true,
      isError: false,
    });

    const result = useGetNotificationPreferences();
    expect(result.isLoading).toBe(true);
    expect(result.preferences).toBeUndefined();
  });

  it('should show error state when preferences fail to load', () => {
    mockUseGetNotificationPreferences.mockReturnValue({
      preferences: undefined,
      isLoading: false,
      isError: true,
    });

    const result = useGetNotificationPreferences();
    expect(result.isError).toBe(true);
    expect(result.preferences).toBeUndefined();
  });

  it('should return preferences when loaded successfully', () => {
    const result = useGetNotificationPreferences();
    expect(result.preferences).toEqual(defaultPreferences);
    expect(result.isLoading).toBe(false);
    expect(result.isError).toBe(false);
  });

  it('should call updatePreferences with correct data when saving', () => {
    const { result } = renderHook(() => useUpdateNotificationPreferences());

    act(() => {
      result.current.updatePreferences(defaultPreferences);
    });

    expect(mockUpdatePreferences).toHaveBeenCalledWith(defaultPreferences);
  });

  it('should return isPending true when update is in progress', () => {
    mockUseUpdateNotificationPreferences.mockReturnValue({
      updatePreferences: mockUpdatePreferences,
      isPending: true,
      isError: false,
      isSuccess: false,
    });

    const result = useUpdateNotificationPreferences();
    expect(result.isPending).toBe(true);
  });

  it('should call onSuccess callback when update succeeds', () => {
    const onSuccess = jest.fn();
    mockUseUpdateNotificationPreferences.mockImplementation((callbacks) => {
      return {
        updatePreferences: () => {
          callbacks?.onSuccess?.();
        },
        isPending: false,
        isError: false,
        isSuccess: true,
      };
    });

    const { result } = renderHook(() =>
      useUpdateNotificationPreferences({ onSuccess })
    );

    act(() => {
      result.current.updatePreferences(defaultPreferences);
    });

    expect(onSuccess).toHaveBeenCalled();
  });

  it('should call onError callback when update fails', () => {
    const testError = new Error('Update failed');
    const onError = jest.fn();
    mockUseUpdateNotificationPreferences.mockImplementation((callbacks) => {
      return {
        updatePreferences: () => {
          callbacks?.onError?.(testError);
        },
        isPending: false,
        isError: true,
        isSuccess: false,
        error: testError,
      };
    });

    const { result } = renderHook(() =>
      useUpdateNotificationPreferences({ onError })
    );

    act(() => {
      result.current.updatePreferences(defaultPreferences);
    });

    expect(onError).toHaveBeenCalledWith(testError);
  });
});

describe('Category preference toggle logic', () => {
  it('should transform enabled=false to displayPreference=0 when saving', () => {
    const disabledPreferences = {
      notificationsEnabled: true,
      quietHoursEnabled: false,
      quietHoursStart: null,
      quietHoursEnd: null,
      quietHoursTimezone: null,
      categoryPreferences: {
        questionnaireSubmitted: { enabled: false, displayPreference: 3 },
        templateUpdated: { enabled: true, displayPreference: 1 },
        userInvited: { enabled: false, displayPreference: 2 },
        menuUpdated: { enabled: true, displayPreference: 3 },
        paymentDue: { enabled: false, displayPreference: 3 },
      },
    };

    // When a category is disabled, its display preference should be 0 (None) in the request
    const EXPECTED_DISABLED_DISPLAY = 0;

    // Verify the expected transformation logic
    expect(disabledPreferences.categoryPreferences.questionnaireSubmitted.enabled).toBe(false);
    expect(EXPECTED_DISABLED_DISPLAY).toBe(0);

    expect(disabledPreferences.categoryPreferences.templateUpdated.enabled).toBe(true);
  });
});

describe('Quiet hours toggle logic', () => {
  it('should disable quiet hours when notifications are disabled', () => {
    const preferencesWithNotificationsOff = {
      notificationsEnabled: false,
      quietHoursEnabled: true,
      quietHoursStart: '22:00',
      quietHoursEnd: '08:00',
      quietHoursTimezone: 'UTC',
      categoryPreferences: {
        questionnaireSubmitted: { enabled: true, displayPreference: 3 },
        templateUpdated: { enabled: true, displayPreference: 3 },
        userInvited: { enabled: true, displayPreference: 3 },
        menuUpdated: { enabled: true, displayPreference: 3 },
        paymentDue: { enabled: true, displayPreference: 3 },
      },
    };

    // When notifications are disabled, quiet hours should be effectively disabled
    const notificationsEnabled = preferencesWithNotificationsOff.notificationsEnabled;
    const quietHoursCanBeActive = notificationsEnabled;
    expect(quietHoursCanBeActive).toBe(false);
  });
});
