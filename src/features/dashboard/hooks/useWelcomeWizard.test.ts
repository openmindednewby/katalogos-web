/**
 * Tests for useWelcomeWizard hook.
 * Focuses on step navigation, localStorage, API mutation calls, and server-side state.
 */
import { renderHook, act } from '@testing-library/react-native';

import type { DashboardData } from '@/components/Dashboard/types';
import WizardStep from '@/shared/enums/WizardStep';

import { useWelcomeWizard, type PreferencesInput } from './useWelcomeWizard';


const mockMutate = jest.fn();
const mockCreateMenuMutate = jest.fn();
const mockUseCurrentTenant = jest.fn();

jest.mock('@/auth/AuthProvider', () => ({
  useAuth: () => ({ userInfo: { tenantId: 'tenant-1' } }),
}));

jest.mock('@/hooks/tenants', () => ({
  useCurrentTenant: (): unknown => mockUseCurrentTenant(),
}));

jest.mock('@/server/autoGeneratedHooks/identity/tenants/tenants', () => ({
  useIdentityServiceAPITenantsUpdateTenant: () => ({
    mutate: mockMutate,
    isPending: false,
  }),
}));

jest.mock('@/server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus', () => ({
  useOnlineMenuWebTenantMenusCreate: () => ({
    mutate: mockCreateMenuMutate,
    isPending: false,
  }),
}));

const EMPTY_DASHBOARD: DashboardData = {
  menuCount: 0,
  templateCount: 0,
  isLoading: false,
  hasMenus: false,
  hasTemplates: false,
  isEmpty: true,
};

const POPULATED_DASHBOARD: DashboardData = {
  menuCount: 1,
  templateCount: 0,
  isLoading: false,
  hasMenus: true,
  hasTemplates: false,
  isEmpty: false,
};

const WIZARD_KEY = 'menuflow_onboarding_completed';

function makePreferences(overrides?: Partial<PreferencesInput>): PreferencesInput {
  return {
    data: undefined,
    isLoading: false,
    updatePreferences: jest.fn(),
    ...overrides,
  };
}

describe('useWelcomeWizard', () => {
  beforeEach(() => {
    localStorage.clear();
    mockMutate.mockReset();
    mockCreateMenuMutate.mockReset();
    mockUseCurrentTenant.mockReset();
    // Default: no tenant name set yet (user pre-dates the /register flow).
    mockUseCurrentTenant.mockReturnValue({ tenantName: null, isLoading: false });
  });

  it('shows wizard when dashboard is empty and not completed', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    expect(result.current.isWizardVisible).toBe(true);
    expect(result.current.step).toBe(WizardStep.BusinessName);
  });

  it('hides wizard when dashboard is not empty', () => {
    const { result } = renderHook(() => useWelcomeWizard(POPULATED_DASHBOARD, makePreferences()));
    expect(result.current.isWizardVisible).toBe(false);
  });

  it('hides wizard when localStorage flag is set', () => {
    localStorage.setItem(WIZARD_KEY, 'true');
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    expect(result.current.isWizardVisible).toBe(false);
  });

  it('skips step 1 and advances to step 2', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.handleSkip(); });
    expect(result.current.step).toBe(WizardStep.Logo);
  });

  it('skips step 2 and advances to step 3', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.handleSkip(); });
    expect(result.current.step).toBe(WizardStep.CreateMenu);
  });

  it('skips step 3 and advances to completed', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.handleSkip(); });
    expect(result.current.step).toBe(WizardStep.Completed);
  });

  it('calls updateTenant on step 1 continue with business name', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.setBusinessName('Pizza Place'); });
    act(() => { result.current.handleContinue(); });
    expect(mockMutate).toHaveBeenCalledWith(
      { data: { tenantId: 'tenant-1', name: 'Pizza Place' } },
      expect.objectContaining({ onSuccess: expect.any(Function) }),
    );
  });

  it('advances step 1 without API call when business name is empty', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.handleContinue(); });
    expect(mockMutate).not.toHaveBeenCalled();
    expect(result.current.step).toBe(WizardStep.Logo);
  });

  it('calls createMenu on step 3 continue', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.setMenuName('Lunch Menu'); });
    act(() => { result.current.handleContinue(); });
    expect(mockCreateMenuMutate).toHaveBeenCalledWith(
      { data: { name: 'Lunch Menu', description: null, templateSlug: undefined } },
      expect.objectContaining({ onSuccess: expect.any(Function) }),
    );
  });

  it('does not call createMenu when menu name is empty', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.handleContinue(); });
    expect(mockCreateMenuMutate).not.toHaveBeenCalled();
  });

  it('sets localStorage flag on handleComplete', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.handleComplete(); });
    expect(localStorage.getItem(WIZARD_KEY)).toBe('true');
    expect(result.current.isWizardVisible).toBe(false);
  });

  it('passes description to createMenu when provided', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.setMenuName('Dinner'); });
    act(() => { result.current.setMenuDescription('Evening specials'); });
    act(() => { result.current.handleContinue(); });
    expect(mockCreateMenuMutate).toHaveBeenCalledWith(
      { data: { name: 'Dinner', description: 'Evening specials', templateSlug: undefined } },
      expect.objectContaining({ onSuccess: expect.any(Function) }),
    );
  });

  it('passes templateSlug to createMenu when a template is selected', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.handleSkip(); });
    act(() => { result.current.setMenuName('My Cafe'); });
    act(() => { result.current.setSelectedTemplateSlug('cafe'); });
    act(() => { result.current.handleContinue(); });
    expect(mockCreateMenuMutate).toHaveBeenCalledWith(
      { data: { name: 'My Cafe', description: null, templateSlug: 'cafe' } },
      expect.objectContaining({ onSuccess: expect.any(Function) }),
    );
  });

  it('initializes selectedTemplateSlug as null', () => {
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    expect(result.current.selectedTemplateSlug).toBeNull();
  });

  // --- Server-side state tests ---

  it('hides wizard when server preferences say wizardCompleted is true', () => {
    const prefs = makePreferences({ data: { wizardCompleted: true } });
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, prefs));
    expect(result.current.isWizardVisible).toBe(false);
  });

  it('hides wizard while preferences are loading', () => {
    const prefs = makePreferences({ isLoading: true });
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, prefs));
    expect(result.current.isWizardVisible).toBe(false);
  });

  it('calls updatePreferences with wizardCompleted on handleComplete', () => {
    const mockUpdate = jest.fn();
    const prefs = makePreferences({
      data: { language: 'en', timezone: 'UTC' },
      updatePreferences: mockUpdate,
    });
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, prefs));
    act(() => { result.current.handleComplete(); });
    expect(mockUpdate).toHaveBeenCalledWith({
      language: 'en',
      timezone: 'UTC',
      wizardCompleted: true,
    });
  });

  it('uses localStorage fast-path before server responds', () => {
    localStorage.setItem(WIZARD_KEY, 'true');
    const prefs = makePreferences({ isLoading: true, data: undefined });
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, prefs));
    expect(result.current.isWizardVisible).toBe(false);
  });

  it('shows wizard when server says not completed and localStorage empty', () => {
    const prefs = makePreferences({ data: { wizardCompleted: false } });
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, prefs));
    expect(result.current.isWizardVisible).toBe(true);
  });

  // --- Auto-skip Step 1 when tenant already has a name ---

  it('starts at Step 1 when tenant has no name yet', () => {
    mockUseCurrentTenant.mockReturnValue({ tenantName: null, isLoading: false });
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    expect(result.current.step).toBe(WizardStep.BusinessName);
  });

  it('auto-skips Step 1 to Step 2 (Logo) when tenant already has a name', () => {
    mockUseCurrentTenant.mockReturnValue({ tenantName: "Joe's Pizza", isLoading: false });
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    expect(result.current.step).toBe(WizardStep.Logo);
  });

  it('treats whitespace-only tenant name as missing and starts at Step 1', () => {
    mockUseCurrentTenant.mockReturnValue({ tenantName: '   ', isLoading: false });
    const { result } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    expect(result.current.step).toBe(WizardStep.BusinessName);
  });

  it('auto-skips only once — does not re-skip when user manually returns to Step 1', () => {
    // Start: tenant has a name -> auto-skip to Logo.
    mockUseCurrentTenant.mockReturnValue({ tenantName: "Joe's Pizza", isLoading: false });
    const { result, rerender } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    expect(result.current.step).toBe(WizardStep.Logo);

    // The Step1 sub-component would only ever set step back to BusinessName via
    // the underlying useState API, not via the public surface — but the guard
    // protects against the *render-loop* form of the bug. Re-rendering with
    // the same tenantName must not "stick" the user away from Logo.
    rerender(undefined);
    expect(result.current.step).toBe(WizardStep.Logo);
  });

  it('does not auto-skip when tenant name resolves later (still respects one-shot guard)', () => {
    mockUseCurrentTenant.mockReturnValue({ tenantName: null, isLoading: true });
    const { result, rerender } = renderHook(() => useWelcomeWizard(EMPTY_DASHBOARD, makePreferences()));
    expect(result.current.step).toBe(WizardStep.BusinessName);

    // Tenant name resolves a tick later — we DO honor it on the next render.
    mockUseCurrentTenant.mockReturnValue({ tenantName: "Joe's Pizza", isLoading: false });
    rerender(undefined);
    expect(result.current.step).toBe(WizardStep.Logo);
  });
});
