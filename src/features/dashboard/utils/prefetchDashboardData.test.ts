import { queryClient } from '@/lib/queryClient';

import { prefetchDashboardData } from './prefetchDashboardData';

jest.mock('@/lib/queryClient', () => ({
  queryClient: {
    prefetchQuery: jest.fn().mockResolvedValue(undefined),
  },
}));

jest.mock('@/server/autoGeneratedHooks/identity/me/me', () => ({
  getIdentityServiceAPIMeGetPreferencesQueryOptions: jest.fn(() => ({ queryKey: ['prefs'] })),
  getIdentityServiceAPIMeGetBusinessProfileQueryOptions: jest.fn(() => ({ queryKey: ['profile'] })),
}));

jest.mock('@/server/autoGeneratedHooks/onlinemenu/tenantmenus/tenantmenus', () => ({
  getOnlineMenuWebMenuListQueryOptions: jest.fn(() => ({ queryKey: ['menus'] })),
}));

describe('prefetchDashboardData (Katalogos)', () => {
  beforeEach(() => {
    jest.clearAllMocks();
  });

  it('prefetches preferences, business profile, and menu list (3 queries)', () => {
    prefetchDashboardData();

    const EXPECTED_PREFETCH_COUNT = 3;
    expect(queryClient.prefetchQuery).toHaveBeenCalledTimes(EXPECTED_PREFETCH_COUNT);
  });

  it('prefetches preferences query', () => {
    prefetchDashboardData();

    expect(queryClient.prefetchQuery).toHaveBeenCalledWith(
      expect.objectContaining({ queryKey: ['prefs'] }),
    );
  });

  it('prefetches business profile query', () => {
    prefetchDashboardData();

    expect(queryClient.prefetchQuery).toHaveBeenCalledWith(
      expect.objectContaining({ queryKey: ['profile'] }),
    );
  });

  it('prefetches menu list query', () => {
    prefetchDashboardData();

    expect(queryClient.prefetchQuery).toHaveBeenCalledWith(
      expect.objectContaining({ queryKey: ['menus'] }),
    );
  });

  it('does NOT prefetch questioner templates (Katalogos is the OnlineMenu product)', () => {
    prefetchDashboardData();

    expect(queryClient.prefetchQuery).not.toHaveBeenCalledWith(
      expect.objectContaining({ queryKey: ['templates'] }),
    );
  });

  it('does not throw when prefetchQuery rejects', () => {
    (queryClient.prefetchQuery as jest.Mock).mockRejectedValue(new Error('fail'));

    expect(() => prefetchDashboardData()).not.toThrow();
  });
});
